//-----------------------------------------------------------------------

struct pNotch PitchNotchStruct={0};

void PitchNotchParaUpdate(void)
{
      PitchNotchStruct.W1 =2*PI*(F32)P_PitchNotchFrq/1000.0F;      // Do not miss the 2*pi !!!
      PitchNotchStruct.W2 =2*PI*(F32)P_PitchNotchFrq/1000.0F;      // (W2 = W1)
      PitchNotchStruct.D1 =(F32)P_PitchNotchDampingNom/1000.0F;
      PitchNotchStruct.D2 =(F32)P_PitchNotchDampingDenom/1000.0F;
      PitchNotchStruct.DT =0.02;                      // Sample time 20 mS
      PitchNotchStruct.OK =P_PitchNotchON;               // Flag for notch filter on/off
      PitchNotchStruct.Init=1;
}

static F32 PitchNotch (F32 InputPF)
{
   F32 rWT1, rWT2;
   F32 OutputPF;
   U8 i;
   static F32 K  = 0;
   static F32 Kn = 0;
   static F32 K0 = 0;
   static F32 K1 = 0;
   static F32 K2 = 0;
   static F32 K3 = 0;
   static F32 K4 = 0;

   if (PitchNotchStruct.OK)
   {
      if (PitchNotchStruct.Init == 1)
      {
         rWT1 = PitchNotchStruct.W1 * PitchNotchStruct.DT;
         PitchNotchStruct.Num[0] = 4.0F + 4.0F*PitchNotchStruct.D1*rWT1 + rWT1*rWT1;
         PitchNotchStruct.Num[1] =-8.0F + 2.0F*rWT1*rWT1;
         PitchNotchStruct.Num[2] = 4.0F - 4.0F*PitchNotchStruct.D1*rWT1 + rWT1*rWT1;
         rWT2 = PitchNotchStruct.W2 * PitchNotchStruct.DT;
         PitchNotchStruct.Den[0] = 4.0F + 4.0F*PitchNotchStruct.D2*rWT2 + rWT2*rWT2;
         PitchNotchStruct.Den[1] =-8.0F + 2.0F*rWT2*rWT2;
         PitchNotchStruct.Den[2] = 4.0F - 4.0F*PitchNotchStruct.D2*rWT2 + rWT2*rWT2;

         //For unity DC gain:
         for (i=0; i<=2; i++)
         {
            PitchNotchStruct.Num[i] *= (PitchNotchStruct.W2 * PitchNotchStruct.W2);
            PitchNotchStruct.Den[i] *= (PitchNotchStruct.W1 * PitchNotchStruct.W1);
         }
         PitchNotchStruct.Work[0] = InputPF;
         PitchNotchStruct.Work[1] = InputPF;
         PitchNotchStruct.Work[2] = InputPF;
         PitchNotchStruct.Work[3] = InputPF;

         if (PitchNotchStruct.Den[0] != 0)
            K = 1 / PitchNotchStruct.Den[0];
         else
            K = 0;

         Kn = -K;
         K0 = PitchNotchStruct.Num[0] * K;
         K1 = PitchNotchStruct.Num[1] * K;
         K2 = PitchNotchStruct.Num[2] * K;
         K3 = PitchNotchStruct.Den[1] * Kn;
         K4 = PitchNotchStruct.Den[2] * Kn;
         PitchNotchStruct.Init = 0;
      }
      OutputPF = K0 * InputPF + K1 * PitchNotchStruct.Work[0] + K2 * PitchNotchStruct.Work[1] + K3 * PitchNotchStruct.Work[2] + K4 * PitchNotchStruct.Work[3];

      PitchNotchStruct.Work[3] = PitchNotchStruct.Work[2];
      PitchNotchStruct.Work[2] = OutputPF;
      PitchNotchStruct.Work[1] = PitchNotchStruct.Work[0];
      PitchNotchStruct.Work[0] = InputPF;
      return(OutputPF);
   }
   else

      return(InputPF);
}


static void CalcPitchAngle(void)
{
   static U8 DelayMs = 0;
   static S32 PrevPitchAngle;
   S32 PitchAngleDiff;

   DelayMs += 20;

   if ( DelayMs >= 120)
   {
      PitchAngleDiff = V_PitchAngle - PrevPitchAngle;
      V_PitchAngleSpeed = (PitchAngleDiff * 1000) / 120;
      PrevPitchAngle = V_PitchAngle;
      DelayMs = 0;
   }
}

