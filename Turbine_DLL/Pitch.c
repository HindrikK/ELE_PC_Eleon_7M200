struct pNotch
{
   U8 OK;
   F32 W1;
   F32 W2;
   F32 D1;
   F32 D2;
   F32 DT;
   U8 Init;
   F32 Num[3];
   F32 Den[3];
   F32 Work[4];
};


//-----------------------------------------------------------------------
