static STATUS LocalCtrl( U32 Interval )
{
    STATUS LocalStatus = 0;


// simulate blade movement

#ifndef DLL_SIMU_EXTERNALPITCH  // using internal simulation

    if (DLL_IN_Initialize)
    {
        LimiterPitchAngle1.Init_value = P_PAT_PitchAngleStop;
        LimiterPitchAngle1.Reset = 1;
        LimiterPitchAngle2.Init_value = P_PAT_PitchAngleStop;
        LimiterPitchAngle2.Reset = 1;
        LimiterPitchAngle3.Init_value = P_PAT_PitchAngleStop;
        LimiterPitchAngle3.Reset = 1;
        //HystUpDown_Control(pitchAngleHyst, P_PAT_PitchAngleStop);
    }

    switch (*V_MDSCurrentMode_value)
    {
        case MODE_SAFETY:

            // Emergency pitching - 4 deg/s to 90 deg position
            LimiterPitchAngle1.RateUp = 400;
            LimiterPitchAngle1.RateDown = 400;
            V_MDSActPitchAngleA = RateLim(&LimiterPitchAngle1, P_PAT_PitchAngleStop);
            LimiterPitchAngle2.RateUp = 400;
            LimiterPitchAngle2.RateDown = 400;
            V_MDSActPitchAngleB = RateLim(&LimiterPitchAngle2, P_PAT_PitchAngleStop);
            LimiterPitchAngle3.RateUp = 400;
            LimiterPitchAngle3.RateDown = 400;
            V_MDSActPitchAngleC = RateLim(&LimiterPitchAngle3, P_PAT_PitchAngleStop);
            
            V_PitchAngle = (V_MDSActPitchAngleA + V_MDSActPitchAngleB + V_MDSActPitchAngleC) / 3;
            break;

        default:

            // Pitch angle feedback simulation, dynamics according to pitch speed limit value
            LimiterPitchAngle1.RateUp = *V_PitchSpeedLimit_value;
            LimiterPitchAngle1.RateDown = *V_PitchSpeedLimit_value;
            V_MDSActPitchAngleA = RateLim(&LimiterPitchAngle1, V_PitchAngleSetPoint1);
            LimiterPitchAngle2.RateUp = *V_PitchSpeedLimit_value;
            LimiterPitchAngle2.RateDown = *V_PitchSpeedLimit_value;
            V_MDSActPitchAngleB = RateLim(&LimiterPitchAngle2, V_PitchAngleSetPoint2);
            LimiterPitchAngle3.RateUp = *V_PitchSpeedLimit_value;
            LimiterPitchAngle3.RateDown = *V_PitchSpeedLimit_value;
            V_MDSActPitchAngleC = RateLim(&LimiterPitchAngle3, V_PitchAngleSetPoint3);
            
            V_PitchAngle = (V_MDSActPitchAngleA + V_MDSActPitchAngleB + V_MDSActPitchAngleC) / 3;
            break;
    }
    
#else   // using external simulation

    V_PitchAngle = (DLL_IN_PitchAngle_1 + DLL_IN_PitchAngle_2 + DLL_IN_PitchAngle_3) / 3 * 100;
    
    V_MDSActPitchAngleA = DLL_IN_PitchAngle_1 * 100;
    V_MDSActPitchAngleB = DLL_IN_PitchAngle_2 * 100;
    V_MDSActPitchAngleC = DLL_IN_PitchAngle_3 * 100;
    
#endif


    *V_MDSCurrentMode_value = *V_MDSRequestMode_value;

    return LocalStatus;
}

