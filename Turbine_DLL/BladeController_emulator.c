//************************
// Rate limiter struct
//************************
typedef struct
{
    //Inputs
    BOOL Enable;        // If Enable is false then RateLim() will just output input value, else it will return recalculated(limited) input value.
    BOOL Reset;         // All values are initalized
    BOOL TimeMode;      // TRUE - time delay between RateLim() calls can be different and is calculated from system time. "Time_delta" is not checked in this case.
    // FALSE - RateLim() function call period should be constant and delay value for calculations is taken from "Time_delta".
    U32 Time_delta;     // Fixed time step/delay - used when "TimeMode" = FALSE.
    S32 RateUp;         // Maximum rate of increasing signal per defined time base.
    S32 RateDown;       // Maximum rate of decreasing signal per defined time base.
    U32 TimeBase;       // Base time for "RateUp" and "RateDown" rate values.
    S32 Init_value; // Value after Reset

    //Outputs
    S32 Output;         // Current output value (scaled x100)

    //Internal
    S32 max_newOut;
    time_t TimeStamp_prev;

} SRateLimiter;


//************************
// Rate limiter function
//************************
#define SCALE_VALUE 100     // for preserving precision during calculations without using floating point
							// and to keep track of small value changes, that would be otherwise lost when calling function very often

static S32 RateLim( SRateLimiter* pRateLimiter, S32 Input )
{
    S32 _Input = Input * SCALE_VALUE;

    if ( pRateLimiter->Reset )
    {
        pRateLimiter->Reset = 0;
        pRateLimiter->Output = pRateLimiter->Init_value * SCALE_VALUE;
        pRateLimiter->TimeStamp_prev = OS_GetTimeInMs();
    }

    //-----------------------------------------------------------------------
    //Time Step calculation
    S64 time_del;
    time_t TimeStamp;

    if ( pRateLimiter->TimeMode )
    {
        TimeStamp = OS_GetTimeInMs();
        time_del = (S64)(TimeStamp - pRateLimiter->TimeStamp_prev);
        pRateLimiter->TimeStamp_prev = TimeStamp;
    }
    else
    {
        time_del = (S64)pRateLimiter->Time_delta;
    }
    //-----------------------------------------------------------------------

    if (pRateLimiter->Enable)
    {
        if ( pRateLimiter->TimeBase && time_del )
        {
            if ( _Input > pRateLimiter->Output )
            {
                pRateLimiter->max_newOut = pRateLimiter->Output + (S32)((S64)pRateLimiter->RateUp * SCALE_VALUE * time_del / (S64)pRateLimiter->TimeBase);
                if ( _Input > pRateLimiter->max_newOut )
                {
                    pRateLimiter->Output = pRateLimiter->max_newOut;
                }
                else
                {
                    pRateLimiter->Output = _Input;
                }
            }
            else if ( _Input < pRateLimiter->Output )
            {
                pRateLimiter->max_newOut = pRateLimiter->Output - (S32)((S64)pRateLimiter->RateDown * SCALE_VALUE * time_del / (S64)pRateLimiter->TimeBase);
                if ( _Input < pRateLimiter->max_newOut )
                {
                    pRateLimiter->Output = pRateLimiter->max_newOut;
                }
                else
                {
                    pRateLimiter->Output = _Input;
                }
            }
        }
    }
    else
    {
        pRateLimiter->Output = _Input;
    }

    return pRateLimiter->Output / SCALE_VALUE;
}


