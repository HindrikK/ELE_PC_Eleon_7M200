static U8 StatusSetFunc1113(void)
{
    S32 maxAngle = V_MDSActPitchAngleA;
    S32 minAngle = V_MDSActPitchAngleA;
    
    if (V_MDSActPitchAngleB > maxAngle)
    {
        maxAngle = V_MDSActPitchAngleB;
    }
    if (V_MDSActPitchAngleC > maxAngle)
    {
        maxAngle = V_MDSActPitchAngleC;
    }
    
    if (V_MDSActPitchAngleB < minAngle)
    {
        minAngle = V_MDSActPitchAngleB;
    }
    if (V_MDSActPitchAngleC < minAngle)
    {
        minAngle = V_MDSActPitchAngleC;
    }
    
    if ( (maxAngle - minAngle) >= P_BLC_AngleMaxDeviation)
    {
        return 1;
    }
    
    return 0;
}
