static void Yaw_UntwistRunCW_State(BOOL firstentry)
{
   static int step = 0;
   static int startPosition = 0;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   //-----------------------------------------------------------------------
   if (firstentry)
   {
      step = 0;
      V_YAW_ProcessTimer = 0;
      V_YAW_StartCountdown = P_YAW_NormalStartDelay_1;

      strcpy(V_YAW_State_Txt, "Starting untwisting CW");
      V_YAW_State = YAW_UNTWIST_CW;
      OS_StatusCodeSetAutomatic(715);   // set "Cable autounwind" status code
   }


   //-----------------------------------------------------------------------
   //CONTROL
   //-----------------------------------------------------------------------
   switch (Yaw_Mode)
   {
      case YAW_NORMAL_MODE:
      case YAW_STRONG_MODE:
      case YAW_SOFT_MODE:

         switch (step)
         {
            case 0:     // By now, turbine has been stopped by active untwisting status code. Wait for rotor speed to drop below 2,00 rpm.
            {
               if (V_ControllerRpm < 2000)
               {
                  step = 1;
               }
               break;
            }

            case 1:
            {
               Release_AllBrakes();
               Run_AllMotors_SpdTrq(true, P_YAWM_UntwistSpeedPct, P_YAWM_NormalTorqueMotorPct, P_YAWM_NormalTorqueGenPct);
               step = 2;

               break;
            }

            case 2:
            {
               int result;

               strcpy(V_YAW_State_Txt, "Untwisting CW");
               result = Run_AllMotors_SpdTrq(false, P_YAWM_UntwistSpeedPct, P_YAWM_NormalTorqueMotorPct, P_YAWM_NormalTorqueGenPct);

               if (result == 1)
               {
                  // calculate max time it could take to yaw 270 degrees(and add 20 s just in case)
                  V_YAW_ProcessTimeout = CalcYawingTime(270, ((F32)P_YAWM_UntwistSpeedPct / 100) ) + 20000;
                  startPosition = V_YAW_NacelleTwistDeg;
                  step = 3;
               }
               else if (result < 0)    // FAULT
               {
                  StateFunctionPointer = Yaw_ControlErrorStop_State;
                  OS_StatusCodeResetAutomatic(715);   // reset "Cable autounwind" status code
               }
               break;
            }

            case 3:
            {
               int rotated;
               rotated = V_YAW_NacelleTwistDeg - startPosition;

               // In first step, untwist 270 degrees, to prevent "oveshooting" in case wind direction has changed during untwisting
               if (rotated >= 2700)
               {
                  if (V_WID_WindDirectionRef > 0)
                  {
                     // calculate new max time in ms it could take to yaw specified degrees
                     V_YAW_ProcessTimeout = CalcYawingTime( ((F32)V_WID_WindDirectionRef / 10), ((F32)P_YAWM_UntwistSpeedPct / 100) ) + 20000;
                  }
                  else
                  {
                     // finish untwisting and change yaw direction, because wind direction changed
                     StateFunctionPointer = Yaw_AutoRunCCW_State;
                     OS_StatusCodeResetAutomatic(715);   // reset "Cable autounwind" status code
                  }

                  V_YAW_ProcessTimer = 0;      // reset yawing timeout timer
                  step = 4;                  // go to next step
               }
               break;
            }

            case 4:
            {
               // In second step, continue yawing until 5 degrees into wind
               if (V_WID_WindDirectionRef < P_YAW_NormalStopDeg)
               {
                  StateFunctionPointer = Yaw_AutoStop_State;
                  OS_StatusCodeResetAutomatic(715);   // reset "Cable autounwind" status code
               }
               break;
            }
         }
         break;

      default:
      {
         Yaw_Mode = YAW_NORMAL_MODE;
         break;
      }
   }


   //-----------------------------------------------------------------------
   // EXIT CONDITIONS
   //-----------------------------------------------------------------------

   // timeout, if yawing takes too long
   V_YAW_ProcessTimer += YAW_TASK_SCANTIME;
   if (V_YAW_ProcessTimer > V_YAW_ProcessTimeout)   // TIMEOUT
   {
      CustomLog_MakeEntry(YawSystemLog, "Untwisting CW timeout");
      //StateFunctionPointer = Yaw_ControlErrorStop_State;
      StateFunctionPointer = Yaw_AutoStop_State;
      OS_StatusCodeResetAutomatic(715);   // reset "Cable autounwind" status code
      return;
   }

   // check buttons
   if (BlockButton_Flag)
   {
      BlockButton_Flag = false;
      StateFunctionPointer = Yaw_ManualDisable_State;
      OS_StatusCodeResetAutomatic(715);   // reset "Cable autounwind" status code
   }
   /*
   else if (CwButton_Flag)
   {
      CwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCW_State;
   }
   else if (CcwButton_Flag)
   {
      CcwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCCW_State;
   }
   else if (AutoButton_Flag)
   {
      AutoButton_Flag = false;
      StateFunctionPointer = Yaw_AutoStop_State;
   }
   */
}

