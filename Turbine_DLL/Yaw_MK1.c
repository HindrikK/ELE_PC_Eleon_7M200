/// <summary>
/// Calculate nacelle twist degrees based on yaw absolute encoder
/// </summary>
static void CalcCableTwistAngle()
{
   // Check for yaw encoder error
   if (AI_YAW_AbsoluteEncoderSignal < 3900 || AI_YAW_AbsoluteEncoderSignal > 20100)
   {
      YawEncoderError_Flag = true;
   }
   else
   {
      YawEncoderError_Flag = false;
   }

   // add hysteresis for nacelle absolute encoder signal, because it is too unprecise
   AI_YAW_AbsoluteEncoderSignal = HystUpDown_Control(YawEncoderHysteresis, AI_YAW_AbsoluteEncoderSignal);
   V_YAW_YawAngleEncoder = ((S64)((S32)AI_YAW_AbsoluteEncoderSignal - (S32)4000) * ((S32)P_YAW_AbsoluteEncoderDeg20mA - (S32)P_YAW_AbsoluteEncoderDeg4mA) / ((S32)16000) + (S32)P_YAW_AbsoluteEncoderDeg4mA) / 10;
   
   
   // select which sensor to use for general yaw angle
   if(P_YAW_YawAngleSource == 0)
   {
       V_YAW_NacelleTwistDeg = V_YAW_YawAngleEncoder;
   }
   else
   {
   	   V_YAW_NacelleTwistDeg = V_YAWM_YawAngleMotor;
   }
}


/// <summary>
/// Calculate nacelle direction
/// </summary>
static void CalcNacelleDirection()
{
   static S32 TempNacelleDirection;

   TempNacelleDirection = (V_YAW_NacelleTwistDeg - P_YAW_NorthOffset) % 3600;
   if (TempNacelleDirection < 0) { TempNacelleDirection += 3600; }
   if (TempNacelleDirection > 3599) { TempNacelleDirection -= 3600; }
   V_NacelleDirection = TempNacelleDirection;
}



/// <summary>
/// Check if untwisting is needed
/// </summary>
/// <param name="CableTwist">Cable twist angle [0,1 deg]</param>
/// <returns>0: not twisted; 1: twisted CW; 2: twisted CCW</returns>
static U8 CheckCableTwist(S32 CableTwist)
{
   if ((V_GridRealPower10Min / 1000) < P_YAW_UntwistPowerReference)
   {
      if (CableTwist > P_YAW_UntwistDegLowPower)
      {
         return 1;   // twisted in CW direction
      }
      else if (CableTwist < -(P_YAW_UntwistDegLowPower))
      {
         return 2;   // twisted in CCW direction
      }
      else
      {
         return 0;
      }
   }
   else
   {
      if (CableTwist > P_YAW_UntwistDegHighPower)
      {
         return 1;   // twisted in CW direction
      }
      else if (CableTwist < -(P_YAW_UntwistDegHighPower))
      {
         return 2;   // twisted in CCW direction
      }
      else
      {
         return 0;
      }
   }
}


// calculate max time in [ms] it could take to yaw specified degrees with specified speed
U32 CalcYawingTime(F32 DegToYaw/*0,1deg*/, F32 YawSpeedPct/*0,01%*/)
{
   F32 speedDegSec = ((F32)P_YAW_RatedYawSpeed / 10) * (YawSpeedPct / 100);
   
   if (DegToYaw > 0)
   {
      return (U32)(DegToYaw / speedDegSec) * 1000;
   }
   else
   {
      return (U32)(-DegToYaw / speedDegSec) * 1000;
   }
}




