// yaw encoder angle changing too fast
// update interval for this status code has to be 100 ms, otherwise it will calculate wrong speed
// yaw angle change is measured over 2 s period, because shorter period could give false alarm, because of jumpy encoder signal
static U8 StatusSetFunc5300(void)
{
   static BOOL firstentry = true;
   static S32 TwistDeg_array[20];

   if (firstentry) // function called first time, preload buffer with current degrees
   {
      U8 i;
      for (i = 0; i < 20; i++)
      {
         TwistDeg_array[i] = V_YAW_YawAngleEncoder;
      }
      firstentry = false;
      return 0;
   }

   if (!YawEncoderError_Flag)
   {
      // insert new position value into buffer and look for MIN and MAX angle values
      S32 Min_Deg = 99999;
      S32 Max_Deg = -99999;
      U8 i;
      for (i = 0; i < 19; i++)
      {
         if (TwistDeg_array[i] < Min_Deg) { Min_Deg = TwistDeg_array[i]; }    // look for MIN value
         if (TwistDeg_array[i] > Max_Deg) { Max_Deg = TwistDeg_array[i]; }    // look for MAX value
         TwistDeg_array[i] = TwistDeg_array[i + 1];
      }
      TwistDeg_array[19] = V_YAW_YawAngleEncoder;

      S32 deg_Change = Max_Deg - Min_Deg;
      if (deg_Change > (P_YAW_NacelleTwistDegMaxSpeed * 2))
      {
         return 1;
      }
   }

   return 0;
}

