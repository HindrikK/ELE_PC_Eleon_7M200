//***********************************************************************
// STATE : Yaw is stopped in auto control.
//       Keeping turbine exactly upwind at 0 degrees
//***********************************************************************
static void Yaw_AutoStop_State(BOOL firstentry)
{
   static U8 step;
   static int slipPreventionTimer;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   //-----------------------------------------------------------------------
   if (firstentry)
   {
      strcpy(V_YAW_State_Txt, "Auto Stopping");
      V_YAW_State = YAW_STOPPED_AUTO;
      YawStartTimer_1s = 0;
      yawStartTimer = 0;
      _yawStartDelay = P_YAW_NormalStartDelay_1;
      V_YAW_StartCountdown = P_YAW_NormalStartDelay_1;
      slipPreventionTimer = 0;

      step = 0;
   }


   //-----------------------------------------------------------------------
   //CONTROL
   //-----------------------------------------------------------------------
   switch (Yaw_Mode)
   {
      case YAW_NORMAL_MODE:
      case YAW_STRONG_MODE:
      {
         int result;

         switch (step)
         {
            case 0:
            {
               if (P_YAWM_NormalStrainEnable) { step = 1; }
               else { step = 3; }
               break;
            }

            case 1:  // START STRAINING
            {
               // check what is motor run direction before stop and decide how to strain
               if (GetRunDirection() == 2) // if motors running in positive speed/direction
               {
                  Set_Group1MotorsSpeedAndTorque(P_YAWM_NormalStrainSpeedPct, P_YAWM_NormalStrainTorquePct, P_YAWM_NormalTorqueGenPct);
                  Set_Group2MotorsSpeedAndTorque(-P_YAWM_NormalStrainSpeedPct, P_YAWM_NormalStrainTorquePct, P_YAWM_NormalTorqueGenPct);
               }
               else
               {
                  Set_Group1MotorsSpeedAndTorque(-P_YAWM_NormalStrainSpeedPct, P_YAWM_NormalStrainTorquePct, P_YAWM_NormalTorqueGenPct);
                  Set_Group2MotorsSpeedAndTorque(P_YAWM_NormalStrainSpeedPct, P_YAWM_NormalStrainTorquePct, P_YAWM_NormalTorqueGenPct);
               }
               Release_AllBrakes();
               Run_AllMotors(true);

               V_YAW_ProcessTimeout = 10000; // straining timeout 10 s
               V_YAW_ProcessTimer = 0;
               step = 2;
               break;
            }

            case 2:  // STRAINING
            {
               V_YAW_ProcessTimer += YAW_TASK_SCANTIME;

               S16 maxSpeed = GetMaxSpeedPct();
               if (maxSpeed < P_YAWM_MinSpeedPct) // wait until strained(all motors stopped)
               {
                  step = 3;
               }
               else if (V_YAW_ProcessTimer >= V_YAW_ProcessTimeout)
               {
                  step = 3;
               }

               Restrain_Timer = P_YAWM_NormalReStrainPeriod * 1000;

               break;
            }

            case 3:
            {
               Stop_AllMotors_Trq(true, P_YAWM_StopTorquePct);
               step = 4;
               break;
            }

            case 4:
            {
               result = Stop_AllMotors_Trq(false, P_YAWM_StopTorquePct);
               if (result == 1)     // SUCCESS
               {
                  strcpy(V_YAW_State_Txt, "Auto Stop");
                  Apply_AllBrakes();
                  //Stop_AllMotorsNoRamp(true);
                  ResetAllMotorPositions();
                  step = 5;
               }
               else if (result < 0) // FAULT
               {
                  StateFunctionPointer = Yaw_ControlErrorStop_State;
               }

               break;
            }

            case 5:  // STOPPED
            {
               // decide if should start yawing automatically
               if (V_WindSpeed30Sec < P_YAW_MinWindForYaw)
               {
                  break;
               }
               else if (V_PitchAngle < P_PitchStopPosition - 200) // turbine running(blades not in stop position)
               {
                  if (V_WID_WindDirectionRef > P_YAW_NormalStartDeg_2 ||
                      V_WID_WindDirectionRef < -P_YAW_NormalStartDeg_2)
                  {
                     _yawStartDelay = P_YAW_NormalStartDelay_2;
                     yawStartTimer += YAW_TASK_SCANTIME;
                  }
                  else if (V_WID_WindDirectionRef > P_YAW_NormalStartDeg_1)
                  {
                     //_yawStartDelay = P_YAW_NormalStartDelay_1;// - (S32)((F32)(V_WID_WindDirectionRef - P_YAW_NormalStartDeg_1) * TimerGain);
                     _yawStartDelay = Linear_Conversion(V_WID_WindDirectionRef/*Input*/, P_YAW_NormalStartDeg_1/*InLow*/, P_YAW_NormalStartDeg_2/*InHigh*/, P_YAW_NormalStartDelay_1/*OutLow*/, P_YAW_NormalStartDelay_2/*OutHigh*/);
                     yawStartTimer += YAW_TASK_SCANTIME;
                  }
                  else if (V_WID_WindDirectionRef < -P_YAW_NormalStartDeg_1)
                  {
                     //_yawStartDelay = P_YAW_NormalStartDelay_1;// - (S32)((F32)(-V_WID_WindDirectionRef - P_YAW_NormalStartDeg_1) * TimerGain);
                     _yawStartDelay = Linear_Conversion(-V_WID_WindDirectionRef/*Input*/, P_YAW_NormalStartDeg_1/*InLow*/, P_YAW_NormalStartDeg_2/*InHigh*/, P_YAW_NormalStartDelay_1/*OutLow*/, P_YAW_NormalStartDelay_2/*OutHigh*/);
                     yawStartTimer += YAW_TASK_SCANTIME;
                  }
                  else    // yaw deviation is in limits, so reset timer counters
                  {
                     YawStartTimer_1s = 0;
                     yawStartTimer = 0;
                     _yawStartDelay = P_YAW_NormalStartDelay_1;
                     V_YAW_StartCountdown = P_YAW_NormalStartDelay_1;
                  }
               }
               else     // turbine stopped(blades in stop position)
               {
                  if (V_WID_WindDirectionRef > P_YAW_StoppedStartRelDeg || V_WID_WindDirectionRef < -P_YAW_StoppedStartRelDeg)
                  {
                     _yawStartDelay = P_YAW_SoftStartDelay;
                     yawStartTimer += YAW_TASK_SCANTIME;
                  }
                  else    // yaw deviation is in limits, so reset timer counters
                  {
                     YawStartTimer_1s = 0;
                     yawStartTimer = 0;
                     _yawStartDelay = P_YAW_StoppedStartDelay;
                     V_YAW_StartCountdown = P_YAW_StoppedStartDelay;
                  }
               }

               if (yawStartTimer >= 1000)   // counts every 1 s
               {
                  yawStartTimer = 0;
                  YawStartTimer_1s++;
                  V_YAW_StartCountdown = _yawStartDelay - YawStartTimer_1s;
               }

               if (V_YAW_StartCountdown <= 0)
               {
                  V_YAW_StartCountdown = 0;    // set value to 0, to prevent it being negative

                  // detect in which directions should yaw
                  if (V_WID_WindDirectionRef > 0)
                  {
                     StateFunctionPointer = Yaw_AutoRunCW_State;
                  }
                  else
                  {
                     StateFunctionPointer = Yaw_AutoRunCCW_State;
                  }
               }

               // decide if should re-strain yaw
               if (P_YAWM_NormalReStrainEnable)
               {
                  if (Restrain_Timer > 0)
                  {
                     Restrain_Timer -= YAW_TASK_SCANTIME;
                  }
                  else
                  {
                     // go to straining step if too many motors have moved more than allowed
                     if (CheckAllMotorPositions(P_YAWM_NormalReStrainRev) >= P_YAWM_NormalReStrainNum)
                     {
                        CustomLog_MakeEntry(YawSystemLog, "Brakes slipping - restrain");

                        step = 1;
                     }
                  }
               }

               break;
            }
         }

         break;
      }

      case YAW_SOFT_MODE:
      {
         int result;

         switch (step)
         {
            case 0:
            {
               Release_AllBrakes();

               // make all motors run with zero speed
               Run_AllMotors_SpdTrq(true, 0, P_YAWM_NormalTorqueMotorPct, P_YAWM_NormalTorqueGenPct);

               step = 1;
               break;
            }

            case 1:
            {
               result = Run_AllMotors_SpdTrq(false, 0, P_YAWM_NormalTorqueMotorPct, P_YAWM_NormalTorqueGenPct);
               if (result == 1)     // SUCCESS
               {
                  strcpy(V_YAW_State_Txt, "Auto Stop");
                  step = 2;
               }
               else if (result < 0) // FAULT
               {
                  StateFunctionPointer = Yaw_ControlErrorStop_State;
               }

               break;
            }

            case 2:
            {
               Set_Group1MotorsSpeedAndTorque(P_YAWM_SoftStopSpeedPct, P_YAWM_SoftStopTorqueMotorPct, P_YAWM_SoftStopTorqueGenPct);
               Set_Group2MotorsSpeedAndTorque(-P_YAWM_SoftStopSpeedPct, P_YAWM_SoftStopTorqueMotorPct, P_YAWM_SoftStopTorqueGenPct);
               step = 3;

               break;
            }

            case 3:     // keep holding yaw position with motors
            {
               static BOOL slipPrevention = false;

               // check if and how many yaw motors have moved too fast
               // if more than YAW_SLIP_MOT_COUNT motors have moved too fast, it means yaw can not hold against the wind
               if (!slipPrevention && CheckMotorsSpeedPct(P_YAWM_SoftStopSpeedPct + P_YAWM_SoftSlipDetectSpeed) > YAW_SLIP_MOT_COUNT)
               {
                  // quickly run yaw motors with slip prevention torque and speed, without ramping. This should hold yaw and stop runout situation.
                  Set_AllMotorsSpeedAndTorqueNoRamp(YAW_MOT_ZERO_SPEED, P_YAWM_SoftSlipPreventTorquePct, P_YAWM_SoftSlipPreventTorquePct);
                  slipPrevention = true;
               }
               else if (slipPrevention)
               {
                  slipPreventionTimer += YAW_TASK_SCANTIME;
                  if (slipPreventionTimer > YAW_SLIP_PREVENT_TIME)
                  {
                     slipPreventionTimer = 0;
                     slipPrevention = false;
                     Set_Group1MotorsSpeedAndTorque(P_YAWM_SoftStopSpeedPct, P_YAWM_SoftStopTorqueMotorPct, P_YAWM_SoftStopTorqueGenPct);
                     Set_Group2MotorsSpeedAndTorque(-P_YAWM_SoftStopSpeedPct, P_YAWM_SoftStopTorqueMotorPct, P_YAWM_SoftStopTorqueGenPct);
                  }
               }


               // decide if should start yawing automatically
               if (V_WindSpeed30Sec < P_YAW_MinWindForYaw)
               {
                  break;
               }
               else if (V_PitchAngle < P_PitchStopPosition - 200) // turbine running(blades not in stop position)
               {
                  if (V_WID_WindDirectionRef > P_YAW_SoftStartRelDeg || V_WID_WindDirectionRef < -P_YAW_SoftStartRelDeg)
                  {
                     _yawStartDelay = P_YAW_SoftStartDelay;
                     yawStartTimer += YAW_TASK_SCANTIME;
                  }
                  else    // yaw deviation is in limits, so reset timer counters
                  {
                     YawStartTimer_1s = 0;
                     yawStartTimer = 0;
                     _yawStartDelay = P_YAW_SoftStartDelay;
                     V_YAW_StartCountdown = P_YAW_SoftStartDelay;
                  }
               }
               else     // turbine stopped(blades in stop position)
               {
                  if (V_WID_WindDirectionRef > P_YAW_StoppedStartRelDeg || V_WID_WindDirectionRef < -P_YAW_StoppedStartRelDeg)
                  {
                     _yawStartDelay = P_YAW_StoppedStartDelay;
                     yawStartTimer += YAW_TASK_SCANTIME;
                  }
                  else    // yaw deviation is in limits, so reset timer counters
                  {
                     YawStartTimer_1s = 0;
                     yawStartTimer = 0;
                     _yawStartDelay = P_YAW_StoppedStartDelay;
                     V_YAW_StartCountdown = P_YAW_StoppedStartDelay;
                  }
               }

               if (yawStartTimer >= 1000)   // counts every 1 s
               {
                  yawStartTimer = 0;
                  YawStartTimer_1s++;
                  V_YAW_StartCountdown = _yawStartDelay - YawStartTimer_1s;
               }

               if (V_YAW_StartCountdown <= 0)
               {
                  V_YAW_StartCountdown = 0;    // set value to 0, to prevent it being negative

                  // detect in which directions should yaw
                  if (V_WID_WindDirectionRef > 0)
                  {
                     StateFunctionPointer = Yaw_AutoRunCW_State;
                  }
                  else
                  {
                     StateFunctionPointer = Yaw_AutoRunCCW_State;
                  }
               }

               break;
            }
         }

         break;
      }


      default:
      {
         Yaw_Mode = YAW_NORMAL_MODE;
         break;
      }
   }


   //-----------------------------------------------------------------------
   // check buttons
   //-----------------------------------------------------------------------
   if (BlockButton_Flag)
   {
      BlockButton_Flag = false;
      StateFunctionPointer = Yaw_ManualDisable_State;
   }
   else if (CwButton_Flag)
   {
      CwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCW_State;
   }
   else if (CcwButton_Flag)
   {
      CcwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCCW_State;
   }
   else if (AutoButton_Flag)
   {
      AutoButton_Flag = false;
      ForceUpwindNow();
   }
}


//***********************************************************************
// STATE : Yaw is manually disabled (pressing Yaw block button)
//       Brakes are applied and motors are stopped.
//***********************************************************************
static void Yaw_ManualDisable_State(BOOL firstentry)
{
   static U8 step;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   //-----------------------------------------------------------------------
   if (firstentry)
   {
      strcpy(V_YAW_State_Txt, "Disabling");
      V_YAW_State = YAW_STOPPED_MANUAL;
      V_YAW_StartCountdown = P_YAW_NormalStartDelay_1;

      CustomLog_MakeEntry(YawSystemLog, "Yaw disabled");

      step = 0;
   }


   //-----------------------------------------------------------------------
   //CONTROL
   //-----------------------------------------------------------------------
   switch (Yaw_Mode)
   {
      case YAW_NORMAL_MODE:
      case YAW_STRONG_MODE:
      case YAW_SOFT_MODE:
      {
         int result;

         switch (step)
         {
            case 0:
            {
               Stop_AllMotors_Trq(true, P_YAWM_StopTorquePct);
               step = 1;
               break;
            }

            case 1:
            {
               result = Stop_AllMotors_Trq(false, P_YAWM_StopTorquePct);
               if (result == 1)     // SUCCESS
               {
                  strcpy(V_YAW_State_Txt, "Manually Disabled");
                  Apply_AllBrakes();
                  step = 2;
               }
               else if (result < 0) // FAULT
               {
                  StateFunctionPointer = Yaw_ControlErrorStop_State;
               }

               return;	// when in "step 1"(motor stopping procedure) will just return and don't check exit conditions
            }

            case 2:
            {
               // do nothing, just stay in manual stop mode
               break;
            }
         }

         break;
      }

      default:
      {
         Yaw_Mode = YAW_NORMAL_MODE;
         break;
      }
   }


   //-----------------------------------------------------------------------
   // EXIT CONDITIONS
   //-----------------------------------------------------------------------

   // check buttons
   if (BlockButton_Flag)
   {
      BlockButton_Flag = false;
   }
   else if (CwButton_Flag)
   {
      CwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCW_State;
   }
   else if (CcwButton_Flag)
   {
      CcwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCCW_State;
   }
   else if (AutoButton_Flag)
   {
      AutoButton_Flag = false;
      StateFunctionPointer = Yaw_AutoStop_State;
   }
}

//***********************************************************************
// STATE : Stopped with error
//***********************************************************************
static void Yaw_ControlErrorStop_State(BOOL firstentry)
{
   static U8 step;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   //-----------------------------------------------------------------------
   if (firstentry)
   {
      switch (Yaw_Mode)
      {
         case YAW_NORMAL_MODE:
         case YAW_SOFT_MODE:
         case YAW_STRONG_MODE:

            strcpy(V_YAW_State_Txt, "Control error stopping");
            V_YAW_State = YAW_CONTROL_ERROR;

            Stop_AllMotors_Trq(true, P_YAWM_StopTorquePct);

            CustomLog_MakeEntry(YawSystemLog, "Control error");

            break;
      }

      V_YAW_StartCountdown = P_YAW_NormalStartDelay_1;

      step = 0;
   }


   //-----------------------------------------------------------------------
   //CONTROL
   //-----------------------------------------------------------------------
   switch (Yaw_Mode)
   {
      case YAW_NORMAL_MODE:
      case YAW_SOFT_MODE:
      case YAW_STRONG_MODE:
      {
         int result;

         switch (step)
         {
            case 0:
            {
               result = Stop_AllMotors_Trq(false, P_YAWM_StopTorquePct);
               if (result == 1)     // SUCCESS
               {
                  Apply_AllBrakes();
                  strcpy(V_YAW_State_Txt, "Control error stop");
                  step = 1;
               }
               else if (result < 0) // FAULT
               {
                  Apply_AllBrakes();
                  Stop_AllMotorsNoRamp(true);		// just in case
                  strcpy(V_YAW_State_Txt, "Control error stop");
                  step = 1;
               }

               return;	// when in "step 0"(motor stopping procedure) will just return and don't check exit conditions
            }

            case 1:
            {
               // do nothing and wait for exit conditions

               break;
            }
         }

         break;
      }

      default:
      {
         Yaw_Mode = YAW_NORMAL_MODE;
         break;
      }
   }

   // clear button flags
   BlockButton_Flag = false;
   CwButton_Flag = false;
   CcwButton_Flag = false;
   AutoButton_Flag = false;


   //-----------------------------------------------------------------------
   // EXIT CONDITIONS
   //-----------------------------------------------------------------------

   if (V_ActiveControlProgram2 == 0 || V_ActiveControlProgram2 == 30)
   {
      if (!V_YAWM_Fault && V_YAWM_Ready)
      {
         StateFunctionPointer = Yaw_AutoStop_State;
      }
   }
}


