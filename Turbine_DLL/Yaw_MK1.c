//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// This function calculates the sum of two angles in 360 degrees system
// INPUT:
//    Angle_1 - [0,1 degrees] angle to add
//    Angle_2 - [0,1 degrees] angle to add
// RETURN:
//    Angle - [0,1 degrees] sum of the two angles in 360 degrees system
static int CalcAngleSum(int Angle_1, int Angle_2)
{
   int result = Angle_1 + Angle_2;
   if (result > 3599) { result -= 3600; }
   else if (result < 0) { result += 3600; }

   return result;
}

// This function handles subtraction of two angles in 360 degrees system
// INPUT:
//    Angle_1 - [0,1 degrees] angle to subtract from
//    Angle_2 - [0,1 degrees] angle to subtract
// RETURN:
//    Angle - [0,1 degrees] result of subtraction in 360 degrees system
static int CalcAngleSubtract(int Angle_1, int Angle_2)
{
   int result = Angle_1 - Angle_2;
   if (result > 3599) { result -= 3600; }
   else if (result < 0) { result += 3600; }

   return result;
}

// This function returns the angle distance/difference between two angles in 360 degrees system.
// The result never exceeds 180 degrees.(distance to the left or right - smaller will be returned)
// Compares Angle_2 relative to Angle_1
// INPUT:
//    Angle_1 - [0,1 degrees] angle to subtract from
//    Angle_2 - [0,1 degrees] angle to subtract
// RETURN:
//    Angle - [0,1 degrees] difference between angles. Positive when Angle_2 > Angle_1 and negative when Angle_1 > Angle_2
static int CalcAngleDistanceBetween(int Angle_1, int Angle_2)
{
   int result_1 = CalcAngleSubtract(Angle_1, Angle_2);
   int result_2 = CalcAngleSubtract(Angle_2, Angle_1);

   if (result_1 < result_2) { return -result_1; }
   else { return result_2; }
}
