//***********************************************************************
// STATE : Yawing the nacelle up to wind in auto control
//***********************************************************************
static void Yaw_AutoRunCW_State(BOOL firstentry)
{
   static U8 step;
   static int slipPreventionTimer;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   //-----------------------------------------------------------------------
   if (firstentry)
   {
      // before entering the run state, check if cable twist is going to stay in limits after yawing
      U8 twistCheck1 = CheckCableTwist(V_YAW_NacelleTwistDeg + V_WID_WindDirectionRef);
      switch (twistCheck1)
      {
         case 0:
            break;
         case 1:
            StateFunctionPointer = Yaw_UntwistRunCCW_State;
            return;
         case 2:
            StateFunctionPointer = Yaw_UntwistRunCW_State;
            return;
      }

      step = 0;
      V_YAW_ProcessTimer = 0;
      V_YAW_StartCountdown = P_YAW_NormalStartDelay_1;
      slipPreventionTimer = 0;
   }


   //-----------------------------------------------------------------------
   //CONTROL
   //-----------------------------------------------------------------------
   switch (Yaw_Mode)
   {
      case YAW_NORMAL_MODE:
      case YAW_STRONG_MODE:

         switch (step)
         {
            case 0:
            {
               strcpy(V_YAW_State_Txt, "Start Yawing CW");
               V_YAW_State = YAW_RUN_CW;

               V_YAW_ProcessTimeout = CalcYawingTime( ((F32)V_WID_WindDirectionRef / 10), ((F32)P_YAWM_NormalSpeedPct / 100) ) * 2 + 10000;

               Release_AllBrakes();
               Run_AllMotors_SpdTrq(true, P_YAWM_NormalSpeedPct, P_YAWM_NormalTorqueMotorPct, P_YAWM_NormalTorqueGenPct);

               step = 1;
               break;
            }

            case 1:
            {
               int result;

               result = Run_AllMotors_SpdTrq(false, P_YAWM_NormalSpeedPct, P_YAWM_NormalTorqueMotorPct, P_YAWM_NormalTorqueGenPct);
               if (result == 1)     // SUCCESS
               {
                  strcpy(V_YAW_State_Txt, "Auto Yawing CW");
                  step = 2;
               }
               else if (result < 0) // FAULT
               {
                  StateFunctionPointer = Yaw_ControlErrorStop_State;
               }
               break;
            }

            case 2:
            {
               if (V_PitchAngle < P_PitchStopPosition - 200) // turbine running(blades not in stop position)
               {
                  // wait until correct yaw angle achieved
                  if (V_WID_WindDirectionRef <= P_YAW_NormalStopDeg)
                  {
                     StateFunctionPointer = Yaw_AutoStop_State;
                  }
               }
               else
               {
                  // wait until correct yaw angle achieved
                  if (V_WID_WindDirectionRef <= P_YAW_StoppedStopRelDeg)
                  {
                     StateFunctionPointer = Yaw_AutoStop_State;
                  }
               }
               break;
            }
         }
         break;

      case YAW_SOFT_MODE:

         switch (step)
         {
            case 0:
            {
               strcpy(V_YAW_State_Txt, "Start Yawing CW");
               V_YAW_State = YAW_RUN_CW;

               V_YAW_ProcessTimeout = CalcYawingTime( ((F32)V_WID_WindDirectionRef / 10), ((F32)P_YAWM_SoftRunGLeadSpeedPct / 100) ) * 2 + 10000;

               Set_Group1MotorsSpeedAndTorque(P_YAWM_SoftRunGLeadSpeedPct, P_YAWM_SoftRunGLeadTorqueMotorPct, P_YAWM_SoftRunGLeadTorqueGenPct);
               Set_Group2MotorsSpeedAndTorque(P_YAWM_SoftRunGLagSpeedPct, P_YAWM_SoftRunGLagTorqueMotorPct, P_YAWM_SoftRunGLagTorqueGenPct);
               Release_AllBrakes();
               Run_AllMotors(true);

               step = 1;
               break;
            }

            case 1:
            {
               int result;

               result = Run_AllMotors(false);
               if (result == 1)     // SUCCESS
               {
                  strcpy(V_YAW_State_Txt, "Auto Yawing CW");
                  step = 2;
               }
               else if (result < 0) // FAULT
               {
                  StateFunctionPointer = Yaw_ControlErrorStop_State;
               }
               break;
            }

            case 2:
            {
               if (V_PitchAngle < P_PitchStopPosition - 200) // turbine running(blades not in stop position)
               {
                  // wait until correct yaw angle achieved
                  if (V_WID_WindDirectionRef <= P_YAW_SoftStopRelDeg)
                  {
                     StateFunctionPointer = Yaw_AutoStop_State;
                  }
               }
               else
               {
                  // wait until correct yaw angle achieved
                  if (V_WID_WindDirectionRef <= P_YAW_StoppedStopRelDeg)
                  {
                     StateFunctionPointer = Yaw_AutoStop_State;
                  }
               }

               // check if and how many yaw motors have moved too fast
               // if more than YAW_SLIP_MOT_COUNT motors have moved too fast, it means yaw can not hold against the wind
               if (CheckMotorsSpeedPct(P_YAWM_SoftRunGLeadSpeedPct + P_YAWM_SoftSlipDetectSpeed) > YAW_SLIP_MOT_COUNT)
               {
                  // Run all yaw motors with leading group speed and slip prevention torque. This should hold yaw and stop runout situation.
                  Set_AllMotorsSpeedAndTorqueNoRamp(P_YAWM_SoftRunGLeadSpeedPct, P_YAWM_SoftSlipPreventTorquePct, P_YAWM_SoftSlipPreventTorquePct);
                  step = 10;
               }

               break;
            }

            case 10:
            {
               // wait for slip prevention delay
               slipPreventionTimer += YAW_TASK_SCANTIME;
               if (slipPreventionTimer > YAW_SLIP_PREVENT_TIME)
               {
                  slipPreventionTimer = 0;
                  Set_Group1MotorsSpeedAndTorque(P_YAWM_SoftRunGLeadSpeedPct, P_YAWM_SoftRunGLeadTorqueMotorPct, P_YAWM_SoftRunGLeadTorqueGenPct);
                  Set_Group2MotorsSpeedAndTorque(P_YAWM_SoftRunGLagSpeedPct, P_YAWM_SoftRunGLagTorqueMotorPct, P_YAWM_SoftRunGLagTorqueGenPct);
                  step = 2;
               }

               break;
            }
         }
         break;

      default:
      {
         Yaw_Mode = YAW_NORMAL_MODE;
         break;
      }
   }


   //-----------------------------------------------------------------------
   // EXIT CONDITIONS
   //-----------------------------------------------------------------------

   // timeout, if yawing takes too long
   V_YAW_ProcessTimer += YAW_TASK_SCANTIME;
   if (V_YAW_ProcessTimer > V_YAW_ProcessTimeout)   // TIMEOUT
   {
      CustomLog_MakeEntry(YawSystemLog, "Yawing CW timeout");
      //StateFunctionPointer = Yaw_ControlErrorStop_State;
      StateFunctionPointer = Yaw_AutoStop_State;
      return;
   }

   // check cable twist
   int twistCheck2 = CheckCableTwist(V_YAW_NacelleTwistDeg);
   switch (twistCheck2)
   {
      case 0:
         break;
      case 1:
         StateFunctionPointer = Yaw_UntwistRunCCW_State;
         break;
      case 2:
         StateFunctionPointer = Yaw_UntwistRunCW_State;
         break;
   }

   // check buttons
   if (BlockButton_Flag)
   {
      BlockButton_Flag = false;
      StateFunctionPointer = Yaw_ManualDisable_State;
   }
   else if (CwButton_Flag)
   {
      CwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCW_State;
   }
   else if (CcwButton_Flag)
   {
      CcwButton_Flag = false;
      StateFunctionPointer = Yaw_ManualRunCCW_State;
   }
   else if (AutoButton_Flag)
   {
      StateFunctionPointer = Yaw_AutoStop_State;
      AutoButton_Flag = false;
   }
}

