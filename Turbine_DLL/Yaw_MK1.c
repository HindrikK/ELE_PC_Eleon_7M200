//------------------------------------------------------------------------------
// YAW MODE SELECTION
//------------------------------------------------------------------------------
static void YawModeSelection()
{
   static U8 Yaw_ModeSelection_Prev = 255;


   // if mode selection parameter changed
   if (P_YAW_ModeSelection != Yaw_ModeSelection_Prev)
   {
      Yaw_ModeSelection_Prev = P_YAW_ModeSelection;

      switch (P_YAW_ModeSelection)
      {
         case YAW_AUTOSELECT_MODE:

            // In auto mode, the actual yaw mode is decided later in this function(look downward)
            strcpy(V_YAW_ModeSelection_Txt, "Auto");
            break;

         case YAW_NORMAL_MODE:

            Yaw_Mode = YAW_NORMAL_MODE;
            strcpy(V_YAW_ModeSelection_Txt, "Normal");
            strcpy(V_YAW_Mode_Txt, "Normal");
            break;

         case YAW_SOFT_MODE:

            Yaw_Mode = YAW_SOFT_MODE;
            strcpy(V_YAW_ModeSelection_Txt, "Soft");
            strcpy(V_YAW_Mode_Txt, "Soft");
            break;

         case YAW_STRONG_MODE:

            Yaw_Mode = YAW_STRONG_MODE;
            strcpy(V_YAW_ModeSelection_Txt, "Strong");
            strcpy(V_YAW_Mode_Txt, "Strong");
            break;

         default:

            Yaw_Mode = YAW_NORMAL_MODE;
            strcpy(V_YAW_ModeSelection_Txt, "Normal");
            strcpy(V_YAW_Mode_Txt, "Normal");
            break;
      }
   }


   // automatically choosing suitable mode when autoselect mode is choosed
   if (P_YAW_ModeSelection == YAW_AUTOSELECT_MODE)
   {
      if (V_PitchAngle >= P_PitchStopPosition - 200)  // if turbine stopped(blades in stop position), then switch to normal mode
      {
         strcpy(V_YAW_Mode_Txt, "Normal");
         Yaw_Mode = YAW_NORMAL_MODE;
      }
      else if (V_WindSpeed30Sec > P_YAW_SoftModeWind && Yaw_Mode != YAW_SOFT_MODE)
      {
         strcpy(V_YAW_Mode_Txt, "Soft");
         Yaw_Mode = YAW_SOFT_MODE;
      }
      else if (V_WindSpeed30Sec < P_YAW_NormalModeWind && Yaw_Mode != YAW_NORMAL_MODE)
      {
         strcpy(V_YAW_Mode_Txt, "Normal");
         Yaw_Mode = YAW_NORMAL_MODE;
      }
   }
   
   
   // detect when yaw mode changed; either automatically or manually by parameter change
   if(Yaw_Mode != Yaw_Mode_prev)
   {
      Yaw_Mode_prev = Yaw_Mode;
      
      // always go to stop state when mode has changed and yaw is in AutoStop state - this makes it to re-enter AutoStop state with new mode right away
      if(StateFunctionPointer == Yaw_AutoStop_State)
      {
      	 //StateFunctionPointer = Yaw_AutoStop_State;
      	 StateFunctionPointer_Prev = Yaw_ManualRunCW_State; // set previous state to some other state value to make it think that state changed, otherwise it won't re-enter AutoStop state
      }
   }
}


static void CheckYawProgram()
{
   static U8 V_ActiveControlProgram2_Prev = 255;

   if (V_ActiveControlProgram2 != V_ActiveControlProgram2_Prev)
   {
      switch (V_ActiveControlProgram2)
      {
         case YAW_PROG_0 :
         case YAW_PROG_30 :
         {
            break;
         }

         case YAW_PROG_50 :      // automatic yawing disabled
         {
            StateFunctionPointer = Yaw_ManualDisable_State;
            break;
         }

         case YAW_PROG_70 :      // cable untwisting
         {
            break;
         }

         case YAW_PROG_80 :      // high wind speed, force to keep upwind
         {
            //ForceUpwindNow();
            break;
         }

         case YAW_PROG_100 :  // keep turbine 90 degrees to wind
         {
            break;
         }
         case YAW_PROG_230 :  // yawing blocked in endstop position, twisted limit switch
         {
            StateFunctionPointer = Yaw_Twisted_State;
            break;
         }
         case YAW_PROG_240 :  // yawing blocked in all directions, use manual yawing in Service Yawing mode
         {
            StateFunctionPointer = Yaw_Blocked_State;
            break;
         }
         case YAW_PROG_250 :  // keep turbine 180 degrees to wind
         {
            StateFunctionPointer = Yaw_Blocked_State;
            break;
         }
         default:
         {
            StateFunctionPointer = Yaw_Blocked_State;
            break;
         }
      }

      V_ActiveControlProgram2_Prev = V_ActiveControlProgram2;
   }
}



static STATUS LocalCtrl(U32 Interval)
{
   STATUS LocalStatus = 0;

#if defined(YAW_DEBUG) || defined(INCLUDE_DEBUG)
   V_ActiveControlProgram2 = 0;
#endif

   CalcCableTwistAngle();
   CalcNacelleDirection();


   // create startup delay
   static int startupTimer = 5000;    // don't start any yaw program automatically before 5 s
   if (startupTimer > 0)
   {
      startupTimer -= YAW_TASK_SCANTIME;
      sprintf(V_YAW_State_Txt, "Startup delay: %d", startupTimer / 1000);
      return LocalStatus;
   }


   Button_Scan();
   YawModeSelection();

   CheckYawProgram();


   // reloading parameters from menu
   if (V_YAW_LoadParameters)
   {
      V_YAW_LoadParameters = false;
      OS_VarDB_UpdateVariableByName("V_YAW_LoadParameters", &V_YAW_LoadParameters, sizeof(V_YAW_LoadParameters));
      LoadParameters();
      InfoMessage("Parameters reloaded", 1);
   }


   if (V_YAWM_Fault)
   {
      StateFunctionPointer = Yaw_ControlErrorStop_State;
   }
   else if (!V_YAWM_Ready)
   {
      StateFunctionPointer = Yaw_Blocked_State;
   }


   //***********************************************************************
   // Function call to operating state functions - uses pointer to function
   // "StateFunctionPointer" points to the mode function that should be called
   //***********************************************************************

   if (StateFunctionPointer != StateFunctionPointer_Prev)
   {
	   StateFunctionPointer_Prev = StateFunctionPointer;
	   (*StateFunctionPointer)(true);
   }
   else
   {
      (*StateFunctionPointer)(false);
   }


   // check if command is set, to clear error log
   if (V_YAW_ClearLog)
   {
      CustomLog_ClearAll(YawSystemLog);
      V_YAW_ClearLog = false;
      OS_VarDB_UpdateVariableByName("V_YAW_ClearLog", &V_YAW_ClearLog, sizeof(V_YAW_ClearLog));
   }


   return LocalStatus;
}

static STATUS ShutdownCtrl(U8 ModuleState, U8* DoneState, U32 Interval)
{
   STATUS LocalStatus = 0;

   LocalStatus = LocalCtrl(Interval);
   STATUS_LINE_VAR_RETURN_VALUE(LocalStatus);

   switch (ModuleState)
   {
      case AS_SHUTDOWN:
      {
         // Activate "Manual Shutdown" status code

         *DoneState = AS_SHUTDOWN;
         break;
      }
      case AS_REBOOT:
      {

         *DoneState = AS_REBOOT;
         break;
      }
      case AS_SHUTDOWN_FAST:
      {

         *DoneState = AS_SHUTDOWN_FAST;
         break;
      }
      default:
      {
         *DoneState = ModuleState;
         break;
      }
   }
   return LocalStatus;
}

