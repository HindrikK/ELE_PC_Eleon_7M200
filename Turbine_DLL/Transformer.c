static void TrafoFanStateTxt(S32 State, S32 *PrevState, char *Text, S32 Size)
{
    if( State != *PrevState )
    {
        switch( State )
        {
           case TRAFO_STATE_AUTO_OFF:
                strncpy( Text, "AUTO OFF", Size );
                break;

           case TRAFO_STATE_AUTO_ON:
                strncpy( Text, "AUTO ON", Size );
                break;

           case TRAFO_STATE_FORCED_OFF:
                strncpy( Text, "FORCED OFF", Size );
                break;

            case TRAFO_STATE_FORCED_ON:
                strncpy( Text, "FORCED ON", Size );
                break;

            case TRAFO_STATE_UNDEFINED:
                //drop to default
            default:
                strncpy( Text, "WARNING", Size );
                break;
        }
    }

    *PrevState = State;
}
//_______________________________________________________________________
static void TrafoFanAutoCtlr()
{
    if( P_Transformer_Fan_Start < AI_GridRealPower )
    {
        StartTimer(&TrafoFanStartDelay);
        ResetTimer(&TrafoFanStopDelay);

        if( StandardTimer(&TrafoFanStartDelay) )
        {
            ResetTimer(&TrafoFanStartDelay);
            DO_Transformer_Fan = ON;
            V_Transformer_Fan_State = TRAFO_STATE_AUTO_ON;
        }
    }
    else if( (P_Transformer_Fan_Stop > AI_GridRealPower) &&
            DI_Transformer_ThermostatT1 && DI_Transformer_ThermostatT2)
    {
        StartTimer(&TrafoFanStopDelay);
        ResetTimer(&TrafoFanStartDelay);

        if( StandardTimer(&TrafoFanStopDelay) )
        {
            ResetTimer(&TrafoFanStopDelay);
            DO_Transformer_Fan = OFF;
            V_Transformer_Fan_State = TRAFO_STATE_AUTO_OFF;
        }
    }
}
//_______________________________________________________________________
static STATUS LocalCtrl( void )
{
    STATUS LocalStatus = 0;
    static S32 PrevFanState = TRAFO_STATE_UNDEFINED;


    switch(P_Transformer_Fan_ServiceCtrl)
    {
        case TRAFO_MODE_AUTO:
            TrafoFanAutoCtlr();
            break;

        case TRAFO_MODE_FORCED_OFF:
            DO_Transformer_Fan = OFF;
            V_Transformer_Fan_State = TRAFO_STATE_FORCED_OFF;
            break;

        case TRAFO_MODE_FORCED_ON:
            DO_Transformer_Fan = ON;
            V_Transformer_Fan_State = TRAFO_STATE_FORCED_ON;
            break;

        default:
            DO_Transformer_Fan = OFF;
            V_Transformer_Fan_State = TRAFO_STATE_UNDEFINED;
            break;
    }

    TrafoFanStateTxt(V_Transformer_Fan_State, &PrevFanState, V_Transformer_Fan_StateTxt, sizeof(V_Transformer_Fan_StateTxt));

    return LocalStatus;
}
