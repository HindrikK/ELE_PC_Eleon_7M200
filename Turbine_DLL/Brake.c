static void AssignParameters (U8 Changed)
{
    if ( 1 == Changed )
    {
        HydraulicPump_StartDelay.P_Time   = P_HydrOilPump_StartDelay;
        HydraulicPump_StopDelay.P_Time    = P_HydrOilPump_StopDelay;
        HydraulicPump_OnTimer.P_Time      = P_HydrOilPump_MaxTime;

        ParametersChanged[GENERAL_GROUP] = 0;
    }
}
//________________________________________________________________________
void HydraulicPumpCtrl(U8 *FirstTime)
{
    if(BlockHydraulicPump())
    {
        *FirstTime = 1;
        DO_BRK_HydPump = OFF;
        V_Hydraulic_Equip0_State = STATE_BLOCKED;
        return;
    }

    if ( *FirstTime )
    {
        Set_Timer_Param(P_TIMER_RESTART, &HydraulicPump_StartDelay, NULL);
        Set_Timer_Param(P_TIMER_RESTART, &HydraulicPump_StopDelay , NULL);
        Set_Timer_Param(P_TIMER_RESTART, &HydraulicPump_OnTimer   , NULL);
        HydraulicPumpTimeExceeded = 0;
        DO_BRK_HydPump = OFF;
        V_Hydraulic_Equip0_State = STATE_STOPPED;
        AssignParameters(*FirstTime);
        *FirstTime = 0;
    }

    AssignParameters(ParametersChanged[GENERAL_GROUP]);

    static U8 Hydraulic_OilPressure;

    if( Hysteresis( V_Hydraulic_OilPressure,
                    P_HydrOilPump_StartPress,
                    P_HydrOilPump_StopPress,
                    &Hydraulic_OilPressure,
                    ALIBRARY_INVERT_ENABLE )
      )
    {
        Set_Timer_Param(P_TIMER_RESTART, &HydraulicPump_StopDelay, NULL);

        if( TimerOn(&HydraulicPump_StartDelay) )
        {
            DO_BRK_HydPump = ON;

            if( TimerOn(&HydraulicPump_OnTimer) ) { HydraulicPumpTimeExceeded = 1; }
        }
    }
    else
    {
        Set_Timer_Param(P_TIMER_RESTART, &HydraulicPump_StartDelay, NULL);

        if( TimerOn(&HydraulicPump_StopDelay) )
        {
            DO_BRK_HydPump = OFF;
            Set_Timer_Param(P_TIMER_RESTART, &HydraulicPump_OnTimer, NULL);
            HydraulicPumpTimeExceeded = 0;
        }
    }

    if( (OFF == DO_BRK_HydPump) && !HydraulicPumpTimeExceeded ) { V_Hydraulic_Equip0_State = STATE_AUTO_OFF; }
    else                                                        { V_Hydraulic_Equip0_State = STATE_AUTO_ON;  }
}


//-----------------------------------------------------------------------
static STATUS LocalCtrl(void)
{
    STATUS LocalStatus  = OS_SUCCESS;
    static U8 FirstTime = 1;
    static S32 HydraulicPressSensorState_Old = 100500;



    V_Hydraulic_OilPressure = (U32)LinearLimConvertion( AI_Hydraulic_PressureMa,
                                                        P_Hydraulic_OilPressInputMin,
                                                        P_Hydraulic_OilPressInputMax,
                                                        P_Hydraulic_OilPressOutputMin,
                                                        P_Hydraulic_OilPressOutputMax );
    SensorStatus();
    SensorStatusTxt( V_HydraulicPressSensorState,
                     &HydraulicPressSensorState_Old,
                     V_HydraulicPressSensorStateTxt,
                     sizeof(V_HydraulicPressSensorStateTxt) );

    //-------------------------------------------------------------------

    HOilPumpPermission();
    BlockHydraulicPump();

    switch(P_HydrOilPump_ServiceCtrl)
    {
        case MODE_AUTO :
        {
            HydraulicPumpCtrl(&FirstTime);
            if( !V_Hydraulic_Equip0_Mode )
            {
                DO_BRK_HydPump = OFF;
                V_Hydraulic_Equip0_State = STATE_STOPPED;
                FirstTime = 1;
            }
        }
        break;

        case MODE_SERVICE_OFF :
        {
            DO_BRK_HydPump = OFF;
            V_Hydraulic_Equip0_State = STATE_SERVICE_OFF;
            FirstTime = 1;
        }
        break;

        case MODE_SERVICE_ON :
        {
            DO_BRK_HydPump = ON;
            V_Hydraulic_Equip0_State = STATE_SERVICE_ON;
            FirstTime = 1;
        }
        break;
        default:
        {}
            break;
    }
    WriteState(V_Hydraulic_Equip0_State, V_Hydraulic_Equip0_StateTxt, sizeof(V_Hydraulic_Equip0_StateTxt));
    

    BrakeControl();
    UpdateTextMenus();
    UpdateSummations();

   return LocalStatus;
}

