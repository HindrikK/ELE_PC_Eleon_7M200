static BOOL TimerOn(STimer_t *pTimer)
{
    if( !pTimer->FirstRun )
    {
        pTimer->FirstRun = 1;
        pTimer->TimeStart = OS_GetTimeInMs();
    }

    if( pTimer->Start && !pTimer->OStatus )
    {
        pTimer->TimeRest = OS_GetTimeInMs() - pTimer->TimeStart;
        if ( pTimer->TimeRest >= pTimer->P_Time )
        {
            pTimer->TimeStart = 0;
            pTimer->OStatus   = 1;
        }
    }
    return pTimer->OStatus;
}
