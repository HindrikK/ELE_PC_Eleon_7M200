static S32 LinearConvertion(S32 Value, S32 MinIn, S32 MaxIn, S32 MinOut, S32 MaxOut)
{
    S32 Res;

    if (MaxIn != MinIn) { Res = ((S64)Value - MinIn)*(MaxOut - MinOut)/(MaxIn - MinIn) + MinOut; }
    else                { Res = MinOut; }

    return Res;
}
//-----------------------------------------------------------------------
static S32 LinearLimConvertion(S32 Value, S32 MinIn, S32 MaxIn, S32 MinOut, S32 MaxOut)
{
    S32 Res;
    S32 MinVal;
    S32 MaxVal;

    if (MinOut != MaxOut)
    {
      MinVal = MinOut;
      MaxVal = MaxOut;
    }
    else
    {
      MinVal = MaxOut;
      MaxVal = MinOut;
    }

    Res = LinearConvertion(Value, MinIn, MaxIn, MinOut, MaxOut);

    if      (Res < MinVal) { Res = MinVal; }
    else if (Res > MaxVal) { Res = MaxVal; }

    return Res;
}
