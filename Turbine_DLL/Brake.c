static U8 Hysteresis(S32 Input, S32 LowValue, S32 HighValue, U8 *State, U8 Invert)
{
    if( ALIBRARY_INVERT_DISABLE == Invert )
    {
        if( *State ) { *State = (Input < LowValue ) ? 0 : *State; }
        else         { *State = (Input > HighValue) ? 1 : *State; }
    }
    else if( ALIBRARY_INVERT_ENABLE == Invert )
    {
        if( !*State ) { *State = (Input < LowValue ) ? 1 : *State; }
        else          { *State = (Input > HighValue) ? 0 : *State; }
    }

    return *State;
}
