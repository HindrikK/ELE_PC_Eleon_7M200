U8 BlockHydraulicPump(void)
{
    if      ( V_Grid_ErrorActive     ) { V_Hydraulic_Equip0_Status = 1; }
    else if ( V_Inverter_StatusLVRT  ) { V_Hydraulic_Equip0_Status = 2; }
    else if ( StatusCodeActive(1213) ) { V_Hydraulic_Equip0_Status = 4; }        //(H) oper.time > max.
    else if ( StatusCodeActive(1239) ) { V_Hydraulic_Equip0_Status = 5; }        //(H) oil (P)  FB
	 else if ( StatusCodeActive(1298) ) { V_Hydraulic_Equip0_Status = 6; }
    else                               { V_Hydraulic_Equip0_Status = 0; }

    return V_Hydraulic_Equip0_Status;
}
