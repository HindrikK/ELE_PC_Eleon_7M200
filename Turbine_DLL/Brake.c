static struct StandardTimerVarStruct ValvesDelayTimer =
{
    0,                               // Timer value [ms]
    &P_Brake_ValvesDelay,            // TimeMax value [ms]
    TIMER_20MS,                      // Internal variable, always set to 0
    TIMER_STOP,                      // TIMER_DOWN / TIMER_STOP / TIMER_UP
    0                                // Standard timer called flag.
};

void SetGeneratorBrake(U8 State)
{
    static U8 initapp = 1;
    static U8 initrel = 1;

    switch(State)
    {
        case BRAKE_CONTROL_APPLY:
        {
            if(initapp)
            {
                ResetTimer(&ValvesDelayTimer);
                StartTimer(&ValvesDelayTimer);
                initapp = 0;
                initrel = 1;
            }
            DO_RotorBrake2    = VALVE_OPEN;

            if(StandardTimer(&ValvesDelayTimer))
            {
                DO_RotorBrake     = VALVE_CLOSE;
                V_BrakeCommand    = BRAKE_APPLY;
            }
            break;
        }
        case BRAKE_CONTROL_RELEASE:
        {
            if(initrel)
            {
                ResetTimer(&ValvesDelayTimer);
                StartTimer(&ValvesDelayTimer);
                initapp = 1;
                initrel = 0;
            }
            DO_RotorBrake     = VALVE_OPEN;
            if(StandardTimer(&ValvesDelayTimer))
            {
                DO_RotorBrake2    = VALVE_CLOSE;
                V_BrakeCommand    = BRAKE_RELEASE;
            }
            break;
        }
        default:
        {
            AppSystemLog(5000, "ERROR: Wrong generator brake state !!!!");
            break;
        }
    }
}
//---------------------------------------------------------------------------
U8 GeneratorBrakeReleased(void)
{
    if(  DO_RotorBrake  == VALVE_OPEN
      && DO_RotorBrake2 == VALVE_CLOSE )
    {//released
        return TRUE;
    }
    return FALSE;
}

//-----------------------------------------------------------------------
static void BrakeControl(void)
{
    if( BRAKE_MODE_MANUAL == V_Brake_Service )
    {
        if( (GetOperateState() == Service) || (V_ActiveControlProgram1 >= 50))
        {
            switch( V_ServiceValve )
            {
                case BRAKE_APPLY:
                {                    
                    if( V_ControllerRpm < P_BrakeManualMax )
                    {
                        SetGeneratorBrake( BRAKE_CLOSE );
                    }
                    break;
                }
                case BRAKE_RELEASE:
                {
                    SetGeneratorBrake( BRAKE_OPEN );
                    break;
                }
                default:
                {
                    V_Brake_Service = BRAKE_MODE_AUTO;
                    OS_VarDB_UpdateU8( V_Brake_Service_ID, V_Brake_Service );

                    V_ServiceValve = BRAKE_RELEASE;
                    OS_VarDB_UpdateU8( V_ServiceValve_ID, V_ServiceValve );
                    break;
                }
            }
        }
    }
    else
    {
        if( V_ServiceValve != BRAKE_RELEASE )
        {
            V_ServiceValve = BRAKE_RELEASE;
            OS_VarDB_UpdateU8( V_ServiceValve_ID, V_ServiceValve );
        }
        SetGeneratorBrake(V_Brake_Mode);
    }
}
//-----------------------------------------------------------------------
static void UpdateTextMenus(void)
{
    static U8 OldBrakeMode              = DUMMY_U8;
    static U8 OldBrakeCommand           = DUMMY_U8;
    static U8 OldGeneratorBrakeReleased = DUMMY_U8;
    
    // Update brake mode text
    //--------------------------------------------------
    if (V_Brake_Service != OldBrakeMode)
    {
        switch (V_Brake_Service)
        {
            case BRAKE_MODE_AUTO:
            {
                strcpy(V_BrakeModeText,"Auto");
                break;
            }
            case BRAKE_MODE_MANUAL:
            {
                strcpy(V_BrakeModeText,"Manual");
                break;
            }
            default :
            {
                strcpy(V_BrakeModeText,"UNKNOWN");
                break;
            }
        }
        OldBrakeMode = V_Brake_Service;
    }
    
    // Update brake state text
    //--------------------------------------------------
    if (V_BrakeCommand != OldBrakeCommand || GeneratorBrakeReleased() != OldGeneratorBrakeReleased)
    {
        if( BRAKE_RELEASE == V_BrakeCommand && FALSE == GeneratorBrakeReleased() )
        {
            strcpy( V_BrakeStateText, "Releasing" );
        }
        else if( BRAKE_RELEASE == V_BrakeCommand && TRUE == GeneratorBrakeReleased() )
        {
            V_Brake_State = BRAKE_STATE_RELEASED;
            strcpy( V_BrakeStateText, BRAKE_STATUS_TEXT_RELEASED );
        }
        else if( BRAKE_APPLY == V_BrakeCommand && TRUE == GeneratorBrakeReleased() )
        {
            strcpy( V_BrakeStateText, "Applying" );
        }
        else if( BRAKE_APPLY == V_BrakeCommand && FALSE == GeneratorBrakeReleased() )
        {
            V_Brake_State = BRAKE_STATE_APPLIED;
            strcpy( V_BrakeStateText, BRAKE_STATUS_TEXT_APPLIED );
        }
        else
        {
            V_Brake_State = BRAKE_STATE_APPLIED_ERR;
            strcpy( V_BrakeStateText, "UNKNOWN" );
        }
        
        OldBrakeCommand           = V_BrakeCommand;
        OldGeneratorBrakeReleased = GeneratorBrakeReleased();
    }
}
