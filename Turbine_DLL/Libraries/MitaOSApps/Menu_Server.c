static void Process(Client* client)
{
	MenuServer server = client->Server;


	int received = 0;
	int result;
	if (!client->LoggedIn&&!client->startedLogin) {
		received = OS_Recv(client->TCP_Socket, server->Recv_Buffer_TCP, 1, 0);
	}
	else {
		received = OS_Recv(client->TCP_Socket, server->Recv_Buffer_TCP, MENU_SERVER_RECV_BUFFER_TCP_SIZE, 0);
	}
	
	if (client->CurrentMenu!= NULL &&client->ForceUpdateMenu) {
		SendMenuLines(client, client->CurrentMenuGroup->Name, client->CurrentMenu->Name);
		client->ForceUpdateMenu = false;
	}
	// receive TCP command frame
	if (received > 0)
	{
		client->ReceivedCounter += 1;
		if (client->startedLogin){
			ReadUserInformationPacket(client, received);
			client->startedLogin = false;
			client->LoggedIn = true;
		
		}else if (client->WaitingForButtonLinkNum) {


			HandleMenuButtonClick(client,0, received);
			client->WaitingForButtonLinkNum = false;

		}
		else {
		//ADD Processing cases

			switch (server->Recv_Buffer_TCP[0])
			{
			case PROT_LINK_BUTTON_ACIVATED:
				if (received <= 1) {
					client->WaitingForButtonLinkNum = true;
				}
				else {

					HandleMenuButtonClick(client,1, received);
				}
				break;
			case PROT_CLIENT_UPDATE:
				HandleChangeValueCommand(client,received);
				break;
			case PROT_LOGIN:
				client->startedLogin = true;
				break;

			}
		}

	}
	else if (received == OS_NOT_CONNECTED)
	{
		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Client lost"); }
		AbortClient(client);
		return;
	}

	if (!client->startedLogin || !client->WaitingForButtonLinkNum) {
		SendPeriodicPacket(client);
		SendPeriodicUpdatePacket(client);
	}

}

//***********************************************************************
// STATE : Communication interface is disabled, waiting for enable command
//***********************************************************************
static void Disabled_State(BOOL firstentry, MenuServer server)
{
	//-----------------------------------------------------------------------
	//ENTRY ACTION
	//-----------------------------------------------------------------------
	if (firstentry)
	{
		if (server->StatusInfoEnabled) {
			strcpy(server->Status->StatusTxt, "Disabled");


			if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Interface disabled"); }
		}
		AbortAllConnections(server);
	}

	//-----------------------------------------------------------------------
	// EXIT CONDITIONS
	//-----------------------------------------------------------------------

	if (server->ServerEnabled) // enabling
	{
		server->StateFunctionPointer = Startup_State;
	}
}

//***********************************************************************
// STATE : Interface error state
//***********************************************************************
static void InterfaceError_State(BOOL firstentry, MenuServer server)
{
	//-----------------------------------------------------------------------
	//ENTRY ACTION
	//-----------------------------------------------------------------------
	if (firstentry)
	{
		if (server->StatusInfoEnabled) {
			strcpy(server->Status->StatusTxt, "Interface error");

			if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Interface error state"); }
		}
		server->ServerEnabled_prev = server->ServerEnabled;
	}

	//-----------------------------------------------------------------------
	// EXIT CONDITIONS
	//-----------------------------------------------------------------------

	if (server->ServerEnabled && !server->ServerEnabled_prev) // restarting when interface is reenabled
	{
		server->StateFunctionPointer = Startup_State;
	}

	server->ServerEnabled_prev = server->ServerEnabled;
}

//***********************************************************************
// STATE : Starting up interface
//***********************************************************************
static void Startup_State(BOOL firstentry, MenuServer server)
{
	//-----------------------------------------------------------------------
	//ENTRY ACTION
	//-----------------------------------------------------------------------
	if (firstentry)
	{
		if (server->StatusInfoEnabled) {
			strcpy(server->Status->StatusTxt, "Startup");

			if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Startup"); }
		}
		if (!(server->ServerEnabled)) // disabling
		{
			server->StateFunctionPointer = Disabled_State;
			return;
		}
		if (StartListeningIncoming_TCP(server) != 0) { server->StateFunctionPointer = InterfaceError_State; }
		server->StateFunctionPointer = Operational_State;
		return;
	}

}

