//returns bytes read, writes \0 to end
int ReadUTF(char * buf, int startIndex, int bufferLength, char * outString, int outStringLength) {
	
	if((bufferLength - startIndex) < 2) {
		return -1; //buffer too short  
	}
	int utfLength = (U16)((((U8)buf[startIndex]) * 256) + (U8)buf[startIndex+1]);		
	
	if (utfLength > (bufferLength - startIndex + 2)) {
		return -2;  // buffer dowes not hold whole utf string 
	}

	int i = 0;
	int currentPosition = startIndex + 2;
	for (; (i < outStringLength-1 && i<utfLength); i++, currentPosition++) 
	{
		outString[i] = buf[currentPosition];

	}
	outString[i] = '\0';
	

	return i+2;
}



