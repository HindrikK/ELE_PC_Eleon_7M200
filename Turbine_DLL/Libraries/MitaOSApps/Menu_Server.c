// updates all clients IP addresses in menu lines
static void UpdateAllClientsIPTxt(MenuServer server)
{
	if (!server->StatusInfoEnabled) { return; }

	Client* client;

	int i;
	for (i = 0; i < server->MaxNumOfClients; i++)
	{
		client = List_Select(server->myClients, i);     // get client
		if (client != NULL)
		{
			snprintf((char *)&(server->Status->Clients[i].ClientIp), sizeof(server->Status->LocalIPString), "%u.%u.%u.%u", client->ClientAddressTCP.id.is_ip_addrs[0], client->ClientAddressTCP.id.is_ip_addrs[1],
				client->ClientAddressTCP.id.is_ip_addrs[2], client->ClientAddressTCP.id.is_ip_addrs[3]);
			server->Status->Clients[i].srcPortTCP = client->ClientAddressTCP.port;
		}
		else
		{
			snprintf((char *)&(server->Status->Clients[i].ClientIp), sizeof(server->Status->LocalIPString), "\0");	// no client
			server->Status->Clients[i].srcPortTCP = 0;
		}
	}
}

MenuServer MenuServer_New( struct menu_group_struct *OS_MenuGroups, U32 scantime, S32 localIP)
{
	void* newObj;
	int i;
	STATUS Status;
	size_t ErrorEntry;

	Status = OS_VarDB_CreateLocals(LocalVars, sizeof(LocalVars) / sizeof(LocalVars[0]), &ErrorEntry, AppLocalParameterPosition());
	if (Status != 0)
	{
		
		return -1;
	}
	Status = OS_DoesVariablesExist(ModulInputs, MODUL_INPUTS_SIZE);
	if (Status != 0)
	{
		return -1;
	}

	int result = OS_Allocate_Memory(GetMemPool(), &newObj, sizeof(struct Menu_Server), OS_NO_SUSPEND);
	if (result != OS_SUCCESS) { return NULL; }

	((MenuServer)newObj)->OS_MenuGroups = OS_MenuGroups;
	((MenuServer)newObj)->LocalIP = localIP;
	((MenuServer)newObj)->NumOfClients = 0;

	((MenuServer)newObj)->Task_Scantime = scantime;
	((MenuServer)newObj)->periodicEnabled = false;
	((MenuServer)newObj)->onUpdatePeriodicEnabled = false;
	((MenuServer)newObj)->Status = NULL;
	((MenuServer)newObj)->StatusInfoEnabled = false;
	((MenuServer)newObj)->periodicPeriod = 5000;
	((MenuServer)newObj)->onUpdatePeriodicPeriod = 1000;
	((MenuServer)newObj)->MaxNumOfClients = MENU_SERVER_CLIENTSLISTCAPACITY;
	((MenuServer)newObj)->StateFunctionPointer = Startup_State;
	((MenuServer)newObj)->StateFunctionPointer_Prev = 0;


	// create new list
	((MenuServer)newObj)->myClients = List_New(MENU_SERVER_CLIENTSLISTCAPACITY, sizeof(Client *));
	if (((MenuServer)newObj)->myClients == NULL)
	{
#if defined I104S_DEBUG
		OS_com_printf("List_New failed\n");
#endif
		OS_DEBUGLOG_make_entry(0, "List_New failed in Eleon Interface");

		return NULL;
	}

	// clear buffers
	memset(((MenuServer)newObj)->Recv_Buffer_TCP, 0, sizeof(((MenuServer)newObj)->Recv_Buffer_TCP));
	memset(((MenuServer)newObj)->Send_Buffer_TCP, 0, sizeof(((MenuServer)newObj)->Send_Buffer_TCP));


	return (MenuServer)newObj;
}

S32 MenuServer_EnableStatusData(MenuServer server, MenuServer_StatusInfo statusInfo, CustomLog customLog)
{
	if (statusInfo->Type != 139) { return -2; }         // pointer to invalid StatusInfo structure
	if (customLog == NULL) { return -3; }               // CustomLog NULL

	server->Status = statusInfo;
	//set Status default values
	server->Status->NumOfClients = 0;
	strcpy(server->Status->StatusTxt, "Not Initalized\0");
	strcpy(server->Status->LocalIPString, "000.000.000.000\0");
	int i;
	for (i = 0; i < MENU_SERVER_CLIENTSLISTCAPACITY; i++) {
		server->Status->Clients[i].Received = 0;
		server->Status->Clients[i].Sent = 0;
		snprintf((char *)&(server->Status->Clients[i].ClientIp), sizeof(server->Status->LocalIPString), "\0");	// no client
		server->Status->Clients[i].srcPortTCP = 0;
	}
	server->Status->InterfaceEventLog = customLog;
	server->StatusInfoEnabled = true;
	if (server->Status->InterfaceEventLog == NULL)
	{

		OS_DEBUGLOG_make_entry(0, "IEC104S eventlog NULL");

		return NULL;
	}

	return 0;
}
