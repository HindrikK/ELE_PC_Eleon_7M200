static void AbortClient(Client *client)
{
	MenuServer server = client->Server;
	int Status = 0;

#if defined I104S_DEBUG
	OS_com_printf("I104S starting abort\r\n");
#endif
	Status = OS_Abort(client->TCP_Socket);
	if (Status != OS_SUCCESS)
	{
		OS_DEBUGLOG_make_entry(0, "Error aborting service TCP socket: %d\r\n", Status);
		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Error aborting TCP"); }
	}

	int index = List_Find(server->myClients, client);
	List_Remove(server->myClients, index); // remove disconnected client from list
	server->NumOfClients = List_Count(server->myClients);
	if (server->StatusInfoEnabled) {
		server->Status->NumOfClients = server->NumOfClients;
	}
	UpdateAllClientsIPTxt(server);
	// reset counters
	client->ReceivedCounter = 0;
	client->SentCounter = 0;
	OS_Deallocate_Memory(client); // deallocate client struct data memory
	if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Client aborted"); }
}

static void AbortAllConnections(MenuServer server)
{
	int Status = 0;
	int index;
	int nextIndex;

	// abort listening socket
	if (server->ListenTCP_Socket)
	{
		Status = OS_Abort(server->ListenTCP_Socket);
		if (Status != OS_SUCCESS)
		{
			OS_DEBUGLOG_make_entry(0, "Error closing listen TCP socket: %d\r\n", Status);
			if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Error closing TCP listener"); }

		}
	}

	if (server->NumOfClients > 0)
	{
		// get first client
		index = List_First(server->myClients);                  // get index of first client in list

		int clientsCount = server->NumOfClients;
		int i;
		for (i = 0; i < clientsCount; i++)
		{
			nextIndex = List_Next(server->myClients, index);    // get index of next client in list
			AbortClient(List_Select(server->myClients, index));
			index = nextIndex;
		}
	}

#if defined I104S_DEBUG
	OS_com_printf("All connections closed\r\n");
#endif
}

static STATUS StartListeningIncoming_TCP(MenuServer server)
{
	STATUS LocalStatus = 0;
	int result;

	// Create new socket for listening inbound TCP connections
	result = OS_Socket(OS_FAMILY_IP, OS_TYPE_STREAM, OS_NONE);
	if (result < 0)
	{
		OS_DEBUGLOG_make_entry(0, "Can't create \"ListenTCP_Socket\" socket: %d\r\n", result);
		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Error creating TCP socket"); }
#if defined I104S_DEBUG
		OS_com_printf("Can't create \"ListenTCP_Socket\" socket: %d\r\n", result);
#endif
		return result;
	}
	else
	{
		server->ListenTCP_Socket = result;
		OS_DEBUGLOG_make_entry(0, "Socket \"ListenTCP_Socket\" created: %d\r\n", result);
		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "TCP socket created"); }
#if defined I104S_DEBUG
		OS_com_printf("Socket \"ListenTCP_Socket\" created: %d\r\n", result);
#endif
	}



	// Initialize local TCP endpoint
	server->LocalAddressTCP.family = OS_FAMILY_IP;
	server->LocalAddressTCP.port = server->LocalPortTCP;
	server->LocalAddressTCP.id.is_ip_addrs[0] = (server->LocalIP & 0x000000ff);
	server->LocalAddressTCP.id.is_ip_addrs[1] = (server->LocalIP & 0x0000ff00) >> 8;
	server->LocalAddressTCP.id.is_ip_addrs[2] = (server->LocalIP & 0x00ff0000) >> 16;
	server->LocalAddressTCP.id.is_ip_addrs[3] = (server->LocalIP & 0xff000000) >> 24;
	server->LocalAddressTCP.name = "WP4x00_TCP";
	if (server->StatusInfoEnabled) {
		sprintf((server->Status->LocalIPString), "%u.%u.%u.%u", server->LocalAddressTCP.id.is_ip_addrs[0], server->LocalAddressTCP.id.is_ip_addrs[1], server->LocalAddressTCP.id.is_ip_addrs[2], server->LocalAddressTCP.id.is_ip_addrs[3]);
	}
	// Bind local endpoint to socket
	LocalStatus = OS_Bind(server->ListenTCP_Socket, &server->LocalAddressTCP, 0);
	if (LocalStatus <= 0)
	{
		//OS_DEBUGLOG_make_entry(0, "Error binding TCP socket: %d, localIP: %s\r\n", LocalStatus, (server->Status->LocalIPString));
		OS_DEBUGLOG_make_entry(0, "Error binding TCP socket in menuserver: %d, localIP: %s\r\n", LocalStatus);

		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "TCP socket binding failed"); }
#if defined I104S_DEBUG
		OS_com_printf("Error binding TCP socket: %d, localIP: %s\r\n", LocalStatus, V_I104S_LocalIP);
#endif
		return LocalStatus;
	}
	else
	{
		OS_DEBUGLOG_make_entry(0, "TCP socket binded: %d\r\n", LocalStatus);
#if defined I104S_DEBUG
		OS_com_printf("TCP socket binded: %d\r\n", LocalStatus);
#endif
	}

	// Start listening incoming connections
	LocalStatus = OS_Listen(server->ListenTCP_Socket, 0);
	if (LocalStatus != OS_SUCCESS)
	{
		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Started listening TCP"); }
#if defined I104S_DEBUG
		OS_com_printf("Error listening TCP socket: %d\r\n", LocalStatus);
#endif
		return LocalStatus;
	}
	else
	{
		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Started listening TCP"); }
#if defined I104S_DEBUG
		OS_com_printf("Started listening TCP socket\r\n");
#endif
	}

	return LocalStatus;
}

// accept incoming connections and handle LogIn procedure
static void AcceptConnection(MenuServer server)
{
	int result;
	struct addr_struct address;
	U16 TCP_Socket;
	Client* newClient;

	result = 0;
	OS_Fcntl(server->ListenTCP_Socket, OS_SETFLAG, OS_NO_BLOCK);
	result = OS_Accept(server->ListenTCP_Socket, &address, 0);
	OS_Fcntl(server->ListenTCP_Socket, OS_SETFLAG, OS_BLOCK);

	// new client connection received
	if (result >= 0)
	{
		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "New incoming connection"); }

		if (server->NumOfClients < server->MaxNumOfClients)     // don't accept any new connection when already max number of clients
		{
			TCP_Socket = result;

		}
		else
		{
			if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Connection refused(max limit)"); }
			OS_Close_Socket(result);
			return;
		}

		// create new client in memory
		result = OS_Allocate_Memory(GetMemPool(), (void**)&newClient, sizeof(Client), OS_NO_SUSPEND);  // allocate memory for new client struct
		if (result != OS_SUCCESS)
		{
			if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Failed to allocate memory for new client struct"); }
			OS_Close_Socket(TCP_Socket);
			return;
		}

		// PRESET VALUES
		newClient->ClientAddressTCP.family = OS_FAMILY_IP;
		newClient->ClientAddressTCP.id.is_ip_addrs[0] = address.id.is_ip_addrs[0];
		newClient->ClientAddressTCP.id.is_ip_addrs[1] = address.id.is_ip_addrs[1];
		newClient->ClientAddressTCP.id.is_ip_addrs[2] = address.id.is_ip_addrs[2];
		newClient->ClientAddressTCP.id.is_ip_addrs[3] = address.id.is_ip_addrs[3];
		newClient->ClientAddressTCP.name = "WP4x00_TCP";
		newClient->ClientAddressTCP.port = address.port;
		newClient->TCP_Socket = TCP_Socket;
		newClient->Server = server;
		newClient->KeepAlive_Timer = 0;
		newClient->ReceivedCounter = 0;
		newClient->SentCounter = 0;
		newClient->StartDT = false;
		newClient->AutoTimer = 0;
		newClient->onUpdateAutoTimer = 0;
		newClient->UpdatedDataSent = true;
		newClient->startedLogin = false;
		newClient->WaitingForButtonLinkNum = false;
		newClient->LoggedIn = false;
		newClient->CurrentMenuGroup = server->OS_MenuGroups;
		newClient->CurrentMenuGroup = server->OS_MenuGroups->first_menu;
		newClient->CurrentMenu = NULL;
		newClient->ForceUpdateMenu = false;
		newClient->AccessLevel = PASSWORD_LEVEL_UNLOGGED_IN;

		newClient->CurrenStartRowNum = 2;

		// add new client to the list
		List_Trim(server->myClients);	// trim the list just in case some clients have dropped and the list is not continuous
		result = List_Add(server->myClients, newClient);

		if (result < 0) // when adding new client into list fails
		{
			if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Failed to add new client to list"); }
			OS_Deallocate_Memory(newClient);
			OS_Close_Socket(TCP_Socket);
			return;
		}

		server->NumOfClients = List_Count(server->myClients);
		if (server->StatusInfoEnabled) {
			server->Status->NumOfClients = server->NumOfClients;
		}
		UpdateAllClientsIPTxt(server);

		if (server->StatusInfoEnabled) { CustomLog_MakeEntry(server->Status->InterfaceEventLog, "Client accepted"); }
		return;
	}

	return;
}

static int  WriteUTF(char charArray[], char* string, int startPosition)
{
	int position = startPosition + 2;
	int i;
	for (i = 0; string[i] != '\0'; i++) {
		charArray[position++] = string[i];
	}
	//length short	
	charArray[startPosition] = (U8)((i >> 8) & 0xff);
	charArray[startPosition + 1] = (U8)(i & 0xff);
	return i + 2;

}

static int GetVariableType(varid_t varid)
{
	int result;
	type_t type;
	result = OS_VarDB_GetVariableType(varid, &type);
	if (result != OS_SUCCESS)
	{
		return -1;
	}
	switch (type) {
	case TYPE_BOOL:
		return UBYTE;
	case TYPE_U8:
		return UBYTE;
	case TYPE_U16:
		return USHORT;
	case TYPE_U32:
		return UINT;
	case TYPE_U64:
		return ULONG;
	case TYPE_S8:
		return SBYTE;
	case TYPE_S16:
		return SSHORT;
	case TYPE_S32:
		return SINT;
	case TYPE_S64:
		return SLONG;
	case TYPE_F32:
		return FLOAT;
	case TYPE_F64:
		return DOUBLE;
	}

	if ((type >= TYPE_STRING) && (type < TYP_ESUM)) {
		return  UNISTRING;

	}
	return -1;
}

