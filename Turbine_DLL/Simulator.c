	LocalStatus = OS_VarDB_GetVariableID("V_SimulationModeTxt", &varID);
	if (LocalStatus < 0) { return LocalStatus; }
	V_SimulationModeTxt_value = varID->c.Value.string;
	LocalStatus = OS_VarDB_GetVariableID("V_SimulationStateTxt", &varID);
	if (LocalStatus < 0) { return LocalStatus; }
	V_SimulationStateTxt_value = varID->c.Value.string;
	LocalStatus = OS_VarDB_GetVariableID("V_SimStartTurbine", &varID);
	if (LocalStatus < 0) { return LocalStatus; }
	V_SimStartTurbine_value = &(varID->c.Value.bool);
