//**************************************************************************
// Change simulation mode from OS menu
//**************************************************************************
void SimulationModeUpdate()
{
	if(SimulationModeID_Prev != *P_SimulationModeID_value)			// detect change in simulation mode
	{
		switch(*P_SimulationModeID_value)
		{
			case 0:
				Sim_ModeChanged = true;
				SimulationMode_Prev = SimulationMode;
				SimulationMode = SimulationManual;			// assign pointer to function
				break;
			
			case 1:
				Sim_ModeChanged = true;
				SimulationMode_Prev = SimulationMode;
				SimulationMode = Simulator_TurbineApplication_Control;
				break;
			
			case 2:
				Sim_ModeChanged = true;
				SimulationMode_Prev = SimulationMode;
				SimulationMode = Simulator_GHApplication_Control;
				break;
			
			default:
				Sim_ModeChanged = true;
				SimulationMode_Prev = SimulationMode;
				SimulationMode = SimulationManual;
				*P_SimulationModeID_value = false;
				break;
		}
		SimulationModeID_Prev = *P_SimulationModeID_value;
	}
}
