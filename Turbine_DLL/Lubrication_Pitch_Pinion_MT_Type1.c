void PPLubricationPeriodicExtraCtrl( U8 NewModeFlag )
{
static U8 FirstTime = 1;
static U8 State = PP_LUB_AUTO_OFF;

    if( FirstTime )
    {
        FirstTime = 0;
        State = PP_LUB_AUTO_OFF;
        if( (B_PPLubricationTimer > 0) && (B_PPLubricationTimer < P_LubricationPP_PumpOffTime) )
        {
            TimerSet(&PPLubrication_OffTimer, B_PPLubricationTimer);
            TimerStart(&PPLubrication_OffTimer);
        }
        else
        {
            TimerReset(&PPLubrication_OffTimer);
        }
    }

    if( NewModeFlag )
    {
        TimerReset(&PPLubrication_OffTimer);
        State = PP_LUB_AUTO_OFF;
    }

    if( BlockPPLubrication() )
    {
        DO_LubricationPP_Pump = MB_LUB_PUMP_OFF;
        strncpy(V_LubricationPP_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationPP_TimeIndicationTxt));
        V_Lub_Equip0_State = STATE_BLOCKED;
    }
    else
    {
        switch( State )
        {
            case PP_LUB_AUTO_OFF:
            {
                DO_LubricationPP_Pump = MB_LUB_PUMP_OFF;
                V_Lub_Equip0_State = STATE_AUTO_OFF;
                if( TimerCheck(&PPLubrication_OffTimer) )
                {
                    B_PPLubricationTimer = 0;
                    if( (V_BladeSystem_PositionAct >= P_PitchLubricationBladeAngleStop) &&
                        (V_BladeSystem_PositionAct <= P_PitchLubricationBladeAngleStart) &&
                        ( MANAGER_MODE_ENABLE == V_Lub_Equip0_Mode ) )
                    {
                        ResetCycle(&LubricationPP_StrokeCtrl, DI_LubricationPP_PumpFailure);
						      StartCycle(&LubricationPP_StrokeCtrl);
                        State = PP_LUB_AUTO_ON;
                    }
                    else
                    {
                        StopCycle(&LubricationPP_StrokeCtrl);
                        State = PP_LUB_SUSPENDED;
                    }
                }
                else
                {
                    TimeToText(TimerLeft(&PPLubrication_OffTimer), V_LubricationPP_TimeIndicationTxt, sizeof(V_LubricationPP_TimeIndicationTxt));
                    B_PPLubricationTimer = TimerLeft(&PPLubrication_OffTimer);  //  Backup timer
                }
            }
            break;

            case PP_LUB_SUSPENDED:
            {
                DO_LubricationPP_Pump = MB_LUB_PUMP_OFF;
                V_Lub_Equip0_State = STATE_SUSPENDED;
                strncpy(V_LubricationPP_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationPP_TimeIndicationTxt));
                if( (V_BladeSystem_PositionAct >= P_PitchLubricationBladeAngleStop) &&
                    (V_BladeSystem_PositionAct <= P_PitchLubricationBladeAngleStart) &&
                    ( MANAGER_MODE_ENABLE == V_Lub_Equip0_Mode ) &&
                    ( 0 == V_Inverter_StatusLVRT ) )
                {
                    StartCycle(&LubricationPP_StrokeCtrl);
                    State = PP_LUB_AUTO_ON;
                }
                if ( V_Inverter_StatusLVRT )                                                { V_Lub_Equip0_Status = PP_LUB_STATUS_LVRT; }
                else if (MANAGER_MODE_DISABLE == V_Lub_Equip0_Mode)                         { V_Lub_Equip0_Status = PP_LUB_STATUS_LM_BLOCK; }
                else if( (V_BladeSystem_PositionAct < P_PitchLubricationBladeAngleStop) ||
                         (V_BladeSystem_PositionAct > P_PitchLubricationBladeAngleStart))   { V_Lub_Equip0_Status = PP_LUB_STATUS_BLADE_ANGLE; }
                else                                                                        { V_Lub_Equip0_Status = PP_LUB_STATUS_OK; }
            }
            break;

            case PP_LUB_AUTO_ON:
            {
                V_Lub_Equip0_State = STATE_AUTO_ON;
                if( WaitCycle(DI_LubricationPP_PumpFailure, P_LubricationPP_Stroke_ON, &LubricationPP_StrokeCtrl) )
                {
                    TimerReset(&PPLubrication_OffTimer);
                    State = PP_LUB_AUTO_OFF;
                }
                else
                {
                    if( (MANAGER_MODE_DISABLE == V_Lub_Equip0_Mode) || V_Inverter_StatusLVRT )
                    {
                        DO_LubricationPP_Pump = MB_LUB_PUMP_OFF;
                        StopCycle(&LubricationPP_StrokeCtrl);
                        State = PP_LUB_SUSPENDED;
                    }
                    else
                    {
                        DO_LubricationPP_Pump = MB_LUB_PUMP_ON;
                    }
                }
            }
            break;

            default: { } break;
        }
    }
}
