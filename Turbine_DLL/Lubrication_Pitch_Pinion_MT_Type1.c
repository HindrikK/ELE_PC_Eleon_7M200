void PPLubricationAutoCtrl(U8 ServiceModeChanged)
{
static U8 InternalModeOldValue = 0;
static U8 FirstTime = 1;
       U8 NewModeFlag = 0;

    if( FirstTime )
    {
        FirstTime = 0;
        InternalModeOldValue = P_LubricationPP_InternalMode;
    }
    if( InternalModeOldValue != P_LubricationPP_InternalMode )
    {
        InternalModeOldValue = P_LubricationPP_InternalMode;
        NewModeFlag = 1;
    }
    if( ServiceModeChanged ) { NewModeFlag = 1; }

    switch( P_LubricationPP_InternalMode )
    {
        case LUB_MODE_PERIODIC_EXTRA_CTRL: { PPLubricationPeriodicExtraCtrl(NewModeFlag); } break;
        case LUB_MODE_PERIODIC_SIMPLE    : { PPLubricationPeriodicSimple(NewModeFlag);    } break;
        case LUB_MODE_ALWAYS_ON          : { PPLubricationAlwaysOn(NewModeFlag);          } break;
        default:
        {
            DO_LubricationPP_Pump = MB_LUB_PUMP_OFF;
            strncpy(V_LubricationPP_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationPP_TimeIndicationTxt));
            V_Lub_Equip0_State = STATE_AUTO_OFF;
        }
        break;
    }
}

//________________________________________________________________________________________
static STATUS LocalCtrl(void)
{
    STATUS LocalStatus = 0;
    static U8 FirstTime = 1;
    static U8 P_LubricationPP_PumpServiceCtrl_old;
           U8 ServiceModeChanged = 0;

    if( FirstTime )
    {
        AssignParameters();
        FirstTime = 0;
        strncpy(V_LubricationPP_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationPP_TimeIndicationTxt));
        P_LubricationPP_PumpServiceCtrl_old = P_LubricationPP_PumpServiceCtrl;
    }

    if( P_LubricationPP_PumpServiceCtrl_old != P_LubricationPP_PumpServiceCtrl )
    {
        P_LubricationPP_PumpServiceCtrl_old = P_LubricationPP_PumpServiceCtrl;
        ServiceModeChanged = 1;
    }

    if( BlockPPLubrication() ) { V_Lub_Equip0_State = STATE_BLOCKED; }      //Retrieve Status

    switch( P_LubricationPP_PumpServiceCtrl )
    {
        case MODE_AUTO :
        {
            PPLubricationAutoCtrl(ServiceModeChanged);
        }
        break;

        case MODE_SERVICE_OFF :
        {
            DO_LubricationPP_Pump = MB_LUB_PUMP_OFF;
            V_Lub_Equip0_State = STATE_SERVICE_OFF;
            V_Lub_Equip0_Status = PP_LUB_STATUS_OK;
            strncpy(V_LubricationPP_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationPP_TimeIndicationTxt));
        }
        break;

        case MODE_SERVICE_ON :
        {
            DO_LubricationPP_Pump = MB_LUB_PUMP_ON;
            V_Lub_Equip0_State = STATE_SERVICE_ON;
            V_Lub_Equip0_Status = PP_LUB_STATUS_OK;
            strncpy(V_LubricationPP_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationPP_TimeIndicationTxt));
        }
        break;

        default:
        {
        }
        break;
    }

    AssignParameters();

    // update object State and Status and write to VDB if updated
    WriteState (V_Lub_Equip0_State , V_Lub_Equip0_StateTxt , sizeof(V_Lub_Equip0_StateTxt ));
    WriteStatus(V_Lub_Equip0_Status, V_Lub_Equip0_StatusTxt, sizeof(V_Lub_Equip0_StatusTxt));

    return LocalStatus;
}

