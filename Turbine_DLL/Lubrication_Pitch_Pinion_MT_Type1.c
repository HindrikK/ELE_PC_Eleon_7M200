enum { STOP_CYCLE, START_CYCLE };
typedef struct
{
    U8 cycle;
    U8 old_data;
    U8 state;
}cycle_t;

static void StartCycle(cycle_t *a)
{
    a->state = START_CYCLE;
}
static void StopCycle(cycle_t *a)
{
    a->state = STOP_CYCLE;
}
static void ResetCycle(cycle_t *a, U8 data)
{
    a->old_data = data;
    a->cycle = 0;
    a->state = STOP_CYCLE;
}
static U8 WaitCycle(U8 data, U8 cycle_req, cycle_t *a)
{
    if( STOP_CYCLE == a->state )
    {
        return 0;
    }

    if(data != a->old_data)
    {
        a->cycle++;
        a->old_data = data;
    }

    return (cycle_req == (a->cycle/2));
}
