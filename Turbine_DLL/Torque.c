struct tNotch
{
    U8  OK;
    F32 W1;
    F32 W2;
    F32 D1;
    F32 D2;
    F32 DT;
    U8  Init;
    F32 Num[3];
    F32 Den[3];
    F32 Work[4];
};

struct tNotch TorqueNotchStruct = {0};

void NotchParaUpdate(void)
{
    TorqueNotchStruct.W1   = 2 * PI * (F32)P_TorqueNotchFrq / 1000.0F;      // Omega = 2 * pi * f
    TorqueNotchStruct.W2   = 2 * PI * (F32)P_TorqueNotchFrq / 1000.0F;      // Omega1 = Omega2
    TorqueNotchStruct.D1   = (F32)P_TorqueNotchDampingNom   / 1000.0F;      // Damping nominator
    TorqueNotchStruct.D2   = (F32)P_TorqueNotchDampingDenom / 1000.0F;      // Damping denominator
    TorqueNotchStruct.DT   = SAMPLE_TIME;                                   // Sample time 20 mS
    TorqueNotchStruct.OK   = P_TorqueNotchON;                               // Notch filter on/off
    TorqueNotchStruct.Init = 1;                                             // When parameters changed, set InitFlag

   if( P_TorqueNotchON )
   {
      strncpy( V_NotchFilterEnabledText, "ENABLED", sizeof(V_NotchFilterEnabledText) );
   }
   else
   {
      strncpy( V_NotchFilterEnabledText, "DISABLED", sizeof(V_NotchFilterEnabledText) );
   }
}

F32 TorqueNotchFilter (F32 InputTF)
{
    F32 rWT1, rWT2;
    F32 OutputTF;
    U8 i;
    static F32 K  = 0;
    static F32 Kn = 0;
    static F32 K0 = 0;
    static F32 K1 = 0;
    static F32 K2 = 0;
    static F32 K3 = 0;
    static F32 K4 = 0;

    if( TorqueNotchStruct.OK )
    {
        if( TorqueNotchStruct.Init == 1 )
        {
            rWT1 = TorqueNotchStruct.W1 * TorqueNotchStruct.DT;
            TorqueNotchStruct.Num[0] =  4.0F + (4.0F * TorqueNotchStruct.D1 * rWT1) + (rWT1 * rWT1);
            TorqueNotchStruct.Num[1] = -8.0F + (2.0F * rWT1 * rWT1);
            TorqueNotchStruct.Num[2] =  4.0F - (4.0F * TorqueNotchStruct.D1 * rWT1) + (rWT1 * rWT1);

            rWT2 = TorqueNotchStruct.W2 * TorqueNotchStruct.DT;
            TorqueNotchStruct.Den[0] =  4.0F + (4.0F * TorqueNotchStruct.D2 * rWT2) + (rWT2 * rWT2);
            TorqueNotchStruct.Den[1] = -8.0F + (2.0F * rWT2 * rWT2);
            TorqueNotchStruct.Den[2] =  4.0F - (4.0F * TorqueNotchStruct.D2 * rWT2) + (rWT2 * rWT2);


            for( i=0; i<=2; i++ )
            {
                TorqueNotchStruct.Num[i] *= (TorqueNotchStruct.W2 * TorqueNotchStruct.W2);
                TorqueNotchStruct.Den[i] *= (TorqueNotchStruct.W1 * TorqueNotchStruct.W1);
            }

            TorqueNotchStruct.Work[0] = InputTF;
            TorqueNotchStruct.Work[1] = InputTF;
            TorqueNotchStruct.Work[2] = InputTF;
            TorqueNotchStruct.Work[3] = InputTF;

            if   ( TorqueNotchStruct.Den[0] != 0 ) { K = 1 / TorqueNotchStruct.Den[0]; }
            else                                   { K = 0; }

            Kn = -K;
            K0 = TorqueNotchStruct.Num[0] * K;
            K1 = TorqueNotchStruct.Num[1] * K;
            K2 = TorqueNotchStruct.Num[2] * K;
            K3 = TorqueNotchStruct.Den[1] * Kn;
            K4 = TorqueNotchStruct.Den[2] * Kn;
            TorqueNotchStruct.Init = 0;
        }

        OutputTF = (K0 * InputTF)                   +
                   (K1 * TorqueNotchStruct.Work[0]) +
                   (K2 * TorqueNotchStruct.Work[1]) +
                   (K3 * TorqueNotchStruct.Work[2]) +
                   (K4 * TorqueNotchStruct.Work[3]);

        TorqueNotchStruct.Work[3] = TorqueNotchStruct.Work[2];
        TorqueNotchStruct.Work[2] = OutputTF;
        TorqueNotchStruct.Work[1] = TorqueNotchStruct.Work[0];
        TorqueNotchStruct.Work[0] = InputTF;

        return OutputTF;
    }
    else
    {
        return InputTF;
    }
}

