// Abstract: Find the smallest segment/range, in the active torque table, which
//           embraces the specified RPM value.
//
// Notice:
//    The function produces those two index values for "SegL" and "SegR" which will
//    satisfy the condition below with the closest RPM values from the active torque
//    table:
//       ActiveTable(SegL) <= GeneratorRpm < ActiveTable(SegR).
//
//    Special cases are:
//       GeneratorRpm < ActiveTable(SegL), output: SegL = SegR = <first table entry>.
//       ActiveTable(SegR) <= GeneratorRpm, output: SegL = SegR = <last table entry>.
//
// Arguments:
//    GeneratorRpm - Target rpm value.
//    SegL - Table index to identify the left/front part of the segment.
//    SegR - Table index to identify the right/rear part of the segment.
//
static void FindSegmentActTbl(/*In*/ U32 GeneratorRpm, /*Out*/ U8 *SegL, /*Out*/ U8 *SegR)
{
    register U32 GenRpm; // Allow compiler to do loop performance optimization.

    GenRpm = GeneratorRpm;
    *SegL  = 0;
    *SegR  = 0;

    // Range check.
    if( GenRpm <= *ActTblRpm[*SegL] )
    {
        // Specified RPM is less than the smallest RPM value in the torque table.
        // Exit here - leaving both SegL and SegR indexing into the table front entry.
        return;
    }

    // Locate SegR as the first RPM value, in the table, which is greater than the
    // specified RPM value.
    for( ; *SegR < TORQUE_TBL_MAX && GenRpm >= *ActTblRpm[*SegR]; (*SegR)++ );

    // Range check.
    if( *SegR >= TORQUE_TBL_MAX )
    {
        // Specified RPM is greater than the greatest RPM value in the torque table.
        // Set both SegL and SegR to index the table rear entry and exit here.
        *SegR = TORQUE_TBL_MAX - 1;
        *SegL = *SegR;

        return;
    }

    // Found SegR. Now set SegL to index the immediate, previous entry.
    *SegL = *SegR - 1;
}

// Abstract: Find the smallest segment/range, in the active torque table, which
//           embraces the specified POWER value.
//
// Notice:
//
//    Power is calculated as : Power = Nm * 2 * PI * Rpm /60
//
//    The function produces those two index values for "SegL" and "SegR" which will
//    satisfy the condition below with the closest RPM values from the active torque
//    table:
//       ActiveTable(SegL) <= Power < ActiveTable(SegR).
//
//    Special cases are:
//       Power < ActiveTable(SegL), output: SegL = SegR = <first table entry>.
//       ActiveTable(SegR) <= Power, output: SegL = SegR = <last table entry>.
//
// Arguments:
//    PowerLimit - Max. power value.
//    SegL - Table index to identify the left/front part of the segment.
//    SegR - Table index to identify the right/rear part of the segment.
//
static void FindSegmentActTblPower(/*In*/ U32 PowerLimit, /*Out*/ U8 *SegL, /*Out*/ U8 *SegR)
{
    register U32 Power; // Allow compiler to do loop performance optimization.

    Power = PowerLimit;

    *SegL = 0;
    *SegR = 0;

    // Range check.

    if( Power <= POWER(*ActTblRpm[*SegL], *ActTblTorque[*SegL]) )
    {
        // Specified Power is less than the smallest RPM value in the torque table.
        // Exit here - leaving both SegL and SegR indexing into the table front entry.
        return;
    }

    // Locate SegR as the first RPM value, in the table, which is greater than the
    // specified Power value.
    for( ; *SegR < TORQUE_TBL_MAX && Power >= POWER(*ActTblRpm[*SegR], *ActTblTorque[*SegR]); (*SegR)++ );

    // Range check.
    if( *SegR >= TORQUE_TBL_MAX )
    {
        // Specified Power is greater than the greatest RPM value in the torque table.
        // Set both SegL and SegR to index the table rear entry and exit here.
        *SegR = TORQUE_TBL_MAX - 1;
        *SegL = *SegR;

        return;
    }

    // Found SegR. Now set SegL to index the immediate, previous entry.
    *SegL = *SegR - 1;
}



// Abstract: Look the specified generator rpm value up in the currently active
//           torque table and (possibly) interpolate a torque value on the result
//           as necessary.
//
// Arguments:
//    GeneratorRpm - the value for which torque should be calculated.
//
// Return value:
//    Torque value that corresponds to GeneratorRpm.
//
S32 GetTorqueFromRpmActTbl(/*In*/ U32 GeneratorRpm)
{
    U8  SegL;
    U8  SegR;
    S32 Diff;
    S32 TorqueL;
    S32 TorqueR;
    S32 GenRpmL;
    S32 GenRpmR;
    S32 GenRpm;
    S32 TorqueI;
    S32 TorqueDiff;

    // Look the specified generator RPM value up in the currently active
    // torque table.
    FindSegmentActTbl(GeneratorRpm, &SegL, &SegR);

    Diff = *ActTblRpm[SegR] - *ActTblRpm[SegL];
    if( !Diff )
    {
        // Prevent a division by zero on special cases (when SegL and SegR are both
        // set to the same index value - pointing either to the front or the rear of
        // the table).
        // Also applicable when user enters two identical values for two rpm entries
        // in the table.
        return *ActTblTorque[SegL];
    }

    // Align floating points (at SCALE 1000), for fixed point integer math, and interpolate between
    // the segment boundary values.

    GenRpm  = GeneratorRpm;
    GenRpmL = *ActTblRpm[SegL];
    GenRpmR = *ActTblRpm[SegR];
    TorqueL = *ActTblTorque[SegL] * 10;
    TorqueR = *ActTblTorque[SegR] * 10;  

    // Original formula:
    //    WP3100:
    //       ((float)((((signed long)((signed long)(GeneratorRpm)- *(pG-1))) *
    //       (((signed long)*pT - (signed long)*(pT-1))*100L) /(signed long)Diff + ((signed long)*(pT-1)*100L))) / 100.0)
    //    WP4000:
    //       ((GenRpm - GenRpmL) * (TorqueR - TorqueL) * 100L / Diff + TorqueL * 100L) / 100L;
    //
    // Reducing formula and moving division by "Diff" (to avoid variable overflow):
    //    ((GenRpm - GenRpmL) * (TorqueR - TorqueL) * 100L / Diff + TorqueL * 100L) / 100L;
    //    ((GenRpm - GenRpmL) * (TorqueR - TorqueL) * 100L / Diff) / 100L + (TorqueL * 100L) / 100L;
    //    (GenRpm - GenRpmL) * (TorqueR - TorqueL) * 100L / Diff / 100L + TorqueL * 100L / 100L;
    //    (GenRpm - GenRpmL) * (TorqueR - TorqueL) / Diff + TorqueL;
    //    (GenRpm - GenRpmL) / Diff * (TorqueR - TorqueL) + TorqueL;

    // Select interpolation formula according to gradient of torque curve part :
    //
    // TorqueDiff < 0 => negative gradient => Use negative linear interpolation
    // TorqueDiff > 0 => positive gradient => Use positive linear interpolation
    //
    TorqueDiff = *ActTblTorque[SegR] - *ActTblTorque[SegL];
    
    if( TorqueDiff < 0 ) // Diff < 0 => Negative linear interpolation
    {
        TorqueI = (GenRpmR - (U64)GenRpm) * (TorqueL - TorqueR) / Diff + TorqueR;
    }
    else  // Diff > 0 => Positive linear interpolation
    {
        TorqueI = ((U64)GenRpm - GenRpmL) * (TorqueR - TorqueL) / Diff + TorqueL;
    }

    // Return (SCALE 10) torque value.
    return (TorqueI / 10);
}

// Abstract: Look the specified generator rpm value up in the currently active
//           torque table and (possibly) interpolate a torque value on the result
//           as necessary.
//
// Arguments:
//    GeneratorRpm - the value for which torque should be calculated.
//
// Return value:
//    Torque value that corresponds to GeneratorRpm.
//
S32 GetRpmFromPowerActTbl(/*In*/ U32 PowerLimit)
{
    U8  SegL;
    U8  SegR;
    S32 Diff;
    S32 PowerL;
    S32 PowerR;
    S32 GenRpmL;
    S32 GenRpmR;
    S32 Power;
    S32 GenRpmI;


    // Look the specified Power limit value up in the currently active
    // torque table.
    FindSegmentActTblPower(PowerLimit, &SegL, &SegR);

    // Calculate corresponding power
    PowerL = POWER(*ActTblRpm[SegL], *ActTblTorque[SegL]);
    PowerR = POWER(*ActTblRpm[SegR], *ActTblTorque[SegR]);

    Diff = PowerR - PowerL;
    if( !Diff )
    {
        // Prevent a division by zero on special cases (when SegL and SegR are both
        // set to the same index value - pointing either to the front or the rear of
        // the table).
        // Also applicable when user enters two identical values for two rpm entries
        // in the table.
        return *ActTblRpm[SegL];
    }

    // Align floating points (at SCALE 1000), for fixed point integer math, and interpolate between
    // the segment boundary values.

    Power   = PowerLimit;
    GenRpmL = *ActTblRpm[SegL];
    GenRpmR = *ActTblRpm[SegR];


    GenRpmI = ((U64)Power - PowerL) * (GenRpmR - GenRpmL) / Diff + GenRpmL;

    // Return (SCALE 1000) rpm value.
    return GenRpmI;
}


// Abstract: Return a torque pointer into the active torque table, at the specified
//           index location.
//
// Notice:
//    The function returns NULL on out-of-bound index specifications.
//    There is currently no way, for the caller, of knowing if the
//    underlying table has changed between subsequent calls to this
