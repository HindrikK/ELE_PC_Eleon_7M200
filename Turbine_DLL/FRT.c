static U32 RampedPRatedPower(U32 SetPointBeforeRamping)
{
   U32 ReturnValue = 0;

   if(SetPointBeforeRamping < P_RatedPower)
   {
      ReturnValue = SetPointBeforeRamping + ((P_RatedPower * P_FRTPwrRedPowerRamp)/50000);  // scan20
   }
   else
   {
      ReturnValue = P_RatedPower;
   }

   return ReturnValue;
}


