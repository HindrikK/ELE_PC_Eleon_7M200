static struct StandardTimerVarStruct FRTPowerReductionTimer =
{
   0,                               // Timer value [ms]
   &P_FRTPwrRedFreqHighReturnTime,         // TimeMax value [ms]
   TIMER_FRT,                       // Internal variable, always set to 0
   TIMER_STOP,                      // TIMER_DOWN / TIMER_STOP / TIMER_UP
   0                                // Standard timer called flag.
};

static U8 StatusSetFunc2806(void)
{
   static S32 CalculationPower = 0;
   static S32 SubcalculationHigh = 0;
   static S32 ActivePowerHighSetPoint = 0;
   static U32 MinFreqHigh = 0;
   static U8 SavePowerValue = 0;
   static U32 OldAI_GridFrequency = 0;
   static U8 FreqUp = 0;
   static U8 Timer_init = 0;

   if(P_FRTPwrRedFreqHighType != 0 )
   {
      if(V_GridFrequency > (P_RatedGridFrequency + P_GridFreqHighLevel1)) // Reset the status code if status code 120 is active.
      {
         V_FRTActivePowerSetPoint = RampedPRatedPower(V_FRTActivePowerSetPoint); //Return the active set point to the rated value.
         OldAI_GridFrequency = P_RatedGridFrequency; // Reset the old frequency to the rated frequency.
         SavePowerValue = 1; // Allow the status code to calculate a new value.
         FreqUp = 0;
         return 0; //Reset the status code.
      }

		if(!FreqUp && (V_GridRealPower1Sec < (P_FRTPwrRedFreqHighMinP * P_RatedPower)/100))
      {
         if(V_FRTActivePowerSetPoint != P_RatedPower)
         {
            V_FRTActivePowerSetPoint = RampedPRatedPower(V_FRTActivePowerSetPoint);
            return 1;
         }
			return 0;
      }

		MinFreqHigh = P_RatedGridFrequency + P_FRTPwrRedFreqHigh;

		if(SavePowerValue == 1) // Save the Power for calculation.
		{
			CalculationPower = V_GridRealPower1Sec / 1000;
			SavePowerValue = 0;
		}

		if(P_FRTPwrRedFreqHighType == 2 && FreqUp == 1 && V_GridFrequency <= OldAI_GridFrequency )
		{
			if(V_GridFrequency <= (P_RatedGridFrequency + P_FRTPwrRedFreqHighReturn))
			{
				FreqUp = 0;
            V_FRTActivePowerSetPoint = RampedPRatedPower(V_FRTActivePowerSetPoint);
				SavePowerValue = 1;
            if(V_FRTActivePowerSetPoint == P_RatedPower)
            {
               return 0;
            }
			}
			return 1;
		}
      else if(P_FRTPwrRedFreqHighType == 3 && FreqUp == 1 && V_GridFrequency <= OldAI_GridFrequency )
      {
         if(Timer_init == 0)
         {
            //Timer initialization condition.
            ResetTimer(&FRTPowerReductionTimer);  // Reset the timer value to 0 [ms]
            StartTimer(&FRTPowerReductionTimer);  // Start the timer.
            V_CurrentTime2806 = 0;
            Timer_init = 1;
         }

         if(V_GridFrequency <= (P_RatedGridFrequency + P_FRTPwrRedFreqHighReturn) &&
            V_GridFrequency >= (P_RatedGridFrequency - P_FRTPwrRedFreqHighReturnLow))
         {

            StandardTimer(&FRTPowerReductionTimer);
            V_CurrentTime2806 = GetTimer(&FRTPowerReductionTimer); // Read the timer value.

            if(V_CurrentTime2806 >= P_FRTPwrRedFreqHighReturnTime)
            {
               FreqUp = 0;
               V_FRTActivePowerSetPoint = RampedPRatedPower(V_FRTActivePowerSetPoint);

               if(V_FRTActivePowerSetPoint == P_RatedPower)
               {
                  SavePowerValue = 1;
                  Timer_init = 0;
                  return 0;
               }
            }
         }
         return 1;
      }
		else if(V_GridFrequency >= MinFreqHigh && P_FRTPwrRedHigh_pct != 0)
		{
			// IMPORTANT: Notice that the change based on the frequency is subtracted from the current Power Set point

         if( P_FRTPwrRedFreqHighType == 3 &&
             V_GridFrequency > OldAI_GridFrequency &&
             V_GridFrequency >= P_RatedGridFrequency)
         {
            FreqUp = 1;
            Timer_init = 0;
            SubcalculationHigh = (S32)(100000 - (P_FRTPwrRedHigh_pct * (V_GridFrequency - MinFreqHigh)/1000));
            if(SubcalculationHigh <= 0)
               ActivePowerHighSetPoint = 0;
            else
               ActivePowerHighSetPoint = (S32)((SubcalculationHigh * CalculationPower) / 100);
            V_FRTActivePowerSetPoint = ActivePowerHighSetPoint;
         }
         if( P_FRTPwrRedFreqHighType == 2 &&
             V_GridFrequency > OldAI_GridFrequency )
			{
				FreqUp = 1;
            Timer_init = 0;
				SubcalculationHigh = (S32)(100000 - (P_FRTPwrRedHigh_pct * (V_GridFrequency - MinFreqHigh)/1000));
				if(SubcalculationHigh <= 0)
					ActivePowerHighSetPoint = 0;
				else
					ActivePowerHighSetPoint = (S32)((SubcalculationHigh * CalculationPower) / 100);
				V_FRTActivePowerSetPoint = ActivePowerHighSetPoint;
			}
			if(P_FRTPwrRedFreqHighType == 1)
			{
				FreqUp = 1;
				SubcalculationHigh = (S32)(100000 - (P_FRTPwrRedHigh_pct * (V_GridFrequency - MinFreqHigh)/1000));
				if(SubcalculationHigh <= 0)
					ActivePowerHighSetPoint = 0;
				else
					ActivePowerHighSetPoint = (S32)((SubcalculationHigh * CalculationPower) / 100);
				V_FRTActivePowerSetPoint = ActivePowerHighSetPoint;
			}

			OldAI_GridFrequency = V_GridFrequency;
			return 1;
		}

		if(V_GridFrequency < MinFreqHigh && V_GridFrequency > (P_RatedGridFrequency - P_FRTPwrRedFreqLow)) // Normal Operation area.
		{
         V_FRTActivePowerSetPoint = RampedPRatedPower(V_FRTActivePowerSetPoint); //Ramp the active set point to the rated value.
			OldAI_GridFrequency = P_RatedGridFrequency; // Reset the old frequency to the rated frequency.
			SavePowerValue = 1;
			FreqUp = 0;
		}
	}
   if(V_FRTActivePowerSetPoint == P_RatedPower)
   {
      return 0;
   }
   else
      return 1;
}

