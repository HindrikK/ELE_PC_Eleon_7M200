// Function for the calculation of the lower limit of the power reduction at low frequencies.
static void CalcLowFreqPowRedValue(void)
{
   static S32 CalculationLowPower = 0;
   static S32 SubcalculationLow = 0;
   static S32 ActivePowerLowSetPoint = 0;
   static U8 SavePowerValue = 0;

   if(P_FRTAllowSetPointLimitCalc == 1)
   {
      if(SavePowerValue == 1) // Save the Power for calculation.
      {
         CalculationLowPower = V_GridRealPower1Sec / 1000;
         SavePowerValue = 0;
      }

      if(V_GridFrequency >= V_FRT_MinFreqLow && V_GridFrequency <= V_FRT_MaxFreqLow)
      {
         // Calculate the Active Power set Point limt.
         // To avoid the numerical problems the calculation has been split in two.
         SubcalculationLow = (S32)((P_FRTPwrRedLow + V_FRT_LowFreqGradient * (V_GridFrequency - V_FRT_MinFreqLow)));
         ActivePowerLowSetPoint = (S32)((SubcalculationLow * CalculationLowPower) / 100);
         V_FRTActivePowerLowLim = ActivePowerLowSetPoint;
         V_FRTLowFreqSetPointLimAct = 1;
      }

      if(V_GridFrequency > V_FRT_MaxFreqLow && V_GridFrequency < (P_RatedGridFrequency + P_FRTPwrRedFreqHigh)) // Turbine inside normal operation.
      {
         SavePowerValue = 1;
         V_FRTActivePowerLowLim = 0;
         V_FRTLowFreqSetPointLimAct = 0;
      }

      if (StatusCodeActive(121) != 0) // Reset the limit to 0 if the Frequency Low is active.
      {
         SavePowerValue = 0;
         V_FRTActivePowerLowLim = 0;
         V_FRTLowFreqSetPointLimAct = 0;
      }
   }
   if(P_FRTAllowSetPointLimitCalc == 0)
   {
      V_FRTActivePowerLowLim = 0;
      V_FRTLowFreqSetPointLimAct = 0;
   }
}



static U8 FRT_LowFreqCalculation(void)
{
   static U8 Timer_init = 0;
   static U32 SetPoint = 0;
   static U32 GridFrequency = 0;


   if(Timer_init == 0)
   {
      //Timer initialization condition.
      ResetTimer(&Sts2807CalculationTimer);  // Reset the timer value to 0 [ms]
      StartTimer(&Sts2807CalculationTimer);  // Start the timer.
      SetPoint = V_FRTFreqTablePoint0;    // Change the set point back to the frequency @ 0 [ms]
      Timer_init = 1;
   }

   GridFrequency = V_GridFrequency * 10000; // Scaling the Grid frequency for the internal calculation.

   if(GridFrequency <= V_FRTFreqTablePoint0)
   {
      static U32 Current_time = 0;
      StandardTimer(&Sts2807CalculationTimer);
      Current_time = GetTimer(&Sts2807CalculationTimer); // Read the timer value.
      V_CurrentTime2807 = Current_time;
      if(Current_time > 0 && Current_time <= P_FRTLowFreqP1x)
      {
         if(V_FRTFreqTablePoint0 < V_FRTFreqTablePoint1)
         {
            SetPoint = V_FRTFreqTablePoint0 + V_FRTFreqTableGradient1 * Current_time; // The sign is different because of the changed sign in the gradient calculateion

         }

         if(V_FRTFreqTableGradient1 == 0)
         {
            SetPoint = V_FRTFreqTablePoint1;
         }

         if(V_FRTFreqTablePoint0 > V_FRTFreqTablePoint1)
         {
            SetPoint = V_FRTFreqTablePoint0 - V_FRTFreqTableGradient1 * Current_time; // The sign is different because of the changed sign in the gradient calculateion

         }
      }
      if(Current_time > P_FRTLowFreqP1x && Current_time <= P_FRTLowFreqP2x)
      {
         if(V_FRTFreqTablePoint1 < V_FRTFreqTablePoint2)
         {
            SetPoint = V_FRTFreqTablePoint1 + V_FRTFreqTableGradient2 * (Current_time - P_FRTLowFreqP1x);

         }

         if(V_FRTFreqTableGradient2 == 0)
         {
            SetPoint = V_FRTFreqTablePoint2;

         }
         if(V_FRTFreqTablePoint1 > V_FRTFreqTablePoint2)
         {
            SetPoint = V_FRTFreqTablePoint1 - V_FRTFreqTableGradient2 * (Current_time - P_FRTLowFreqP1x);

         }
      }
      if(Current_time > P_FRTLowFreqP2x && Current_time <= P_FRTLowFreqP3x)
      {
         if(V_FRTFreqTablePoint2 > V_FRTFreqTablePoint3)
         {
            SetPoint = V_FRTFreqTablePoint2 - V_FRTFreqTableGradient3 * (Current_time - P_FRTLowFreqP2x);

         }
         if(V_FRTFreqTableGradient3 == 0)
         {
            SetPoint = V_FRTFreqTablePoint3;

         }
         if(V_FRTFreqTablePoint3 > V_FRTFreqTablePoint2)
         {
            SetPoint = V_FRTFreqTablePoint2 + V_FRTFreqTableGradient3 * (Current_time - P_FRTLowFreqP2x);

         }
      }
      if(Current_time > P_FRTLowFreqP3x && Current_time < P_FRTLowFreqP4x)
      {
         if(V_FRTFreqTablePoint3 > V_FRTFreqTablePoint4)
         {
            SetPoint = V_FRTFreqTablePoint3 - V_FRTFreqTableGradient4 * (Current_time - P_FRTLowFreqP3x);

         }
         if(V_FRTFreqTableGradient4 == 0)
         {
            SetPoint = V_FRTFreqTablePoint4;

         }
         if(V_FRTFreqTablePoint4 > V_FRTFreqTablePoint3)
         {
            SetPoint = V_FRTFreqTablePoint3 + V_FRTFreqTableGradient4 * (Current_time - P_FRTLowFreqP3x);

         }
      }
      V_Setpoint2807 = SetPoint;
      // Compare the measured frequency with the set point
      if(GridFrequency >= SetPoint)
      {
         // Do not allow power reduction
         return 1;
      }

      if(GridFrequency < SetPoint)
      {
         // Allow reduction
         return 0;
      }
   }

   else // Reset the timer and return to the first setpoint. Point0
   {
      Timer_init = 0;         // Reset timer.
      SetPoint = V_FRTFreqTablePoint0;    // Reset calculations for next run.
      return 0;         // Reduction is allowed.
   }
}

