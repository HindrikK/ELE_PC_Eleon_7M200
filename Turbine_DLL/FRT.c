static U8 LVRTCurveCalculation(void)
{
   static U8 Timer_init = 0;
   static U32 Setpoint = 0;
   static U32 timediff = 0;


   if(!P_FRT_LVRT_Enable)
   {
      return 0;
   }

   if(Timer_init == 0)
   {
      //Timer initialization condition.
      ResetTimer(&Sts2814CalculationTimer);  // Reset the timer value to 0 [ms]
      StartTimer(&Sts2814CalculationTimer);  // Start the timer.
      V_CurrentTime2814 = 0;
      V_Setpoint2814 = Setpoint = V_LVRTVoltPoint0;     // Change the set point back to the voltage @ 0 [ms]
      Timer_init = 1;
   }

   if(OS_StatusCodeActive(2815))
   {
      return 0;
   }


   // Perform the voltage check.
   if(GridL1VoltageMul1000_local < V_LVRTVoltPoint7 || GridL2VoltageMul1000_local < V_LVRTVoltPoint7 || GridL3VoltageMul1000_local < V_LVRTVoltPoint7)
   {
      static U32 Current_time = 0;
      // Call the timer to have it update the value
      StandardTimer(&Sts2814CalculationTimer);
      Current_time = GetTimer(&Sts2814CalculationTimer); // Read the timer value.
      V_CurrentTime2814 = Current_time;
      if(Current_time > 0 && Current_time <= P_FRTLVRTP1x)
      {
         if(V_LVRTGradient1 == 0)
         {
            Setpoint = V_LVRTVoltPoint1;
         }
         else
         {
            Setpoint = V_LVRTVoltPoint0 + V_LVRTGradient1 * Current_time;
         }

      }

      if(Current_time > P_FRTLVRTP1x && Current_time <= P_FRTLVRTP2x)
      {

         if(V_LVRTGradient2 == 0)
         {
            Setpoint = V_LVRTVoltPoint2;
         }
         else
         {
            timediff = Current_time - P_FRTLVRTP1x;
            Setpoint = V_LVRTVoltPoint1 + V_LVRTGradient2 * timediff;
         }

      }

      if(Current_time > P_FRTLVRTP2x && Current_time <= P_FRTLVRTP3x)
      {

         if(V_LVRTGradient3 == 0)
         {
            Setpoint = V_LVRTVoltPoint3;
         }
         else
         {
            timediff = Current_time - P_FRTLVRTP2x;
            Setpoint = V_LVRTVoltPoint2 + V_LVRTGradient3 * timediff;
         }

      }

      if(Current_time > P_FRTLVRTP3x && Current_time <= P_FRTLVRTP4x)
      {
         if(V_LVRTGradient4 == 0)
         {
            Setpoint = V_LVRTVoltPoint4;
         }
         else
         {
            timediff = Current_time - P_FRTLVRTP3x;
            Setpoint =  V_LVRTVoltPoint3 + V_LVRTGradient4 * timediff;
         }

      }

      if(Current_time > P_FRTLVRTP4x && Current_time <= P_FRTLVRTP5x)
      {
         if(V_LVRTGradient5 == 0)
         {
            Setpoint = V_LVRTVoltPoint5;
         }
         else
         {
            timediff = Current_time - P_FRTLVRTP4x;
            Setpoint = V_LVRTVoltPoint4 + V_LVRTGradient5 * timediff;
         }
      }

      if(Current_time > P_FRTLVRTP5x && Current_time <= P_FRTLVRTP6x)
      {
         if(V_LVRTGradient6 == 0)
         {
            Setpoint = V_LVRTVoltPoint6;
         }
         else
         {
            timediff = Current_time - P_FRTLVRTP5x;
            Setpoint = V_LVRTVoltPoint5 + V_LVRTGradient6 * timediff;
         }
      }

      if(Current_time > P_FRTLVRTP6x && Current_time <= P_FRTLVRTP7x)
      {
         if(V_LVRTGradient7 == 0)
         {
            Setpoint = V_LVRTVoltPoint7;
         }
         else
         {
            timediff = Current_time - P_FRTLVRTP6x;
            Setpoint = V_LVRTVoltPoint6 + V_LVRTGradient7 * timediff;
         }
      }

      V_Setpoint2814 = Setpoint;
      //Compare the set point and the voltage
      if(GridL1VoltageMul1000_local < Setpoint || GridL2VoltageMul1000_local < Setpoint || GridL3VoltageMul1000_local < Setpoint)
      {
         OS_ActivateStatusCode(2815); //Activate 2815 - LVRT ended
         Timer_init = 0; //Return to the initialization condition.
      }

      return 1;
   }
   else
   {
      Timer_init = 0;
   }
   return 0;
}


static STATUS LocalCtrl(void)
{
   STATUS LocalStatus = 0;

   LocalLVRTActiveFlag = LVRTCurveCalculation(); // Calculate if the voltage is below the LVRT curve.

   V_FRT_LVRT_Active = (V_InverterLVRT_Active || LocalLVRTActiveFlag); // Update the variable in the Variable Database.

   V_FRTFrequencyDropActive = P_FRTLowFreqCalcOn ? FRT_LowFreqCalculation() : 0;  // Calculate if Low Frequency Curve has been crossed.

   CalcLowFreqPowRedValue();      // Calculation of the low frequency power limit.

   if(PreCalcParametersChanged)
   {
      UpdatePrecalcParamsOnChange();
   }


   if(LVRTParametersChanged)
   {
      UpdateLVRTCalculations();
   }

   if(FreqTableParametersChanged)
   {
      UpdateFrequencyTableOnChange();
   }

   UpdateModeVars();

   FrequencyUnstableHigh();
   FrequencyUnstableLow();
   VoltageUnstableHigh();
   VoltageUnstableLow();

   LocalStatus = OS_SUCCESS;
   return LocalStatus;
}


