void UpdateLVRTCalculations(void)
{
   static U32 Timediff1 = 0;
   static U32 Timediff2 = 0;
   static U32 Timediff3 = 0;
   static U32 Timediff4 = 0;
   static U32 Timediff5 = 0;
   static U32 Timediff6 = 0;
   static S32 TempTimeDiff = 0;

   V_LVRTVoltPoint0 = ((U32)P_FRTLVRTOffset * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.
   V_LVRTVoltPoint1 = ((U32)P_FRTLVRTP1y * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.
   V_LVRTVoltPoint2 = ((U32)(P_FRTLVRTP2y) * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.
   V_LVRTVoltPoint3 = ((U32)P_FRTLVRTP3y * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.
   V_LVRTVoltPoint4 = ((U32)P_FRTLVRTP4y * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.
   V_LVRTVoltPoint5 = ((U32)P_FRTLVRTP5y * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.
   V_LVRTVoltPoint6 = ((U32)P_FRTLVRTP6y * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.
   V_LVRTVoltPoint7 = ((U32)P_FRTLVRTP7y * V_RatedLine2NeutralVoltage); // Calculate the voltage in mV.


   TempTimeDiff = P_FRTLVRTP2x - P_FRTLVRTP1x;
   if(TempTimeDiff < TIMER_FRT)
   {
       Timediff1 = TIMER_FRT;
   }
   else
   {
       Timediff1 = (U32)TempTimeDiff;
   }

   TempTimeDiff = P_FRTLVRTP3x - P_FRTLVRTP2x;
   if(TempTimeDiff < TIMER_FRT)
   {
       Timediff2 = TIMER_FRT;
   }
   else
   {
       Timediff2 = (U32)TempTimeDiff;
   }

   TempTimeDiff = P_FRTLVRTP4x - P_FRTLVRTP3x;
   if(TempTimeDiff < TIMER_FRT)
   {
       Timediff3 = TIMER_FRT;
   }
   else
   {
       Timediff3 = (U32)TempTimeDiff;
   }

   TempTimeDiff = P_FRTLVRTP5x - P_FRTLVRTP4x;
   if(TempTimeDiff < TIMER_FRT)
   {
       Timediff4 = TIMER_FRT;
   }
   else
   {
       Timediff4 = (U32)TempTimeDiff;
   }

   TempTimeDiff = P_FRTLVRTP6x - P_FRTLVRTP5x;
   if(TempTimeDiff < TIMER_FRT)
   {
       Timediff5 = TIMER_FRT;
   }
   else
   {
       Timediff5 = (U32)TempTimeDiff;
   }

   TempTimeDiff = P_FRTLVRTP7x - P_FRTLVRTP6x;
   if(TempTimeDiff < TIMER_FRT)
   {
       Timediff6 = TIMER_FRT;
   }
   else
   {
       Timediff6 = (U32)TempTimeDiff;
   }


   V_LVRTGradient1 = (V_LVRTVoltPoint1 - V_LVRTVoltPoint0)/(P_FRTLVRTP1x); // The line is always calculated from 0 [ms]

   // The V_LVRTGradient for the line from Point 1 to Point 2
   V_LVRTGradient2 = (V_LVRTVoltPoint2 - V_LVRTVoltPoint1)/(Timediff1);

   // The gradient for the line from Point 2 to Point 3
   V_LVRTGradient3 = (V_LVRTVoltPoint3 - V_LVRTVoltPoint2)/(Timediff2);

   // The gradient for the line from Point 3 to Point 4
   V_LVRTGradient4 = (V_LVRTVoltPoint4 - V_LVRTVoltPoint3)/(Timediff3);

   // The gradient for the line from Point 4 to Point 5
   V_LVRTGradient5 = (V_LVRTVoltPoint5 - V_LVRTVoltPoint4)/(Timediff4);

   // The gradient for the line from Point 5 to Point 6
   V_LVRTGradient6 = (V_LVRTVoltPoint6 - V_LVRTVoltPoint5)/(Timediff5);

   // The gradient for the line from Point 6 to Point 7
   V_LVRTGradient7 = (V_LVRTVoltPoint7 - V_LVRTVoltPoint6)/(Timediff6);


   LVRTParametersChanged = 0;

   return;
}


void UpdateFrequencyTableOnChange(void)
{
   static U32 TimeDifference1 = 0;
   static U32 TimeDifference2 = 0;
   static U32 TimeDifference3 = 0;
   static U32 TimeDifference4 = 0;

   TimeDifference1 = P_FRTLowFreqP1x;

   TimeDifference2 = P_FRTLowFreqP2x - P_FRTLowFreqP1x;
   if(TimeDifference2 < TIMER_FRT)
   {
      TimeDifference2 = TIMER_FRT;
   }

   TimeDifference3 = P_FRTLowFreqP3x - P_FRTLowFreqP2x;
   if(TimeDifference3 < TIMER_FRT)
   {
      TimeDifference3 = TIMER_FRT;
   }

   TimeDifference4 = P_FRTLowFreqP4x - P_FRTLowFreqP3x;
   if(TimeDifference4 < TIMER_FRT)
   {
      TimeDifference4 = TIMER_FRT;
   }

   V_FRTFreqTablePoint0 = (P_RatedGridFrequency - P_FRTLowFreqOffset) * 10000;
   V_FRTFreqTablePoint1 = (P_RatedGridFrequency - P_FRTLowFreqP1y) * 10000;
   V_FRTFreqTablePoint2 = (P_RatedGridFrequency - P_FRTLowFreqP2y) * 10000;
   V_FRTFreqTablePoint3 = (P_RatedGridFrequency - P_FRTLowFreqP3y) * 10000;
   V_FRTFreqTablePoint4 = (P_RatedGridFrequency - P_FRTLowFreqP4y) * 10000;

   // Calculate the gradient from 0 to P_FRTLowFreqP1x

   if(V_FRTFreqTablePoint0 > V_FRTFreqTablePoint1)
   {
      //!IMPORTANT! Note that the sign is changed. This is handled later when the set point is calculated using gradient1.
      V_FRTFreqTableGradient1 = (V_FRTFreqTablePoint0 - V_FRTFreqTablePoint1) / (TimeDifference1);
   }
   else
   {
      V_FRTFreqTableGradient1 = (V_FRTFreqTablePoint1 - V_FRTFreqTablePoint0) / (TimeDifference1);
   }

   if(V_FRTFreqTablePoint1 > V_FRTFreqTablePoint2)
   {
      //!IMPORTANT! Note that the sign is changed. This is handled later when the set point is calculated using gradient1.
      V_FRTFreqTableGradient2 = (V_FRTFreqTablePoint1 - V_FRTFreqTablePoint2) / (TimeDifference2);
   }
   else
   {
      V_FRTFreqTableGradient2 = (V_FRTFreqTablePoint2 - V_FRTFreqTablePoint1) / (TimeDifference2);
   }

   if(V_FRTFreqTablePoint2 > V_FRTFreqTablePoint3)
   {
      V_FRTFreqTableGradient3 = (V_FRTFreqTablePoint2 - V_FRTFreqTablePoint3) / (TimeDifference3);
   }
   else
   {
      V_FRTFreqTableGradient3 = (V_FRTFreqTablePoint3 - V_FRTFreqTablePoint2) / (TimeDifference3);
   }
   if(V_FRTFreqTablePoint3 > V_FRTFreqTablePoint4)
   {
      V_FRTFreqTableGradient4 = (V_FRTFreqTablePoint3 - V_FRTFreqTablePoint4) / (TimeDifference4);
   }
   else
   {
      V_FRTFreqTableGradient4 = (V_FRTFreqTablePoint4 - V_FRTFreqTablePoint3) / (TimeDifference4);
   }

   FreqTableParametersChanged = 0;
   return;
}


