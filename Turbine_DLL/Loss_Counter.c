// Read a string from file. The string must be between start and end characters.
// Function starts to search for the start character from the position that File Descriptor is currently pointing
// Function keeps looking till the end of the file for the start character.
//
//  file - File Descriptor
//  string - pointer for returned string(NULL terminated)
//  max - max string length(without NULL byte)
//  startChar - character that specifies start of the string
//  endChar - character that specifies end of the string
//
//  Returned value shows the number of characters in string(not counting NULL byte)
//  Negative return value indicates error
//    -1 - start character not found
//    -2 - string reached max length but end character not found
//
// Example:
//   File contains text - "This is <file> body"
//   Function parameters StartChar='<'; EndChar='>'
//   Returned string: "file"
static int File_ReadBetween(S32 file, char* string, S32 max, char startChar, char endChar)
{
   char character;

   while (true)// look for start character
   {
      if (OS_Read(file, &character, 1) == 1)
      {
         if (character == startChar)// found start character
         {
            break;
         }
      }
      else
      {
         // ERROR
         return -1;
      }
   }

   int i = 0;
   while (true)// read string and look for end character
   {
      if (i < max)
      {
         if (OS_Read(file, &character, 1) == 1)
         {
            if (character == endChar)// found end character
            {
               string[i] = 0;// string end termination
               return i;
            }
            else
            {
               string[i] = character;
            }
         }
      }
      else
      {
         string[i] = 0;// string end termination
         // ERROR
         return -2;
      }

      i++;// keep reading string and look for end character
   }
}

static void ClosePowerCurveFile()
{
   OS_Close(FileDescriptor_PowerCurveFile);
   OS_Release_File_User();
}

static S32 LoadPowerCurveFile()
{
   int result;
   char FileDirectory[] = "I:/App/Data/PowerCurve/";
   char FilePath[60];
   char FileName[30];

   // load file name
   time_t TimeStamp;
   result = OS_VarDB_ReadVariableByName("P_LC_PowerCurve_FileName", &FileName, sizeof(FileName), &TimeStamp);
   if (result < 0)
   {
      // ERROR
      sprintf(PowerCurveInfo_Txt, "No file name variable(%d)", result);
      InfoMessage(PowerCurveInfo_Txt, 3);
      return -1;
   }
   snprintf(FilePath, sizeof(FilePath) - 1, "%s%s", FileDirectory, FileName);

   // open file
   OS_Become_File_User();
   FileDescriptor_PowerCurveFile = OS_Open(&FilePath, PO_RDONLY, 0);
   if (FileDescriptor_PowerCurveFile <= 0)
   {
      // ERROR
      sprintf(PowerCurveInfo_Txt, "Error opening file(%d)", FileDescriptor_PowerCurveFile);
      InfoMessage(PowerCurveInfo_Txt, 3);
      OS_Release_File_User();
      return -2;
   }

   // read table version
   char string[15];
   result = File_ReadBetween(FileDescriptor_PowerCurveFile, &string, sizeof(string) - 1, '<', '>');
   if (result < 1)
   {
      // ERROR
      snprintf(PowerCurveInfo_Txt, sizeof(PowerCurveInfo_Txt) - 1, "Missing table version(%d)", result);
      InfoMessage(PowerCurveInfo_Txt, 3);
      ClosePowerCurveFile();
      return -3;
   }

   // check if correct version
   if (strcmp(string, "Ver.1.0"))
   {
      // ERROR
      snprintf(PowerCurveInfo_Txt, sizeof(PowerCurveInfo_Txt) - 1, "Wrong table: %s", string);
      InfoMessage(PowerCurveInfo_Txt, 3);
      ClosePowerCurveFile();
      return -4;
   }
   else
   {
      sprintf(PowerCurveInfo_Txt, "Table loaded");
      InfoMessage(PowerCurveInfo_Txt, 3);
   }

   //OS_com_printf("\nPower table values\n");//DEBUG
   
   // read data from table
   int i;
   for (i = 0; i < LC_NUM_OF_CURVE_POINTS; i++)
   {
      //OS_com_printf("rpm[%d]: ", i);//DEBUG
      // read ref value
      result = File_ReadBetween(FileDescriptor_PowerCurveFile, &string, sizeof(string) - 1, '<', '>');// read value
      if (result < 1)
      {
         // ERROR
         snprintf(PowerCurveInfo_Txt, sizeof(PowerCurveInfo_Txt) - 1, "Invalid data format(%d)", result);
         InfoMessage(PowerCurveInfo_Txt, 3);
         ClosePowerCurveFile();
         return -5;
      }

      S32 value;
      result = Convert_StringToInt(&string, &value, NULL);
      if (result < 0)
      {
         // ERROR
         snprintf(PowerCurveInfo_Txt, sizeof(PowerCurveInfo_Txt) - 1, "Invalid data format(%d)", result);
         InfoMessage(PowerCurveInfo_Txt, 3);
         ClosePowerCurveFile();
         return -6;
      }
      LC_PowerCurveTable[i] = (S16)value;
      //OS_com_printf("%d\n", LC_PowerCurveTable[i]);//DEBUG
   }

   // read table end definition
   result = File_ReadBetween(FileDescriptor_PowerCurveFile, &string, sizeof(string) - 1, '<', '>');
   if (result < 1)
   {
      // ERROR
      snprintf(PowerCurveInfo_Txt, sizeof(PowerCurveInfo_Txt) - 1, "Missing table end(%d)", result);
      InfoMessage(PowerCurveInfo_Txt, 3);
      ClosePowerCurveFile();
      return -11;
   }

   // check if correct end definition
   if (strcmp(string, "END_OF_TABLE"))
   {
      // ERROR
      snprintf(PowerCurveInfo_Txt, sizeof(PowerCurveInfo_Txt) - 1, "Invalid end(%s)", string);
      InfoMessage(PowerCurveInfo_Txt, 3);
      ClosePowerCurveFile();
      return -12;
   }

   ClosePowerCurveFile();
   
   return 0;
}


static S32 GetPowerFromPowerCurve(U16 wind)
{
   U16 indexPwr;

   // calculate indexes
   if (wind <= 0)
   {
      return 0;
   }
   else
   {
      indexPwr = wind / LC_WIND_INDEX_DIV;
      if (indexPwr >= LC_NUM_OF_CURVE_POINTS)
      {
         return LC_PowerCurveTable[LC_NUM_OF_CURVE_POINTS - 1];
      }
      else
      {
         // interpolate power value
         F32 value = ((F32)LC_PowerCurveTable[indexPwr + 1] - (F32)LC_PowerCurveTable[indexPwr]) / LC_WIND_INDEX_DIV;
         value = value * (wind - indexPwr * LC_WIND_INDEX_DIV);
         return LC_PowerCurveTable[indexPwr] + (S32)value;
      }
   }
}


