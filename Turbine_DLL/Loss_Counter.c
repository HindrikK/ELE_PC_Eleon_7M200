static STATUS LocalCtrl(U32 Interval)
{
    STATUS LocalStatus = 0;

#if defined(LC_DEBUG) || defined(INCLUDE_DEBUG)
    //V_GridRealPower3Sec = V_LC_GridPower3Sec_Debug;
#endif

    // when Status changed
    if (Status != Status_Prev)
    {
        Status_Prev = Status;
        UpdateStatusTxt();
    }
    
    V_LC_WindPowerCapability = GetPowerFromPowerCurve(V_WindSpeed3Sec / 10);
    // calculate the power difference(loss)
    V_LC_PowerLoss = V_LC_WindPowerCapability - (V_GridRealPower3Sec / 1000);
    // calc lost production
    F32 lostProduction = (F32)V_LC_PowerLoss * (1 / (3600 * ((F32)1000 / LC_SCAN_TIME)));
    if(lostProduction < 0) lostProduction = 0;
    
    
    if(V_ActiveControlProgram1 != 0)    // stopped states
    {
       if(OS_StatusCodeActive(13))  // manual stop
       {
          Status = LC_STOPLOSS;
          LC_Stop_Loss+= lostProduction;
          B_LC_Stop_Loss = (U32)LC_Stop_Loss;
          OS_VarDB_UpdateVariable(B_LC_Stop_Loss_ID, &B_LC_Stop_Loss, sizeof(B_LC_Stop_Loss));
       }
       else if(false/*TSO_Stop*/)
       {
          Status = LC_TSOSTOPLOSS;
          LC_TSOStop_Loss+= lostProduction;
          B_LC_TSOStop_Loss = (U32)LC_TSOStop_Loss;
          OS_VarDB_UpdateVariable(B_LC_TSOStop_Loss_ID, &B_LC_TSOStop_Loss, sizeof(B_LC_TSOStop_Loss));
       }
       else
       {
          Status = LC_FAULTLOSS;
          LC_Fault_Loss+= lostProduction;
          B_LC_Fault_Loss = (U32)LC_Fault_Loss;
          OS_VarDB_UpdateVariable(B_LC_Fault_Loss_ID, &B_LC_Fault_Loss, sizeof(B_LC_Fault_Loss));
       }
    }
    else    // operational states
    {
       // check which setpoint is lowest
       // CODE HERE
       
       if(V_PAT_State == PAT_STATE_LIMITED)
       {
          if( (P_PM_P_Sp_Local_kW / 1000) < P_PAT_GridNominalPower && (!V_PM_ReducedSetPointActive || P_PM_P_Sp_Local_kW < V_PM_ReducedSetPoint_kW) )
          {
             LC_LocalSetpoint_Loss+= lostProduction;
             B_LC_LocalSetpoint_Loss = (U32)LC_LocalSetpoint_Loss;
             OS_VarDB_UpdateVariable(B_LC_LocalSetpoint_Loss_ID, &B_LC_LocalSetpoint_Loss, sizeof(B_LC_LocalSetpoint_Loss));
          }
          else if(V_PM_ReducedSetPointActive)     // automatic power reduction
          {
             if((V_PM_ReducedSetPoint_kW / 1000) < P_PAT_GridNominalPower)
             {
                LC_AutoReduction_Loss+= lostProduction;
                B_LC_AutoReduction_Loss = (U32)LC_AutoReduction_Loss;
                OS_VarDB_UpdateVariable(B_LC_AutoReduction_Loss_ID, &B_LC_AutoReduction_Loss, sizeof(B_LC_AutoReduction_Loss));
             }
          }
       }
       else
       {
          Status = LC_NOLOSS;
       }
    }
    V_LC_Total_Loss = B_LC_Stop_Loss + B_LC_TSOStop_Loss + B_LC_Fault_Loss + B_LC_AutoReduction_Loss + B_LC_LocalSetpoint_Loss + B_LC_TSOSetpoint_Loss;


    // reloading parameters from menu
    if (V_LC_LoadParameters)
    {
        V_LC_LoadParameters = false;
        OS_VarDB_UpdateVariableByName("V_LC_LoadParameters", &V_LC_LoadParameters, sizeof(V_LC_LoadParameters));
        LoadParameters();
        InfoMessage("Parameters reloaded", 1);
    }
    
    // manually reloading power curve file
    if (V_LC_PowerCurve_LoadFile)
    {
       V_LC_PowerCurve_LoadFile = false;
       OS_VarDB_UpdateVariableByName("V_LC_PowerCurve_LoadFile", &V_LC_PowerCurve_LoadFile, sizeof(V_LC_PowerCurve_LoadFile));
       LoadPowerCurveFile();
    }
    
    // reset statistics
    if (V_LC_ResetValues)
    {
        V_LC_ResetValues = false;
        OS_VarDB_UpdateVariableByName("V_LC_ResetValues", &V_LC_ResetValues, sizeof(V_LC_ResetValues));
        ResetStatistics();
    }

    return LocalStatus;
}


