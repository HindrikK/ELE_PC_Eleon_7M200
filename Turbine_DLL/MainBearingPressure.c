static void ValveControl()
{
	switch(P_MBP_ValveService)
	{
		case 0:		// AUTO MODE
			
			if(!OS_StatusCodeActive(5900) && !OS_StatusCodeActive(5901) && !OS_StatusCodeActive(5902) && !OS_StatusCodeActive(5903))
			{
				switch(Step)
				{
					case 0:	//initialize
						V_MBP_ValveOpenTime = 0;
						V_MBP_ValveClosedTime = 0;
						openTime = 0;
						closedTime = 0;
						// decide if open or close valve needed
						if(V_MBP_Pressure_bar <= pressureValveOn_bar)
						{
							processTimer = P_MBP_PressureOpenMinTime;
							DO_MBP_PressureValve = true;
							Step = 10;
						}
						else if(V_MBP_Pressure_bar >= pressureValveOff_bar)
						{
							processTimer = P_MBP_PressureClosedMinTime;
							DO_MBP_PressureValve = false;
							Step = 20;
						}
						break;
					
					case 10:	//valve open min time
						openTime+= MBP_SCANTIME;
						//valve open min time
						processTimer-= MBP_SCANTIME;
						if(processTimer <= 0)
						{
							Step = 11;
						}
						break;

					case 11:	// valve opened till pressure up or max open timeout
						openTime+= MBP_SCANTIME;
						if(V_MBP_Pressure_bar >= pressureValveOff_bar)
						{
							DO_MBP_PressureValve = false;
							processTimer = P_MBP_PressureClosedMinTime;
							V_MBP_ValveOpenTime = openTime;
							closedTime = 0;
							Step = 20;
						}
						
						if(openTime >= P_MBP_ValveOpenMaxTime)
						{
							DO_MBP_PressureValve = false;
							OS_ActivateStatusCode(5903);
							Step = 0;
						}
						break;
						
					case 20:	//valve closed min time
						closedTime+= MBP_SCANTIME;
						//valve open min time
						processTimer-= MBP_SCANTIME;
						if(processTimer <= 0)
						{
							Step = 21;
						}
						break;
					
					case 21:	// valve closed till pressure down
						closedTime+= MBP_SCANTIME;
						if(V_MBP_Pressure_bar <= pressureValveOn_bar)
						{
							DO_MBP_PressureValve = true;
							processTimer = P_MBP_PressureOpenMinTime;
							V_MBP_ValveClosedTime = closedTime;
							openTime = 0;
							Step = 10;
						}
						break;
				}
			}
			else
			{
				DO_MBP_PressureValve = false;
				Step = 0;
			}
			break;
		
		case 1:		// SERVICE OFF
			DO_MBP_PressureValve = false;
			Step = 0;
			break;
		
		case 2:		// SERVICE ON
			DO_MBP_PressureValve = true;
			Step = 0;
			break;
	}
}

static void AirCompressorControl()
{
	switch(P_MBP_AirCompressorService)
	{
		case 0:
			if(!OS_StatusCodeActive(5900) && !OS_StatusCodeActive(5901))
			{
				V_MBP_AirCompressor = true;
			}
			else
			{
				V_MBP_AirCompressor = false;
			}
			break;
			
		case 1:
			V_MBP_AirCompressor = false;
			break;
			
		case 2:
			V_MBP_AirCompressor = true;
			break;
	}
			
	// use analog output to control relay
	if(V_MBP_AirCompressor)
	{
		AO_MBP_AirCompressor = 10000;		// set 10,0 V analog output voltage to activate relay
	}
	else
	{
		AO_MBP_AirCompressor = 0;
	}
}

static void VacuumPumpControl()
{
	static U32 timer = 0;
	
	switch(P_MBP_VacuumPumpService)
	{
		case 0:
			// toggle pump between ON and OFF states by defined periods
			if(!V_MBP_VacuumPump)	// pump OFF
			{
				timer+= MBP_SCANTIME;
				if(timer >= P_MBP_VacPumpOffTime)
				{
					V_MBP_VacuumPump = true;
					timer = 0;
				}
			}
			else	// pump ON
			{
				timer+= MBP_SCANTIME;
				if(timer >= P_MBP_VacPumpOnTime)
				{
					V_MBP_VacuumPump = false;
					timer = 0;
				}
			}
			break;
			
		case 1:
			V_MBP_VacuumPump = false;
			break;
			
		case 2:
			V_MBP_VacuumPump = true;
			break;
	}
	
	// use analog output to control relay
	if(V_MBP_VacuumPump)
	{
		AO_MBP_VacuumPump = 10000;		// set 10,0 V analog output voltage to activate relay
	}
	else
	{
		AO_MBP_VacuumPump = 0;
	}
}

  
static STATUS LocalCtrl( U32 Interval )
{
   STATUS LocalStatus = 0;
   
   V_MBP_Pressure_bar = Linear_Conversion(AI_MBP_Pressure_mA/*Input*/, P_MBP_PressureMin_mA/*InLow*/, P_MBP_PressureMax_mA/*InHigh*/, P_MBP_PressureMin_bar/*OutLow*/, P_MBP_PressureMax_bar/*OutHigh*/);
   
   //calculate valve duty cycle
   S32 time = V_MBP_ValveOpenTime + V_MBP_ValveClosedTime;
   if (time > 0)
   {
      V_MBP_ValveDutyCycle = (U16)(((U32)V_MBP_ValveOpenTime * 1000) / time);
   }
   V_MBP_ValveDutyCycle = (U16)FilterAvg_Control(DutyAverage, V_MBP_ValveDutyCycle);
   V_MBP_ValveDutyCycle = (U16)RateLim_Control(DutyCycleLimiter, V_MBP_ValveDutyCycle/*Input*/);

	//calculate dynamic pressure setpoint based on valve duty cycle
	S16 pressureOffset;
	if(V_MBP_ValveDutyCycle > P_MBP_ValveDuty_2)
	{
		pressureOffset = (S16)Linear_Conversion(P_MBP_ValveDuty_2/*Input*/, P_MBP_ValveDuty_1/*InLow*/, P_MBP_ValveDuty_2/*InHigh*/, P_MBP_DutyPressOffset_1/*OutLow*/, P_MBP_DutyPressOffset_2/*OutHigh*/);
	}
	else if(V_MBP_ValveDutyCycle < P_MBP_ValveDuty_1)
	{
		pressureOffset = (S16)Linear_Conversion(P_MBP_ValveDuty_1/*Input*/, P_MBP_ValveDuty_1/*InLow*/, P_MBP_ValveDuty_2/*InHigh*/, P_MBP_DutyPressOffset_1/*OutLow*/, P_MBP_DutyPressOffset_2/*OutHigh*/);
	}
	else
	{
		pressureOffset = (S16)Linear_Conversion(V_MBP_ValveDutyCycle/*Input*/, P_MBP_ValveDuty_1/*InLow*/, P_MBP_ValveDuty_2/*InHigh*/, P_MBP_DutyPressOffset_1/*OutLow*/, P_MBP_DutyPressOffset_2/*OutHigh*/);
	}
   
   if((P_MBP_PressureValveOff_bar + pressureOffset) >= P_MBP_PressureMaxAlarm)
   {
   		pressureValveOn_bar = P_MBP_PressureValveOn_bar;
   		pressureValveOff_bar = P_MBP_PressureValveOff_bar;
   }
   else
   {
   		pressureValveOn_bar = P_MBP_PressureValveOn_bar + pressureOffset;
   		pressureValveOff_bar = P_MBP_PressureValveOff_bar + pressureOffset;
   }
   
   V_MBP_PressureSetpoint = pressureValveOn_bar;	//show the resulting pressure setpoint in status menu
   
   ValveControl();
   AirCompressorControl();
   VacuumPumpControl();

   return LocalStatus;
}

