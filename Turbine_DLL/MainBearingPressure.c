static STATUS Initialize()
{
	DutyAverage = FilterAvg_New(10/*valueCount*/, 60000/*period*/, MBP_SCANTIME/*sampleTime*/);
	if(DutyAverage == 0) return -1;
	
	// dutyCycle change speed
	DutyCycleLimiter = RateLim_New(20/*RateUp*/, 100/*RateDown*/, 60000/*TimeBase*/, MBP_SCANTIME/*TimeStep*/);
	if(DutyCycleLimiter == 0) return -1;
	
	return 0;
}
  
