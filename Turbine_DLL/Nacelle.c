static const U32 SmokeResetTimeout = 3000;   //  3 seconds
static varid_t V_NacelleSmokeDetectorResetID;

static struct StandardTimerVarStruct SmokeResetTimer =
{
   0,                              // Timer value [ms]
   &SmokeResetTimeout,             // Pointer to the timeout variable [ms]
   NACELLE_TIMER,                  // Timer interval [ms]. The function "StandardTimer" is called with this interval.
   TIMER_STOP,                     // Start TIMER_DOWN / TIMER_STOP / TIMER_UP
   0                               // Is used to check if the "StandardTimer" function is called.
};
//-----------------------------------------------------------------------
// CONTROL                CONTROL                 CONTROL
//-----------------------------------------------------------------------
static void SensorStatus( void )
{
    if( !P_NacelleTempEnable )
    {
        V_NacelleTempSensorState = SENSOR_STATUS_DISABLED;
    }
    else if( (PT_NacelleTemp > P_NacelleTempErrHigh) || (PT_NacelleTemp < P_NacelleTempErrLow) )
    {
        V_NacelleTempSensorState = SENSOR_STATUS_ERROR;
    }
    else
    {
        V_NacelleTempSensorState = SENSOR_STATUS_OK;
    }
}
//___________________________________________________________________
static void SensorStatusTxt( S32 Status, S32 *OldStatus, char *Text, S32 Size )
{
    if( Status != *OldStatus )
    {
        switch( Status )
        {
            case SENSOR_STATUS_OK:
            {
                strncpy( Text, "OK", Size );
            }
                break;
            case SENSOR_STATUS_DISABLED:
            {
                strncpy( Text, "Disabled", Size );
            }
                break;
            case SENSOR_STATUS_ERROR:
            {
                strncpy( Text, "Sensor Error", Size );
            }
                break;
            default:
            {
                strncpy( Text, "WARNING", Size );
            }
                break;
        }
    }

    *OldStatus = Status;
}

static void ResetSmokeDetector()
{
    if (V_NacelleSmokeDetectorReset)
    {
        DO_NacelleSmokeDetectorSupply = 0;
        StartTimer(&SmokeResetTimer);
        if (StandardTimer(&SmokeResetTimer))
        {
            V_NacelleSmokeDetectorReset = 0;
            OS_VarDB_UpdateVariable(V_NacelleSmokeDetectorResetID, &V_NacelleSmokeDetectorReset, sizeof V_NacelleSmokeDetectorReset);
            DO_NacelleSmokeDetectorSupply = 1;
        }
    }
    else
    {
        ResetTimer(&SmokeResetTimer);
        DO_NacelleSmokeDetectorSupply = 1;
    }
}

static STATUS LocalCtrl( void )
{
    static S32 NacelleTempSensorState_Old = 100500;

    // Update value to 5min log and total log
    V_TempNacelleLog = PT_NacelleTemp;

    SensorStatus();
    SensorStatusTxt( V_NacelleTempSensorState, &NacelleTempSensorState_Old, V_NacelleTempSensorStateTxt, sizeof(V_NacelleTempSensorStateTxt) );
    ResetSmokeDetector();

    return OS_SUCCESS;
}

