static STATUS Initialize()
{
    STATUS Status = OS_SUCCESS;
    
    #if defined DLL_SIMU
    Status = OS_VarDB_GetVariableID("DLL_IN_WindDirectionRel", &DLL_IN_WindDirectionRel_ID);
    if (Status != 0)
    {
       return Status;
    }
    #endif

    Status = WindDirectionInit();
    if (Status != OS_SUCCESS) { return Status; }

    WindLeftPI = PI_New_2(PIkpDec, PIkpInc, PIkiDec, PIkiInc, -1800, 1800);
    WindRightPI = PI_New_2(PIkpInc, PIkpDec, PIkiInc, PIkiDec, -1800, 1800);
    DirVariation_RateLim = RateLim_New(1/*RateUp*/, 1/*RateDown*/, 1000/*TimeBase*/, WID_TASK_SCANTIME/*TimeStep*/);

    Status = LoadParameters();
    if (Status != OS_SUCCESS) { return Status; }

    return Status;
}
