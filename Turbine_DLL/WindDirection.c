static STATUS LoadParameters()
{
    STATUS Status = OS_SUCCESS;

    // read parameters from varDB
    Status = OS_VarDB_ReadVariableByName("P_WID_PIkpInc", &PIkpInc, sizeof(F32), NULL);
    if (Status != OS_SUCCESS) { return Status; }
    Status = OS_VarDB_ReadVariableByName("P_WID_PIkpDec", &PIkpDec, sizeof(F32), NULL);
    if (Status != OS_SUCCESS) { return Status; }
    Status = OS_VarDB_ReadVariableByName("P_WID_PIkiInc", &PIkiInc, sizeof(F32), NULL);
    if (Status != OS_SUCCESS) { return Status; }
    Status = OS_VarDB_ReadVariableByName("P_WID_PIkiDec", &PIkiDec, sizeof(F32), NULL);
    if (Status != OS_SUCCESS) { return Status; }
    Status = OS_VarDB_ReadVariableByName("P_WID_DirVariationRateUp", &P_WID_DirVariationRateUp, sizeof(P_WID_DirVariationRateUp), NULL);
    if (Status != OS_SUCCESS) { return Status; }
    Status = OS_VarDB_ReadVariableByName("P_WID_DirVariationRateDown", &P_WID_DirVariationRateDown, sizeof(P_WID_DirVariationRateDown), NULL);
    if (Status != OS_SUCCESS) { return Status; }

    // reload PI regulator parameters
    Status = PI_Set_KpInc_2(WindLeftPI, PIkpDec);
    if (Status != OS_SUCCESS) { return Status; }
    Status = PI_Set_KpDec_2(WindLeftPI, PIkpInc);
    if (Status != OS_SUCCESS) { return Status; }
    Status = PI_Set_KiInc_2(WindLeftPI, PIkiDec);
    if (Status != OS_SUCCESS) { return Status; }
    Status = PI_Set_KiDec_2(WindLeftPI, PIkiInc);
    if (Status != OS_SUCCESS) { return Status; }

    Status = PI_Set_KpInc_2(WindRightPI, PIkpInc);
    if (Status != OS_SUCCESS) { return Status; }
    Status = PI_Set_KpDec_2(WindRightPI, PIkpDec);
    if (Status != OS_SUCCESS) { return Status; }
    Status = PI_Set_KiInc_2(WindRightPI, PIkiInc);
    if (Status != OS_SUCCESS) { return Status; }
    Status = PI_Set_KiDec_2(WindRightPI, PIkiDec);
    if (Status != OS_SUCCESS) { return Status; }

    Status = PI_Reset_2(WindLeftPI);
    if (Status != OS_SUCCESS) { return Status; }
    Status = PI_Reset_2(WindRightPI);
    if (Status != OS_SUCCESS) { return Status; }

    RateLim_Set_RateUp(DirVariation_RateLim, P_WID_DirVariationRateUp);
    RateLim_Set_RateDown(DirVariation_RateLim, P_WID_DirVariationRateDown);


    return OS_SUCCESS;
}


static STATUS LocalCtrl(void)
{
    STATUS LocalStatus = OS_SUCCESS;

#if defined DLL_SIMU
   
    OS_VarDB_ReadVariable(DLL_IN_WindDirectionRel_ID, &DLL_IN_WindDirectionRel, sizeof(DLL_IN_WindDirectionRel), NULL);
    S16 direction_rel = DLL_IN_WindDirectionRel * 10;
    if (direction_rel < 0)
    {
       V_WindDirectionSensor = direction_rel + 3600;
    }
    else
    {
       V_WindDirectionSensor = direction_rel;
    }
   
#else

    // Wind Direction Sensor Selector
    switch (P_WindDirectionSensorSelect)
    {
        case 0: //AUTO
        {
            if (!V_WindDirectionSensor1Status && !V_WindDirectionSensor2Status)
            {
                strcpy(V_WindDirectionSensorsActive, "All");
                // calculate average of the two wind direction sensors values
                U16 diff;
                if (V_WindDirectionSensor2 > V_WindDirectionSensor1)
                {
                    diff = V_WindDirectionSensor2 - V_WindDirectionSensor1;
                    if (diff > 1800) { V_WindDirectionSensor = V_WindDirectionSensor1 - (3600 - diff) / 2; }
                    else { V_WindDirectionSensor = V_WindDirectionSensor1 + diff / 2; }
                }
                else if (V_WindDirectionSensor2 < V_WindDirectionSensor1)
                {
                    diff = V_WindDirectionSensor1 - V_WindDirectionSensor2;
                    if (diff > 1800) { V_WindDirectionSensor = V_WindDirectionSensor1 + (3600 - diff) / 2; }
                    else { V_WindDirectionSensor = V_WindDirectionSensor1 - diff / 2; }
                }
                if (V_WindDirectionSensor >= 3600) { V_WindDirectionSensor -= 3600; }
                else if (V_WindDirectionSensor < 0) { V_WindDirectionSensor += 3600; }
            }
            else if (!V_WindDirectionSensor1Status && V_WindDirectionSensor2Status)
            {
                strcpy(V_WindDirectionSensorsActive, "Sensor 1");
                V_WindDirectionSensor = V_WindDirectionSensor1;
            }
            else if (V_WindDirectionSensor1Status && !V_WindDirectionSensor2Status)
            {
                strcpy(V_WindDirectionSensorsActive, "Sensor 2");
                V_WindDirectionSensor = V_WindDirectionSensor2;
            }
            else
            {
                strcpy(V_WindDirectionSensorsActive, "None");
                V_WindDirectionSensor = 0;
            }
            break;
        }
        case 1: //Sensor 1 selected manually
        {
            if (!V_WindDirectionSensor1Status) //No Errors in sensor 1
            {
                strcpy(V_WindDirectionSensorsActive, "Sensor 1");
                V_WindDirectionSensor = V_WindDirectionSensor1;
            }
            else
            {
                strcpy(V_WindDirectionSensorsActive, "None");
                V_WindDirectionSensor = 0;
            }
            break;
        }
        case 2: //Sensor 2 selected manually
        {
            if (!V_WindDirectionSensor2Status)
            {
                strcpy(V_WindDirectionSensorsActive, "Sensor 2");
                V_WindDirectionSensor = V_WindDirectionSensor2;
            }
            else
            {
                strcpy(V_WindDirectionSensorsActive, "None");
                V_WindDirectionSensor = 0;
            }
            break;
        }
        default:
        {
            break;
        }
    }

#endif

    //-----------------------------
    //Wind Direction calculations
    //-----------------------------

#if defined(WID_DEBUG) || defined(INCLUDE_DEBUG)
    V_WindDirectionSensor = V_WID_WindDirectionSensorTest;   //DEBUG
#endif

    // average values
    F32 avg1s;
    F32 avg3s;
    F32 avg30s;
    F32 avg10m;
    UpdateAvgBearing(&WindDirAvg_Buffer, (F32)V_WindDirectionSensor / 10, NULL, &avg1s, &avg3s, &avg30s, &avg10m);
    V_WindDirectionSensor1Sec = (U16)((avg1s + 0.05) * 10);
    V_WindDirectionSensor3Sec = (U16)((avg3s + 0.05) * 10);
    V_WindDirectionSensor30Sec = (U16)((avg30s + 0.05) * 10);
    V_WindDirectionSensor10Min = (U16)((avg10m + 0.05) * 10);

    // update wind direction ref value, which is used as reference for checking sensor malfunction
    V_WindDirectionRef = V_WindDirectionSensor30Sec;

    // Calculate absolute wind direction based on nacelle position
    V_WindDirection = V_NacelleDirection + V_WindDirectionSensor;
    if (V_WindDirection >= 3600) { V_WindDirection -= 3600; }
    V_WindDirection1Sec = V_NacelleDirection + V_WindDirectionSensor1Sec;
    if (V_WindDirection1Sec >= 3600) { V_WindDirection1Sec -= 3600; }
    V_WindDirection3Sec = V_NacelleDirection + V_WindDirectionSensor3Sec;
    if (V_WindDirection3Sec >= 3600) { V_WindDirection3Sec -= 3600; }
    V_WindDirection30Sec = V_NacelleDirection + V_WindDirectionSensor30Sec;
    if (V_WindDirection30Sec >= 3600) { V_WindDirection30Sec -= 3600; }
    V_WindDirection10Min = V_NacelleDirection + V_WindDirectionSensor10Min;
    if (V_WindDirection10Min >= 3600) { V_WindDirection10Min -= 3600; }

    // Calculate relative wind direction
    if (V_WindDirectionSensor <= 1800) { V_WindDirectionRelative = V_WindDirectionSensor; }
    else { V_WindDirectionRelative = (S16)V_WindDirectionSensor - 3600; }
    if (V_WindDirectionSensor3Sec <= 1800) { V_WindDirectionRelative3Sec = V_WindDirectionSensor3Sec; }
    else { V_WindDirectionRelative3Sec = (S16)V_WindDirectionSensor3Sec - 3600; }
    if (V_WindDirectionSensor30Sec <= 1800) { V_WindDirectionRelative30Sec = V_WindDirectionSensor30Sec; }
    else { V_WindDirectionRelative30Sec = (S16)V_WindDirectionSensor30Sec - 3600; }
    if (V_WindDirectionSensor10Min <= 1800) { V_WindDirectionRelative10Min = V_WindDirectionSensor10Min; }
    else { V_WindDirectionRelative10Min = (S16)V_WindDirectionSensor10Min - 3600; }

    
    // Wind Direction Sensor status update
    if (V_WindDirectionSensor1Status)
    {
        strcpy(V_WDSensor1StatusText, "Error");
    }
    else
    {
        strcpy(V_WDSensor1StatusText, "Ok");
    }
    if (V_WindDirectionSensor2Status)
    {
        strcpy(V_WDSensor2StatusText, "Error");
    }
    else
    {
        strcpy(V_WDSensor2StatusText, "Ok");
    }


    // calculating reference wind direction
    V_WID_WindLeft = (S16)PI_Control_2(WindLeftPI, (F32)V_WindDirectionRelative3Sec, (F32)V_WID_WindLeft);
    V_WID_WindRight = (S16)PI_Control_2(WindRightPI, (F32)V_WindDirectionRelative3Sec, (F32)V_WID_WindRight);
    V_WID_WindDirectionRef = ((V_WID_WindLeft + V_WID_WindRight) % 3600) / 2;
    
    // wind direction variation value calculation
    V_WID_DirVariation = V_WID_WindRight - V_WID_WindLeft;
	V_WID_DirVariation = RateLim_Control(DirVariation_RateLim, V_WID_DirVariation);
    

    // reloading parameters from menu
    if (V_WID_LoadParameters)
    {
        V_WID_LoadParameters = false;
        OS_VarDB_UpdateVariableByName("V_WID_LoadParameters", &V_WID_LoadParameters, sizeof(V_WID_LoadParameters));
        LoadParameters();
    }

    return LocalStatus;
}

