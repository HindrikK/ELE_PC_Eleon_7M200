//*****************************************************************
// Generator speed oscillation detection
//*****************************************************************
static const U32 GenSpeedOscillationPeriodTime = 500; // detect 1,0Hz and higher oscillations
      static struct StandardTimerVarStruct GenSpeedOscillationTimer =
{
   0,                      // Timer value [ms]
   &GenSpeedOscillationPeriodTime, // TimeMax value [ms]
   TIMER_20MS,                // Internal variable, always set to 0
   TIMER_UP,                  // TIMER_DOWN / TIMER_STOP / TIMER_UP
   0                       // Standard timer called flag.
};
static void OperateCtrl_OscillationDetection(void)
{
   static S32 min_G_speed = 0;
   static S32 max_G_speed = 0;
   static U8 init = 1;
   static U8 flag = 0;
   static U8 step = 0;

   if (init)
   {
      init = 0;
      ResetTimer(&GenSpeedOscillationTimer);
      StartTimer(&GenSpeedOscillationTimer);
   }

   if (V_CNV_State == CNV_PRODUCTION)
   {
      if (!flag)
      {
         if (StandardTimer(&GenSpeedOscillationTimer))
         {
            min_G_speed = V_ControllerRpm;
            max_G_speed = V_ControllerRpm;
            ResetTimer(&GenSpeedOscillationTimer);
            StartTimer(&GenSpeedOscillationTimer);
         }
         else
         {
            if (V_ControllerRpm < min_G_speed)
            {
               min_G_speed = V_ControllerRpm;
            }
            if (V_ControllerRpm > max_G_speed)
            {
               max_G_speed = V_ControllerRpm;
            }

            if ((max_G_speed - min_G_speed) >= P_GenOscillationTrigLevel)
            {
               flag = 1;
               ResetTimer(&GenSpeedOscillationTimer);
               StartTimer(&GenSpeedOscillationTimer);

               if (V_ControllerRpm == max_G_speed)
               {
                  step = 0;
               } // next step wait for V_ControllerRpm <= min_G_speed
               else if (V_ControllerRpm == min_G_speed)
               {
                  step = 1;
               } // next step wait for V_ControllerRpm >= max_G_speed
               else
               {
                  flag = 0;
               }
            }
         }
      }
      else
      {
         switch (step)
         {
            case 0: //    wait for minimum in oscillation process
               if (V_ControllerRpm <= (min_G_speed + ((max_G_speed - min_G_speed) / 10)))  // ((max_G_speed - min_G_speed)/10) is for some tolerance
               {
                  step = 1;
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
               }
               if (StandardTimer(&GenSpeedOscillationTimer))
               {
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
                  min_G_speed = V_ControllerRpm;
                  max_G_speed = V_ControllerRpm;
                  flag = 0;
               }
               break;
            case 1: //    wait for maximum in oscillation process
               if (V_ControllerRpm >= (max_G_speed - ((max_G_speed - min_G_speed) / 10)))  //    ((max_G_speed - min_G_speed)/10) is for some tolerance
               {
                  step = 2;
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
               }
               if (StandardTimer(&GenSpeedOscillationTimer))
               {
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
                  min_G_speed = V_ControllerRpm;
                  max_G_speed = V_ControllerRpm;
                  flag = 0;
               }
               break;
            case 2: //    wait for minimum in oscillation process
               if (V_ControllerRpm <= (min_G_speed + ((max_G_speed - min_G_speed) / 10)))  // ((max_G_speed - min_G_speed)/10) is for some tolerance
               {
                  step = 3;
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
               }
               if (StandardTimer(&GenSpeedOscillationTimer))
               {
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
                  min_G_speed = V_ControllerRpm;
                  max_G_speed = V_ControllerRpm;
                  flag = 0;
               }
               break;
            case 3: //    wait for maximum in oscillation process
               if (V_ControllerRpm >= (max_G_speed - ((max_G_speed - min_G_speed) / 10)))  // ((max_G_speed - min_G_speed)/10) is for some tolerance
               {
                  flag = 0;
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
                  V_GeneratorSpeedOscillationFlag = 1;
               }
               if (StandardTimer(&GenSpeedOscillationTimer))
               {
                  ResetTimer(&GenSpeedOscillationTimer);
                  StartTimer(&GenSpeedOscillationTimer);
                  min_G_speed = V_ControllerRpm;
                  max_G_speed = V_ControllerRpm;
                  flag = 0;
               }
               break;
            default:
               ResetTimer(&GenSpeedOscillationTimer);
               StartTimer(&GenSpeedOscillationTimer);
               min_G_speed = V_ControllerRpm;
               max_G_speed = V_ControllerRpm;
               flag = 0;
               break;
         }
      }
   }
   else
   {
      V_GeneratorSpeedOscillationFlag = 0;
   }
}


