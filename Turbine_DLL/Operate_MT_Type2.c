//***********************************************************************
// STATE : Startup
//***********************************************************************
void Startup(BOOL firstentry)
{
   static char Old_ActualOperateStepTxt[ 30 ];
   static U32 FreewheelInitTimerValue = 0;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   if (firstentry)
   {
      UserEvent(EventText_OperateFreewheelInit);
      V_OperateCtrl_State = OPERATE_STATE_UNION_FREEWHEEL_INIT_NA;

      strcpy(V_ActualOperateStateTxt, "Startup");
      strcpy(V_ActualOperateTimerTxt, "None");
      strcpy(V_ActualOperateStepTxt, "None");
      OperateStep = STEP1;
      strcpy(Old_ActualOperateStepTxt, V_ActualOperateStepTxt);

      //-----------------------------------------------------------------------
      V_PitchCtrl_Mode = PITCH_MODE_MANUAL;
      V_Brake_Mode = BRAKE_CONTROL_RELEASE;
      V_CNV_RequestedState = CNV_STOP;
      V_BladeControllerSts1119Enable = DISABLE;

      V_ToPM_P_GridConnect = 0;
      V_ToPM_Q_GridConnect = 0;
      //-----------------------------------------------------------------------

      V_MDSRequestMode = MODE_OPERATE;
      ResetTimer(&MDSRequestTimeoutIntervalTimer);
      StartTimerUp(&MDSRequestTimeoutIntervalTimer);
      FreewheelInitTimerValue = 0;
   }

   //-----------------------------------------------------------------------
   //CONTROL

   V_OperateCtrl_Mode = (V_OperateServiceActive) ? OPERATE_MODE_RUN_SERVICE : OPERATE_MODE_RUN_AUTO;
   //-----------------------------------------------------------------------

   //--------------------------------------------------
   // Wait for MDS to enter state OPERATE as requested
   // if operate waits "too long" for MDS, then set a statuscode
   //--------------------------------------------------
   if (V_MDSCurrentMode != MODE_OPERATE)
   {
      S8 TimerExpired = StandardTimer(&MDSRequestTimeoutIntervalTimer);
      strcpy(V_ActualOperateStepTxt, "Waiting for MDS");

      if (TimerExpired != TIMER_NONE_END)
      {
         OS_ActivateStatusCode(2612);
      }

      V_OperateCtrl_BladePositionLimit = P_OperateCtrl_PitchStopPosition;

      OperateStep = STEP1;

      return;
   }

   strcpy(V_ActualOperateStepTxt, Old_ActualOperateStepTxt);

   //-----------------------------------------------------------------------
   switch (OperateStep)
   {
      case STEP1: // Check for service or run
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_FREEWHEEL_INIT_STEP_1;
         strcpy(V_ActualOperateStepTxt, "STEP 1");

         if (V_PitchServiceTest && PitchServiceTestAllowed)  // Check if pitch controller service test is requested
         {
           StateFunctionPointer = PitchServiceTest;
         }
         else
         {
            OperateStep = STEP2;
         }
         break;
      }
      case STEP2:
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_FREEWHEEL_INIT_STEP_2;

         if (!V_BladeControllerReady)
         {
            V_OperateCtrl_BladePositionLimit = P_OperateCtrl_PitchStopPosition;
            strcpy(V_ActualOperateStepTxt, "STEP 2:Waiting for MDS");
            break;
         }

         strcpy(V_ActualOperateStepTxt, "STEP 2");

         // request PitchAndTorque startup state
         V_PAT_AllowStart = true;

         OperateStep = STEP3;
         break;
      }
      case STEP3: // wait for pitch and torque startup
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_FREEWHEEL_INIT_STEP_3;
         strcpy(V_ActualOperateStepTxt, "STEP 3: Connecting");

         if (V_PAT_State == PAT_STATE_STARTUP)
         {
           if(V_PAT_ConnectRequest)
           {
              V_CNV_RequestedState = CNV_PRODUCTION;
              OperateStep = STEP4;
           }
         }
         break;
      }
      case STEP4: // wait till startup completed
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_FREEWHEEL_INIT_STEP_4;
         strcpy(V_ActualOperateStepTxt, "STEP 4: Ramping up");
         
         if(V_PAT_State > PAT_STATE_STARTUP)
         {
            StateFunctionPointer = OperationG1;
         }
         break;
      }
   }

   strcpy(Old_ActualOperateStepTxt, V_ActualOperateStepTxt);
   V_EventTimeOut = FreewheelInitTimerValue; // Update Display values
   
   // EXIT CONDITIONS
   //-----------------------------------------------------------------------
   if (V_PAT_State == PAT_STATE_LOWWIND)
   {
     StateFunctionPointer = LowWind;
   }
}




