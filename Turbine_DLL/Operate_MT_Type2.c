static struct StandardTimerVarStruct StartWindSpeedDelayTimer =
{
   0, // Timer value [ms]
   &P_StartWindSpeedDelay, // TimeMax value [ms]
   TIMER_20MS, // Internal variable, always set to 0
   TIMER_STOP, // TIMER_DOWN / TIMER_STOP / TIMER_UP
   0 // Standard timer called flag.
};

void Starting(BOOL firstentry)
{
   static U32 StartTimerValue;
   static U8 ForcedStart;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   if (firstentry)
   {
      UserEvent(EventText_OperateStarting);
      V_OperateCtrl_State = OPERATE_STATE_UNION_STARTING;

      strcpy(V_ActualOperateStateTxt, "Starting");
      strcpy(V_ActualOperateStepTxt, "None");
      strcpy(V_ActualOperateTimerTxt, "Secs until start");

      //-----------------------------------------------------------------------
      V_PitchCtrl_Mode = PITCH_MODE_MANUAL;
      V_Brake_Mode = BRAKE_CONTROL_RELEASE;
      V_CNV_RequestedState = CNV_STOP;
      V_BladeControllerSts1119Enable = DISABLE;

      V_ToPM_P_GridConnect = 0;
      V_ToPM_Q_GridConnect = 0;
      //-----------------------------------------------------------------------

      V_GridOperateStartRequest = 1;

      OS_StatusCodeSetReset(0);   // Turbine ready for operation

      ResetTimer(&StartWindSpeedDelayTimer);
      StartTimerValue = 0;
      ForcedStart = 0;

      V_MDSRequestMode = MODE_OPERATE;
      ResetTimer(&MDSRequestTimeoutIntervalTimer);   // Reset MDS timers
      StartTimerUp(&MDSRequestTimeoutIntervalTimer);
   }

   //-----------------------------------------------------------------------
   //CONTROL

   V_OperateCtrl_Mode = (V_OperateServiceActive) ? OPERATE_MODE_RUN_SERVICE : OPERATE_MODE_RUN_AUTO;
   //-----------------------------------------------------------------------

   //--------------------------------------------------
   // Wait for MDS to enter state OPERATE as requested
   // if operate waits "too long" for MDS, then set a statuscode
   //--------------------------------------------------
   if (V_MDSCurrentMode != MODE_OPERATE)
   {
      S8 TimerExpired = StandardTimer(&MDSRequestTimeoutIntervalTimer);
      strcpy(V_ActualOperateStepTxt, "Waiting for MDS");

      if (TimerExpired != TIMER_NONE_END)
      {
         (void)OS_ActivateStatusCode(2612);
      }
      return;
   }
   strcpy(V_ActualOperateStepTxt, "None");

   //--------------------------------------------------
   // If start button is pressed the StartTimerValue is set to zero.
   //--------------------------------------------------
   if (StartButtonFlag)
   {
      SetTimer(&StartWindSpeedDelayTimer, P_StartWindSpeedDelay);
      ForcedStart = 1;
   }
   #if defined DLL_SIMU
   if (DLL_IN_AllowStart)
   {
      SetTimer(&StartWindSpeedDelayTimer, P_StartWindSpeedDelay);
      ForcedStart = 1;
   }
   #endif

   //--------------------------------------------------
   // Check start wind (Start timer Up/Down)
   //--------------------------------------------------
   if (V_WindSpeed30Sec > P_StartWindSpeed)
   {
      StartTimerUp(&StartWindSpeedDelayTimer);
   }
   else
   {
      StartTimerDown(&StartWindSpeedDelayTimer);
   }

   //--------------------------------------------------
   // Call timers and calculate countdown value.
   //--------------------------------------------------
   StandardTimer(&StartWindSpeedDelayTimer);
   V_StartWindTime = P_StartWindSpeedDelay - StartWindSpeedDelayTimer.TimerValue;
   //--------------------------------------------------
   // Finding the highest timer value or set to 0 if forced start.
   //--------------------------------------------------
   if (ForcedStart)
   {
      StartTimerValue = 0;
      strcpy(V_ActualOperateTimerTxt, "Force start");
   }
   else
   {
      StartTimerValue = V_StartWindTime;
      strcpy(V_ActualOperateTimerTxt, "Start wind");
   }
   //--------------------------------------------------
   if (V_GridOperateStartReply != 1)
   {
      strcpy(V_ActualOperateStepTxt, "Waiting for Grid");
      return;
   }
   //--------------------------------------------------
   if (!StartTimerValue)  // Check if start time has expired or forced start (=0)
   {
      if (ForcedStart)  // Check if forced start
      {
         if (!V_BladeControllerReady)
         {
            V_OperateCtrl_BladePositionLimit = P_OperateCtrl_PitchStopPosition;
            strcpy(V_ActualOperateStepTxt, "Waiting for MDS");
            return;
         }
         V_GridOperateStartRequest = 0;
         StateFunctionPointer = YawIntoWind;
         UserEvent(EventText_StartingSkipped);
      }
      else
      {
         if (!V_BladeControllerReady)
         {
            V_OperateCtrl_BladePositionLimit = P_OperateCtrl_PitchStopPosition;
            strcpy(V_ActualOperateStepTxt, "Waiting for MDS");
            return;
         }
         if (V_CNV_State == CNV_READYTOSTART)
         {
            if (0 == V_Grid_ErrorActive)
            {
               V_GridOperateStartRequest = 0;
               StateFunctionPointer = YawIntoWind;
            }
            else
            {
               strcpy(V_ActualOperateStepTxt, "Grid: Error");
            }
         }
         else
         {
            strcpy(V_ActualOperateStepTxt, "Cnv.: Not Inactive");
         }
      }
   }
   else
   {
      strcpy(V_ActualOperateStepTxt, "Startup Timer");
   }

   V_EventTimeOut = StartTimerValue; // Update timer values
}




