//*****************************************************************
// Controls the entry to brake programs states
//*****************************************************************
static void OperateCtrl_BrakeControl(void)
{
   static U8 FirstRun = 1;
   static U8 LocalBrakeProgram;

   if (FirstRun)
   {
      FirstRun = 0;
      LocalBrakeProgram = BRAKE_PROG_199;     // set the brake program value
   }
   else
   {
      LocalBrakeProgram = V_ActiveControlProgram1;
   }

   if ((LocalBrakeProgram > BRAKE_PROG_10) || ActualOperBrakeState)  // if some brake program is set
   {
      if ((LocalBrakeProgram > BRAKE_PROG_10) && (LocalBrakeProgram > ActualOperBrakeState))
      {
         ActualOperBrakeState = LocalBrakeProgram;
      }

      switch (ActualOperBrakeState)
      {
         case BRAKE_PROG_200:
         {
           StateFunctionPointer = BrakeProgram200;
         }
         break;
         case BRAKE_PROG_199:
         {
           StateFunctionPointer = BrakeProgram199;
         }
         break;
         case BRAKE_PROG_99:
         {
           StateFunctionPointer = BrakeProgram99;
         }
         break;
         case BRAKE_PROG_70:
         {
           StateFunctionPointer = BrakeProgram70;
         }
         break;
         case BRAKE_PROG_50:
         {
           StateFunctionPointer = BrakeProgram50;
         }
         break;
         default:
         {
           StateFunctionPointer = BrakeProgram200;
         }
      }
   }
   else // if brake program is not set(turbine is in some operation state)
   {
      strcpy(V_ActualBrakeStateTxt, "None");
   }

   strcpy(BrakeStateTxt, V_ActualBrakeStateTxt);
}


