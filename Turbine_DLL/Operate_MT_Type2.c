//***********************************************************************
// STATE : Selftest 1 (Pitch speed test)
//***********************************************************************
static const U32 Selftest1Timeout = 30 * SEC;

static struct StandardTimerVarStruct Selftest1Timer =
{
   0,                  // Timer value [ms]
   &Selftest1Timeout,  // TimeMax value [ms]
   TIMER_20MS,         // Internal variable, always set to 0
   TIMER_STOP,         // TIMER_DOWN / TIMER_STOP / TIMER_UP
   0                   // Standard timer called flag.
};

void Selftest1(BOOL firstentry)
{
   static U32 Selftest1Time = 0;
   static S32 PitchTestSpan = 0;
   static U32 PitchTestSpeed = 0;
   static U32 Selftest1TimeoutTimerValue = 0;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   if (firstentry)
   {
      UserEvent(EventText_OperateSelftest1);
      V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_1_NA;

      strcpy(V_ActualOperateStateTxt, "Selftest 1");
      strcpy(V_ActualOperateStepTxt, "STEP 1");
      strcpy(V_ActualOperateTimerTxt, "Selftest 1");
      OperateStep = STEP1;

      //-----------------------------------------------------------------------
      V_PitchCtrl_Mode = PITCH_MODE_MANUAL;
      V_Brake_Mode = BRAKE_CONTROL_RELEASE;
      V_MDSRequestMode = MODE_OPERATE;
      V_CNV_RequestedState = CNV_STOP;
      V_BladeControllerSts1119Enable = DISABLE;

      V_ToPM_P_GridConnect = 0;
      V_ToPM_Q_GridConnect = 0;
      //-----------------------------------------------------------------------

      Selftest1Time = 0;
      PitchTestSpan = 0;
      PitchTestSpeed = 0;
      Selftest1TimeoutTimerValue = 0;

      ResetTimer(&MDSRequestTimeoutIntervalTimer);   // Reset MDS timers
      StartTimerUp(&MDSRequestTimeoutIntervalTimer);
   }

   //-----------------------------------------------------------------------
   //CONTROL

   V_OperateCtrl_Mode = (V_OperateServiceActive) ? OPERATE_MODE_RUN_SERVICE : OPERATE_MODE_RUN_AUTO;
   //-----------------------------------------------------------------------

   //--------------------------------------------------
   // Wait for MDS to enter state OPERATE as requested
   // if operate waits "too long" for MDS, then set a statuscode
   //--------------------------------------------------
   if (V_MDSCurrentMode != MODE_OPERATE)
   {
      S8 TimerExpired = StandardTimer(&MDSRequestTimeoutIntervalTimer);
      strcpy(V_ActualOperateStepTxt, "Waiting for MDS");
      if (TimerExpired != TIMER_NONE_END)
      {
         OS_ActivateStatusCode(2612);
      }
      return;
   }

   switch (OperateStep)
   {
      case STEP1: // Close mechanical brake
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_1_STEP_1;
         OperateStep = STEP2;
         strcpy(V_ActualOperateStepTxt, "STEP 2");
         break;
      }
      case STEP2: // Set pitch controller
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_1_STEP_2;
         // Update Pitch Controller
         if (!V_BladeControllerReady)
         {
            V_OperateCtrl_BladePositionLimit = P_OperateCtrl_PitchStopPosition;
            strcpy(V_ActualOperateStepTxt, "STEP 2:Waiting for MDS");
            break;
         }
         strcpy(V_ActualOperateStepTxt, "STEP 2");
         V_OperateCtrl_BladePositionLimit = P_SelftestPitchPos;

         // Start timer for measuring pitch speed
         ResetTimer(&Selftest1Timer);
         StartTimerUp(&Selftest1Timer);

         OperateStep = STEP3;
         strcpy(V_ActualOperateStepTxt, "STEP 3");
         break;
      }
      case STEP3: // Await pitch in test position
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_1_STEP_3;
         if (StandardTimer(&Selftest1Timer))    // Call timer & check for max. test time
         {
            // Selftest 1 failed
            V_SetStatusCodeFlag_8404 = 1; // Selftest 1

           StateFunctionPointer = SelftestSkip;
         }
         else if (V_BlCtrl_PitchAngle <= (P_SelftestPitchPos + P_SelftestPitchPosTol))  // Test position reached
         {
            Selftest1Time = GetTimer(&Selftest1Timer);
            PitchTestSpan = P_OperateCtrl_PitchStopPosition - V_BlCtrl_PitchAngle; // Pitch from 90 to 80
            OperateStep = STEP4;
            strcpy(V_ActualOperateStepTxt, "STEP 4");
         }
         break;
      }
      case STEP4: // Calculate and check pitchspeed
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_1_STEP_4;
         PitchTestSpeed = ((PitchTestSpan * 100) / Selftest1Time);

         if (abs(PitchTestSpeed - (P_SelftestPitchSpeed / 10)) > P_SelftestPitchTol)
         {
            // Selftest 1 failed
            V_SetStatusCodeFlag_8404 = 1; // Selftest 3
           StateFunctionPointer = SelftestSkip;
         }
         else
         {
            // Selftest 1 succeded
           StateFunctionPointer = Selftest2;
         }
         break;
      }
   }

   if (StartButtonFlag)  // Check if skip by operator
   {
     StateFunctionPointer = SelftestSkip;
      UserEvent(EventText_Selftest2Skipped);
   }

   Selftest1TimeoutTimerValue = Selftest1Timeout - Selftest1Timer.TimerValue; // Update remaining time
   if (!Selftest1TimeoutTimerValue)
   {
      V_SetStatusCodeFlag_1112 = 1;
   }

   V_EventTimeOut = Selftest1TimeoutTimerValue; // Update Display values
}




