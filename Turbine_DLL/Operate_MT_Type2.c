//*****************************************************************
// MAIN control loop
//*****************************************************************
static STATUS LocalCtrl(void)
{
   STATUS LocalStatus = OS_SUCCESS;
   
   if (AppConfiguration == 1)   // do only when in FeCon Converter configuration
   {
       OS_VarDB_ReadVariable(V_CNVC_PumpService_ID, &V_CNVC_PumpService, sizeof(V_CNVC_PumpService), NULL);
       OS_VarDB_ReadVariable(V_CNVC_CoolantFanService_ID, &V_CNVC_CoolantFanService, sizeof(V_CNVC_CoolantFanService), NULL);
       OS_VarDB_ReadVariable(V_CNVC_OutdoorFanService_ID, &V_CNVC_OutdoorFanService, sizeof(V_CNVC_OutdoorFanService), NULL);
   }
   
   //-----------------------------------------------------------------------
   CheckButtons(); // Check remote-display buttons
   CheckInternalFlags();
   ZeroRpm();
   //-----------------------------------------------------------------------
   OperateCtrl_BrakeControl();     // check if some brake program needs to be set
   LocalStatus = OperateCtrl_BrakeProg_SetTrig();
   //-----------------------------------------------------------------------
   PitchJogMoveDetect();   // Check if JogMove of blade-bearings is nescessary



   //***********************************************************************
   // Function call to operating state functions - uses pointer to function
   // "StateFunctionPointer" points to the mode function that should be called
   //***********************************************************************
   if (StateFunctionPointer != StateFunctionPointer_Prev)
   {
	   StateFunctionPointer_Prev = StateFunctionPointer;
	   (*StateFunctionPointer)(true);
   }
   else
   {
      (*StateFunctionPointer)(false);
   }



   //-----------------------------------------------------------------------
   OperateCtrl_SummationHandling();
   OperateCtrl_OscillationDetection();
   //-----------------------------------------------------------------------
   OperateCtrl_SystemStatusHandling();

   OperateCtrl_WriteMode();
   OperateCtrl_WritePitchCtrlMode();
   OperateCtrl_WriteBladeCtrlMode();
   //-----------------------------------------------------------------------
   GlobalVariablesHandling();

   return LocalStatus;
}
