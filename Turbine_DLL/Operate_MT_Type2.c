//***********************************************************************
// FUNCTION : Pitch motion detection
//            Detects whether extraordinary movement with the blades (motion)
//            is necessary.
//***********************************************************************
static struct StandardTimerVarStruct PitchJogMoveDetectTimer =
{
   0,                   // Timer value [ms]
   &P_PitchJogMoveTriggerTime,   // TimeMax value [ms]
   TIMER_20MS,             // Internal variable, always set to 0
   TIMER_STOP,             // TIMER_DOWN / TIMER_STOP / TIMER_UP
   0                    // Standard timer called flag.
};

static void PitchJogMoveDetect(void)
{
   static S32 PitchPosOrg = 0;
   static S32 PitchPosMax = 0;
   static S32 PitchNegMax = 0;
   S32 LastPitchMove = 0;
   static U8 State = STEP1;

   switch (State)
   {
      case STEP1: // Idle : Await valid operate state
      {
         if ((StateFunctionPointer == Startup) || (StateFunctionPointer == OperationG1))
         {
            PitchPosOrg = V_BlCtrl_PitchAngle;
            PitchPosMax = 0;
            PitchNegMax = 0;
            PitchJogMoveRequired = 0;
            ResetTimer(&PitchJogMoveDetectTimer);
            StartTimer(&PitchJogMoveDetectTimer);
            State = STEP2;
         }
      }
      break;
      case STEP2: // Detection : Minimum pitch movement within timeframe
      {
         if ((StateFunctionPointer == Startup) || (StateFunctionPointer == OperationG1))
         {
            if (V_BlCtrl_PitchAngle > PitchPosOrg)  // Check max. movement in positive direction
            {
               LastPitchMove = V_BlCtrl_PitchAngle - PitchPosOrg;
               if (LastPitchMove > PitchPosMax)
               {
                  PitchPosMax = LastPitchMove;
               }
            }
            if (PitchPosOrg > V_BlCtrl_PitchAngle)  // Check max. movement in negative direction
            {
               LastPitchMove = PitchPosOrg - V_BlCtrl_PitchAngle;
               if (LastPitchMove > PitchNegMax)
               {
                  PitchNegMax = LastPitchMove;
               }
            }

            if (StandardTimer(&PitchJogMoveDetectTimer))    // Check for detection time expired
            {
               if ((PitchPosMax >= P_PitchJogMoveTriggerPos) || (PitchNegMax >= P_PitchJogMoveTriggerPos))  // Check if movement is OK and restart detection
               {
                  PitchJogMoveRequired = 0;
                  State = STEP1;
               }
               else // Pitchmovement is required
               {
                  PitchJogMoveRequired = 1;
                  State = STEP3;
               }
            }
         }
      }
      break;
      case STEP3: // Acknowledge : Await Jog Move started
      {
         if (StateFunctionPointer == MDSPitchJogMove)
         {
            PitchJogMoveRequired = 0;
            State = STEP1;
         }
      }
      break;
   }
}








