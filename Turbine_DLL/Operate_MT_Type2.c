static const U32 Selftest2Timeout = 30 * SEC;

static struct StandardTimerVarStruct Selftest2Timer =
{
   0,                      // Timer value [ms]
   &Selftest2Timeout,      // TimeMax value [ms]
   TIMER_20MS,             // Internal variable, always set to 0
   TIMER_STOP,             // TIMER_DOWN / TIMER_STOP / TIMER_UP
   0                       // Standard timer called flag.
};

void Selftest2(BOOL firstentry)
{
   static U32 Selftest2TimeoutTimerValue = 0;

   //-----------------------------------------------------------------------
   //ENTRY ACTION
   if (firstentry)
   {
      UserEvent(EventText_OperateSelftest2);
      V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_2_NA;

      strcpy(V_ActualOperateStateTxt, "Selftest 2");
      strcpy(V_ActualOperateStepTxt, "STEP 0");
      strcpy(V_ActualOperateTimerTxt, "Selftest 2");
      OperateStep = STEP0;

      //-----------------------------------------------------------------------
      V_PitchCtrl_Mode                        = PITCH_MODE_MANUAL;
      V_Brake_Mode                            = BRAKE_CONTROL_RELEASE;
      V_MDSRequestMode                        = MODE_OPERATE;
      V_CNV_RequestedState                    = CNV_STOP;
      V_BladeControllerSts1119Enable          = DISABLE;

      V_ToPM_P_GridConnect                    = 0;
      V_ToPM_Q_GridConnect                    = 0;
      //-----------------------------------------------------------------------

      ResetTimer(&MDSRequestTimeoutIntervalTimer);   // Reset MDS timers
      StartTimerUp(&MDSRequestTimeoutIntervalTimer);

      Selftest2TimeoutTimerValue = 0;
   }

   //-----------------------------------------------------------------------
   //CONTROL

   V_OperateCtrl_Mode = (V_OperateServiceActive) ? OPERATE_MODE_RUN_SERVICE : OPERATE_MODE_RUN_AUTO;
   //-----------------------------------------------------------------------

   //--------------------------------------------------
   // Wait for MDS to enter state OPERATE as requested
   // if operate waits "too long" for MDS, then set a statuscode
   //--------------------------------------------------
   if (V_MDSCurrentMode != MODE_OPERATE)
   {
      S8 TimerExpired = StandardTimer(&MDSRequestTimeoutIntervalTimer);
      strcpy(V_ActualOperateStepTxt, "Waiting for MDS");

      if (TimerExpired != TIMER_NONE_END)
      {
         OS_ActivateStatusCode(2612);
      }
      return;
   }

   switch (OperateStep)
   {
      case STEP0:
      {
         if (V_BladeControllerReady)
         {
            V_OperateCtrl_BladePositionLimit    = P_SelftestPitchPos;
            strcpy(V_ActualOperateStepTxt, "STEP 0");
            OperateStep = STEP1;
         }
         else
         {
            V_OperateCtrl_BladePositionLimit    = P_OperateCtrl_PitchStopPosition;
            strcpy(V_ActualOperateStepTxt, "Waiting for MDS");
         }
         break;
      }
      case STEP1: // Start timer
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_4_STEP_1;
         ResetTimer(&Selftest2Timer);
         StartTimerUp(&Selftest2Timer);

         OperateStep = STEP2;
         strcpy(V_ActualOperateStepTxt, "STEP 2");
         break;
      }
      case STEP2: // Await expected pitch positioning finished (Pos = 80)
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_2_STEP_2;
         if (StandardTimer(&Selftest2Timer))    // Call timer & check for timeout
         {
            OperateStep = STEP3;
            strcpy(V_ActualOperateStepTxt, "STEP 3");
         }
         break;
      }
      case STEP3: // Check pitch pos. within tolerance (Pos = 80)
      {
         V_OperateCtrl_State = OPERATE_STATE_UNION_SELFTEST_2_STEP_3;

         if ((V_BlCtrl_PitchAngle > (P_SelftestPitchPos + P_SelftestPitchPosTol)) || (V_BlCtrl_PitchAngle < (P_SelftestPitchPos - P_SelftestPitchPosTol)))
         {
            // Selftest 2 failed
            V_SetStatusCodeFlag_8405 = 1; // Selftest 2
           StateFunctionPointer = SelftestSkip;
         }
         else
         {
            UserEvent(EventText_Selftest2Skipped);
           StateFunctionPointer = SelftestSkip;
         }
         break;
      }
   }

   if (StartButtonFlag)  // Check if skip by operator
   {
     StateFunctionPointer = SelftestSkip;
      UserEvent(EventText_Selftest2Skipped);
   }
   Selftest2TimeoutTimerValue = Selftest2Timeout - Selftest2Timer.TimerValue; // Update remaining time
   if ((!Selftest2TimeoutTimerValue) && (OperateStep == STEP1))
   {
      V_SetStatusCodeFlag_1112 = 1;
   }

   V_EventTimeOut = Selftest2TimeoutTimerValue; // Update Display values
}




