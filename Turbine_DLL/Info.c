#define PERCENT_SIGN                0x25            //Percent sign used in status line and menu lines. Used like single symbol
#define STATUS_LINE_LENGTH          42              //Number of meaning characters in status line. 42 characters chosen due to characters count WP4051 can display
#define STATUS_LINE_TAIL_LENGTH     13              //Number of meaning characters in the end of status line for operate state

#define RIGHTWARDS                  1
#define LEFTWARDS                   -1

const char DEGREE_SIGN[3] = {0xC2, 0xB0, '\0'}; //Degree sign used in status line and menu lines. Used like string. NOTE: has two meaning characters

static STATUS LocalCtrl(void)
{
    static  U8      LongMessageState = 0;
    STATUS  LocalStatus;

    // Refresh availability data
    V_INF_AvailabilityTotal = Availability->Live.Total.Result.Percentage;
    V_INF_AvailabilityMonth = Availability->Live.ThisMonth.Result.Percentage;


    //Get local copy of Info Message and ensure that it will have EOL symbol
    char InfoMessageTxtLocal[sizeof(P_InfoMessageTxt)];
    strncpy(InfoMessageTxtLocal, P_InfoMessageTxt, sizeof(P_InfoMessageTxt) - 1);
    InfoMessageTxtLocal[sizeof(P_InfoMessageTxt) - 1] = '\0';


    S16 RelativeWindDirection = ((S16)V_WindDirection30Sec) - V_NacelleDirection;   //Relative Wind Direction

    if (RelativeWindDirection > 1800)                                                   //Fit Relative Wind Direction in -179.9...180.0 degrees
    {
        RelativeWindDirection -= 3600;
    }
    else
    {
        if (RelativeWindDirection < -1799)
        {
            RelativeWindDirection += 3600;
        }
    }
    //-----------------------------------------------------------------------
    // MENU                        MENU                       MENU
    //-----------------------------------------------------------------------

    StartTimer(&StatusUpdateTimer);
    S8 StatusTimerOut = StandardTimer(&StatusUpdateTimer);  //Separate variable to have possibility to use this timer in menu lines and status line

    if (StatusTimerOut)                                     //Updates menu lines every P_StatusUpdateInterval ms. Used the same timer as for status line
    {
        //--------------------------------------------------------------------
        //System State
        if (!V_ServiceModeSwitch)                           //If service button pressed
        {
            //Prints "LOCAL SERVICE"
            snprintf(V_SystemStateTxt,
                     sizeof(V_SystemStateTxt),
                     "%s",
                     "LOCAL SERVICE"
                    );
        }
        else
        {
            if (B_RemoteService)                            //If service requested by applet
            {
                //Prints "REMOTE SERVICE"
                snprintf(V_SystemStateTxt,
                         sizeof(V_SystemStateTxt),
                         "%s",
                         "REMOTE SERVICE"
                        );
            }
            else                                            //If no service requested
            {
                //Prints system state
                snprintf(V_SystemStateTxt,
                         sizeof(V_SystemStateTxt),
                         "%s",
                         V_SystemStatusText
                        );
            }
        }
        //--------------------------------------------------------------------
        //Control Programs
        U16 ActiveCP1CodeNoTemp;
        if (V_ActiveCP1CodeNo == 65535)
        {
            ActiveCP1CodeNoTemp = 0;                        //Set brake status code No to 0 if 65535
        }
        else
        {
            ActiveCP1CodeNoTemp = V_ActiveCP1CodeNo;        //Else use actual brake status code No
        }

        U16 ActiveCP2CodeNoTemp;
        if (V_ActiveCP2CodeNo == 65535)
        {
            ActiveCP2CodeNoTemp = 0;                        //Set yaw status code No to 0 if 65535
        }
        else
        {
            ActiveCP2CodeNoTemp = V_ActiveCP2CodeNo;        //Else use actual yaw status code No
        }


        snprintf(V_ControlProgramsTxt,                    //Prints active brake program and caused status code and
                 sizeof(V_ControlProgramsTxt),           //active yaw program and caused status code
                 "BP: %u, SC: %u;   YP: %u, SC: %u",
                 V_ActiveControlProgram1,
                 ActiveCP1CodeNoTemp,
                 V_ActiveControlProgram2,
                 ActiveCP2CodeNoTemp
                );
        //--------------------------------------------------------------------
        //Safety Chain / Grid
        if (GridErrorActive())
        {
            snprintf(V_SafetyChainWithGridTxt,                               //Prints Safety Chain state
                     sizeof(V_SafetyChainWithGridTxt),                       //and Grid error
                     "%s;   %s",
                     V_SafetyChainStateText,
                     "Grid Error"
                    );
        }
        else
        {
            snprintf(V_SafetyChainWithGridTxt,                               //Prints Safety Chain state
                     sizeof(V_SafetyChainWithGridTxt),                       //and Grid OK
                     "%s;   %s",
                     V_SafetyChainStateText,
                     "Grid OK"
                    );
        }
        //--------------------------------------------------------------------
        //Nacelle / Wind / Diff
        snprintf(V_DirectionsTxt,                             //Prints nacelle direction, 30 seconds averaged wind direction and difference
                 sizeof(V_DirectionsTxt),                            //between this directions with one number after decimal point and sign for difference
                 "%u.%u%s / %u.%u%s / %c%u.%u%s",
                 V_NacelleDirection / 10,
                 V_NacelleDirection % 10,
                 DEGREE_SIGN,
                 V_WindDirection30Sec / 10,
                 V_WindDirection30Sec % 10,
                 DEGREE_SIGN,
                 (RelativeWindDirection >= 0) ? ' ' : '-',
                 (RelativeWindDirection >= 0) ? RelativeWindDirection / 10 : (RelativeWindDirection * -1) / 10,
                 (RelativeWindDirection >= 0) ? RelativeWindDirection % 10 : (RelativeWindDirection * -1) % 10,
                 DEGREE_SIGN
                );
        //--------------------------------------------------------------------
        //Wind Speed
        snprintf(V_WindSpeedTxt,                                  //Prints current wind speed with one number after decimal point
                 sizeof(V_WindSpeedTxt),
                 "%u.%u m/s",
                 V_WindSpeed / 100,
                 (V_WindSpeed % 100) / 10
                );
        //--------------------------------------------------------------------
        //Blade 1 / Blade 2 / Blade 3 Angles
        snprintf(V_PitchAnglesTxt,                            //Prints actual pitch angle for each blade with one number after decimal point and sign
                 sizeof(V_PitchAnglesTxt),
                 "%c%u.%u%s / %c%u.%u%s / %c%u.%u%s",
                 (V_PitchAngle1 >= 0) ? ' ' : '-',
                 (V_PitchAngle1 >= 0) ? V_PitchAngle1 / 100 : (V_PitchAngle1 * -1) / 100,
                 (V_PitchAngle1 >= 0) ? (V_PitchAngle1 % 100) / 10 : ((V_PitchAngle1 * -1) % 100) / 10,
                 DEGREE_SIGN,
                 (V_PitchAngle2 >= 0) ? ' ' : '-',
                 (V_PitchAngle2 >= 0) ? V_PitchAngle2 / 100 : (V_PitchAngle2 * -1) / 100,
                 (V_PitchAngle2 >= 0) ? (V_PitchAngle2 % 100) / 10 : ((V_PitchAngle2 * -1) % 100) / 10,
                 DEGREE_SIGN,
                 (V_PitchAngle3 >= 0) ? ' ' : '-',
                 (V_PitchAngle3 >= 0) ? V_PitchAngle3 / 100 : (V_PitchAngle3 * -1) / 100,
                 (V_PitchAngle3 >= 0) ? (V_PitchAngle3 % 100) / 10 : ((V_PitchAngle3 * -1) % 100) / 10,
                 DEGREE_SIGN
                );
        //--------------------------------------------------------------------
        //Rotor rpm
        snprintf(V_RPMsTxt,                                       //Prints actual rotor speed with two numbers after decimal point
                 sizeof(V_RPMsTxt),
                 "%u.%u rpm",
                 V_ControllerRpm / 1000,
                 (V_ControllerRpm % 1000) / 10
                );
        //--------------------------------------------------------------------
        //Real Power / Reactive Power
        snprintf(V_PowersTxt,                                 //Prints actual active and reactive power with one number after decimal point and sign
                 sizeof(V_PowersTxt),
                 "%c%u.%u kW / %c%u.%u kVAr",
                 (V_GridRealPower1Sec >= 0) ? ' ' : '-',
                 (V_GridRealPower1Sec >= 0) ? V_GridRealPower1Sec / 1000 : (V_GridRealPower1Sec * -1) / 1000,
                 (V_GridRealPower1Sec >= 0) ? (V_GridRealPower1Sec % 1000) / 100 : ((V_GridRealPower1Sec * -1) % 1000) / 100,
                 (V_GridReactivePower >= 0) ? ' ' : '-',
                 (V_GridReactivePower >= 0) ? V_GridReactivePower / 1000 : (V_GridReactivePower * -1) / 1000,
                 (V_GridReactivePower >= 0) ? (V_GridReactivePower % 1000) / 100 : ((V_GridReactivePower * -1) % 1000) / 100
                );
        //--------------------------------------------------------------------

        ResetTimer(&StatusUpdateTimer);
    }
    //-----------------------------------------------------------------------
    // STATUS LINE TEXT        STATUS LINE TEXT           STATUS LINE TEXT
    //-----------------------------------------------------------------------



    if (0 != V_WP4000StatusText_ID)                          //Executes if got ID of status line variable
    {

        size_t InfoMessageLength = strlen(InfoMessageTxtLocal); //Info message length

        static  char*   PointerToInfoMessage1 = NULL;   //Two pointers used in
        static  char*   PointerToInfoMessage2 = NULL;   //info message scrolling
        static  char    PointerToSpaces[] = "                                                            ";

        static  U8      LongModifier0 = STATUS_LINE_LENGTH;         //Modifier to control spaces length in front of message in first scroll round
        static  U8      LongModifier1 = 0;                          //Modifier to control message length pointed by PointerToInfoMessage1
        static  U8      LongModifier2 = 0;                          //Modifier to control spaces between end of message and begining of new one
        static  U8      LongModifier3 = 0;                          //Modifier to control message length pointed by PointerToInfoMessage2


        // print info message in status line
        //----------------------------------------
        if (V_INF_InfoMsgShowDuration && !SysMsgShow)
        {
            SysMsgShow = true;
            if (V_INF_InfoMsgShowDuration <= SYSMSG_MAXDURATION)
            {
                SysMsg_Timer = V_INF_InfoMsgShowDuration * 1000;    // load timer value in [ms]
            }
            else
            {
                SysMsg_Timer = SYSMSG_MAXDURATION * 1000;
            }
            V_INF_InfoMsgShowDuration = 0;
            OS_VarDB_UpdateVariableByName("V_INF_InfoMsgShowDuration", &V_INF_InfoMsgShowDuration, sizeof(V_INF_InfoMsgShowDuration));

            // read system message text from variable database
            char SysMsgTxt[60];
            time_t TimeStamp;
            if (OS_VarDB_ReadVariableByName("V_INF_InfoMsgTxt", &SysMsgTxt, sizeof(SysMsgTxt), &TimeStamp) == OS_SUCCESS)
            {
                // write system message text to status line
                if (OS_VarDB_UpdateVariable(V_WP4000StatusText_ID, &SysMsgTxt, sizeof(SysMsgTxt)) == OS_SUCCESS) { return OS_SUCCESS; }
            }
        }
        else if (SysMsgShow)    // count system message text displaying duration
        {
            SysMsg_Timer -= SCANTIME_INFO;
            if (SysMsg_Timer <= 0)
            {
                SysMsgShow = false;
            }

            // new message request received
            if (V_INF_InfoMsgShowDuration)
            {
                SysMsgShow = false;
            }

            return OS_SUCCESS;
        }
        //----------------------------------------


        if (InfoMessageLength > STATUS_LINE_LENGTH)         //Info message is longer than status line can display
        {
            StartTimer(&InfoMessageUpdateTimer);
            if (StandardTimer(&InfoMessageUpdateTimer))     //Scroll message every P_InfoMessageUpdateInterval ms
            {
                if (LongMessageState != 1)                  //Service flag to reset pointer and modifiers when leave this state
                {
                    LongMessageState = 1;

                    PointerToInfoMessage1 = InfoMessageTxtLocal;
                    PointerToInfoMessage2 = InfoMessageTxtLocal;

                    LongModifier0 = STATUS_LINE_LENGTH;
                    LongModifier1 = 0;
                    LongModifier2 = 0;
                    LongModifier3 = 0;
                }

                snprintf(V_StatusLineTxt,
                         sizeof(V_StatusLineTxt),
                         "%.*s%.*s%.*s%.*s",
                         LongModifier0,
                         PointerToSpaces,
                         LongModifier1,
                         PointerToInfoMessage1,
                         LongModifier2,
                         PointerToSpaces,
                         LongModifier3,
                         PointerToInfoMessage2
                        );

                if (LongModifier1 < STATUS_LINE_LENGTH)     //Message is moving from right to left first time
                {
                    if (LongModifier0 > 0)
                    {
                        --LongModifier0;                    //Decrease amount of spaces
                    }

                    ++LongModifier1;                        //Increase amount of viewed characters
                }
                else                                        //Message occupies whole line from first character
                {
                    if (PointerToInfoMessage1 - InfoMessageTxtLocal < InfoMessageLength - STATUS_LINE_LENGTH)
                    {
                        ++PointerToInfoMessage1;            //Scroll message while last character occured
                    }
                    else
                    {
                        if (PointerToInfoMessage1 - InfoMessageTxtLocal < InfoMessageLength - STATUS_LINE_LENGTH + MESSAGE_SPACES_LENGTH - 1)
                        {
                            ++PointerToInfoMessage1;        //Scroll message and fill with spaces end of line
                            ++LongModifier2;                //till all the spaces viewed (MESSAGE_SPACES_LENGTH count)
                        }
                        else
                        {
                            if (PointerToInfoMessage1 - InfoMessageTxtLocal < InfoMessageLength)
                            {
                                ++PointerToInfoMessage1;    //Scroll message and spaces and fill end of line with begining of message
                                ++LongModifier3;            //till spaces take position in the beginning of line
                            }
                            else
                            {
                                if (LongModifier2 > 0)
                                {
                                    --LongModifier2;        //Scroll spaces and message
                                    ++LongModifier3;        //till all spaces disappear
                                }
                                else
                                {
                                    PointerToInfoMessage1 = InfoMessageTxtLocal;
                                    LongModifier2 = 0;      //Reset pointer and modifiers and continue from the place above
                                    LongModifier3 = 0;      //when message occupies whole line from first character
                                }
                            }
                        }
                    }
                }

                ResetTimer(&InfoMessageUpdateTimer);
            }
        }
        else
        {
            // print service info message
            if (InfoMessageLength > 0)                      //Message can fit in line fully
            {
                if (LongMessageState == 1)                  //Reset pointer and modifiers if message was too long
                {
                    LongMessageState = 0;

                    PointerToInfoMessage1 = InfoMessageTxtLocal;

                    LongModifier0 = STATUS_LINE_LENGTH;
                    LongModifier1 = 0;
                    LongModifier2 = 0;
                    LongModifier3 = 0;
                }

                snprintf(V_StatusLineTxt,                 //Message is printing in the begining of line
                         sizeof(V_StatusLineTxt),
                         "%s",
                         InfoMessageTxtLocal
                        );
            }
            else                                            //Info message is empty. Print standard status line
            {
                if (LongMessageState == 1)                  //Reset pointer and modifiers if message was too long
                {
                    LongMessageState = 0;

                    PointerToInfoMessage1 = InfoMessageTxtLocal;

                    LongModifier0 = STATUS_LINE_LENGTH;
                    LongModifier1 = 0;
                    LongModifier2 = 0;
                    LongModifier3 = 0;
                }

                /*
                char Yaw = YAW_STOPPED_SYMBOL;              //Set yaw to STOPPED by default

                if (V_YAW_State == YAW_RUN_CW)
                {
                    Yaw = YAW_CW_SYMBOL;                    //Set yaw to CW if respective DO is high
                }

                if (V_YAW_State == YAW_RUN_CCW)
                {
                    Yaw = YAW_CCW_SYMBOL;                   //Set yaw to CCW if respective DO is high
                }
                */

                //String where operate state will be placed
                static  char    OperationStateTxt[STATUS_LINE_TAIL_LENGTH + 1] = "1234567890123";

                //String where old operate state will be placed to track changes
                static  char    ActualOperateStateTxtOld[30];

                //Pointer to operate state
                static  char*   PointerToActualOperateState1 = V_ActualOperateStateTxt;
                static  char*   PointerToActualOperateState2 = V_ActualOperateStateTxt;
                static  char        PointerToSpaces[] = "                              ";

                static  U8      Modifier0 = STATUS_LINE_TAIL_LENGTH;
                static  U8      Modifier1 = 0;
                static  U8      Modifier2 = 0;
                static  U8      Modifier3 = 0;

                //Operate state length
                size_t ActualOperateStateLength = strlen(V_ActualOperateStateTxt);

                StartTimer(&OperateStateUpdateTimer);
                if (StandardTimer(&OperateStateUpdateTimer))//Scroll operate state every P_OperateStateUpdateInterval ms
                {
                    if (strcmp(V_ActualOperateStateTxt, ActualOperateStateTxtOld))  //Check if operate state changed and
                    {
                        //reset pointer and modifiers if yes
                        strcpy(ActualOperateStateTxtOld, V_ActualOperateStateTxt);
                        PointerToActualOperateState1 = V_ActualOperateStateTxt;
                        Modifier0 = STATUS_LINE_TAIL_LENGTH;
                        Modifier1 = 0;
                        Modifier2 = 0;
                        Modifier3 = 0;
                    }

                    if (ActualOperateStateLength > STATUS_LINE_TAIL_LENGTH)//If operate state is too long
                    {
                        snprintf(OperationStateTxt,
                                 sizeof(OperationStateTxt),
                                 "%.*s%.*s%.*s%.*s",
                                 Modifier0,
                                 PointerToSpaces,
                                 Modifier1,
                                 PointerToActualOperateState1,
                                 Modifier2,
                                 PointerToSpaces,
                                 Modifier3,
                                 PointerToActualOperateState2
                                );

                        if (Modifier1 < STATUS_LINE_TAIL_LENGTH)        //Operate state is moving from right to left first time
                        {
                            if (Modifier0 > 0)
                            {
                                --Modifier0;                    //Decrease amount of spaces
                            }

                            ++Modifier1;                        //Increase amount of viewed characters
                        }
                        else                                        //Operate state occupies whole line from first character
                        {
                            if (PointerToActualOperateState1 - V_ActualOperateStateTxt < ActualOperateStateLength - STATUS_LINE_TAIL_LENGTH)
                            {
                                ++PointerToActualOperateState1;         //Scroll Operate state while last character occured
                            }
                            else
                            {
                                if (PointerToActualOperateState1 - V_ActualOperateStateTxt < ActualOperateStateLength - STATUS_LINE_TAIL_LENGTH + OPERATE_SPACES_LENGTH - 1)
                                {
                                    ++PointerToActualOperateState1;     //Scroll Operate state and fill with spaces end of line
                                    ++Modifier2;                //till all the spaces viewed (OPERATE_SPACES_LENGTH count)
                                }
                                else
                                {
                                    if (PointerToActualOperateState1 - V_ActualOperateStateTxt < ActualOperateStateLength)
                                    {
                                        ++PointerToActualOperateState1; //Scroll Operate state and spaces and fill end of line with begining of Operate state
                                        ++Modifier3;            //till spaces take position in the begining of line
                                    }
                                    else
                                    {
                                        if (Modifier2 > 0)
                                        {
                                            --Modifier2;        //Scroll spaces and Operate state
                                            ++Modifier3;        //till all spaces disappear
                                        }
                                        else
                                        {
                                            PointerToActualOperateState1 = V_ActualOperateStateTxt;
                                            Modifier2 = 0;      //Reset pointer and modifiers and continue from the place above
                                            Modifier3 = 0;      //when Operate state occupies whole line from first character
                                        }
                                    }
                                }
                            }
                        }
                    }
                    else                                            //If operate state can be printed fully
                    {
                        strcpy(OperationStateTxt, V_ActualOperateStateTxt);
                    }

                    ResetTimer(&OperateStateUpdateTimer);
                }

                //Variables which will have information for standard status line
                static S16 SL_YawDiff               = 0;            //Difference between nacelle direction and 30 second average wind direction
                static U16 SL_WindSpeedOnes         = 0;            //30 second average wind speed, before decimal point
                static U16 SL_WindSpeedTenths       = 0;            //30 second average wind speed, after decimal point
                static S16 SL_PitchAngle            = 0;            //Average pitch angle, before decimal point
                static U16 SL_PitchAngleTenths      = 0;            //Average pitch angle, after decimal point
                static char SL_PitchAngleTxt[9]     = "--,-\0";     //Average pitch angle text
                static U32 SL_RotorRpmOnes          = 0;            //Rotor rotational speed, before decimal point
                static U32 SL_RotorRpmTenths        = 0;            //Rotor rotational speed, after decimal point
                static S32 SL_GridRealPower         = 0;            //Actual power
                static char SL_StatusTxt[6]         = "OK\0";       //Number of active status codes or OK

                if (StatusTimerOut)                                 //Update status line every P_StatusUpdateInterval ms. Used the same timer as for menu lines
                {
                    SL_YawDiff = RelativeWindDirection / 10;                                    //Division by 10 because of SCALE_DIV 10
                    SL_WindSpeedOnes = V_WindSpeed / 100;                                       //Value before decimal point. Division by 100 because of SCALE_DIV 100
                    SL_WindSpeedTenths = (V_WindSpeed % 100) / 10;                              //Value after decimal point. Division by 10 to have only one number after decimal point
                    if (V_PitchAngle < 0) { snprintf(SL_PitchAngleTxt, sizeof(SL_PitchAngleTxt), "-%d.%u%s", (V_PitchAngle / 100), ((abs(V_PitchAngle) % 100) / 10), DEGREE_SIGN); }
                    else { snprintf(SL_PitchAngleTxt, sizeof(SL_PitchAngleTxt), "%2d.%u%s", (V_PitchAngle / 100), ((abs(V_PitchAngle) % 100) / 10), DEGREE_SIGN); }
                    SL_RotorRpmOnes = V_ControllerRpm / 1000;                                   //Value before decimal point. Division by 1000 because of SCALE_DIV 1000
                    SL_RotorRpmTenths = (V_ControllerRpm % 1000) / 10;                         //Value after decimal point. Division by 10 to have only two numbers after decimal point
                    SL_GridRealPower = V_GridRealPower1Sec / 1000;                              //No rounding, simple whole value. Division by 1000 because of SCALE_DIV 1000
                    U16 Scount = OS_NoOfActiveStatusCodes();
                    if (Scount == 0) { sprintf(SL_StatusTxt, "OK"); }
                    else { snprintf(SL_StatusTxt, sizeof(SL_StatusTxt), "S:%u", Scount); }
                }

                snprintf(V_StatusLineTxt,                                                 //Prints status line. Not used by timer to have possibility
                         sizeof(V_StatusLineTxt),                                        //to update operate state with bigger frequency
                         "%3d%s%c%3d%s%c%2u.%um/s%c%s%c%2u.%urpm%c%4dkW%c%s%c%s",
                         SL_YawDiff,
                         DEGREE_SIGN,
                         SYMBOLS_SEPARATOR,
                         V_NacelleDirection / 10,
                         DEGREE_SIGN,
                         SYMBOLS_SEPARATOR,
                         //Yaw,
                         //SYMBOLS_SEPARATOR,
                         SL_WindSpeedOnes,
                         SL_WindSpeedTenths,
                         SYMBOLS_SEPARATOR,
                         SL_PitchAngleTxt,
                         SYMBOLS_SEPARATOR,
                         SL_RotorRpmOnes,
                         SL_RotorRpmTenths,
                         SYMBOLS_SEPARATOR,
                         SL_GridRealPower,
                         SYMBOLS_SEPARATOR,
                         SL_StatusTxt,
                         LAST_SYMBOL_SEPARATOR,
                         OperationStateTxt
                        );
            }
        }
        //Updates status line variable
        LocalStatus = OS_VarDB_UpdateVariable(V_WP4000StatusText_ID, &V_StatusLineTxt, sizeof(V_StatusLineTxt));
        if (OS_SUCCESS != LocalStatus)
        {
            V_WP4000StatusText_ID = 0;
        }
    }

    return OS_SUCCESS;
}

