static STATUS Initialize()
{
    STATUS LocalStatus;

    LocalStatus = OS_VarDB_GetVariableID("V_WP4000StatusText", &V_WP4000StatusText_ID);     //Reads ID of status line variable
    if (LocalStatus != OS_SUCCESS) { return LocalStatus; }

    StatusUpdateTimer.TimerValue = P_StatusUpdateInterval;                  //Sets timers values to TimeMax to update
    OperateStateUpdateTimer.TimerValue = P_OperateStateUpdateInterval;      //menu lines and status line immediately

    struct os_db_availability* pointer;
    // get pointer(ID) to availability variable
    LocalStatus = OS_VarDB_GetVariableID("AV_Availability", &pointer);
    if (LocalStatus != OS_SUCCESS) { return LocalStatus; }
    // get pointer to availability structure
    Availability = pointer->c.Value.avail;

    return OS_SUCCESS;
}

