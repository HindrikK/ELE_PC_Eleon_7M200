static void PeakDetection(PeakDetector* detector, S32* input, S32* peakMax, S32* peakMin, U32* peakMaxPeriod, U32* peakMinPeriod)
{
    // FAST PEAK DETECTION BASED ON minPeriod
    //------------------------------------------------------
    
    // max peak detection
    if (*input >= detector->peakMax)  // rising
    {
        detector->peakMax = *input;
        detector->peakMax_detTimer = detector->minPeriod;  // delay for edge detection
    }
    else if (detector->peakMax_detTimer > 0)   // falling
    {
        detector->peakMax_detTimer -= detector->timeStep;
        if (detector->peakMax_detTimer <= 0)
        {
            //detector->peakMax = *input;
            detector->peakMax_detected = true;
            detector->peakMin = *input;      // reset previous internal min peak after max peak registered
        }
    }
    
    // min peak detection
    if (*input <= detector->peakMin)  // falling
    {
        detector->peakMin = *input;
        detector->peakMin_detTimer = detector->minPeriod;  // delay for edge detection
    }
    else if (detector->peakMin_detTimer > 0)   // rising
    {
        detector->peakMin_detTimer -= detector->timeStep;
        if (detector->peakMin_detTimer <= 0)
        {
            //detector->peakMin = *input;
            detector->peakMin_detected = true;
            detector->peakMax = *input;      // reset previous internal max peak after min peak registered
        }
    }
    
    
    // DETECTED PEAK OUTPUT BASED ON maxPeriod
    //------------------------------------------------------
    
    // MAX PEAK
    if (detector->peakMax > detector->peakMax_Out)  // rising
    {
        detector->peakMax_Out = detector->peakMax;
        detector->peakMax_period = 0;
        // update external variables
        *peakMax = (S32)detector->peakMax_Out;
    }
    else   // falling
    {
        detector->peakMax_period += detector->timeStep;
        
        // when max period reached
        if (detector->peakMax_period >= detector->maxPeriod)
        {
            // recovering ramp
            detector->peakMax_recoverSpd += detector->peak_recoverSpdStep;
            detector->peakMax_Out-= detector->peakMax_recoverSpd;
            if (detector->peakMax_Out < detector->peakMax)
            {
                detector->peakMax_Out = detector->peakMax;   // prevent recovering lower than peakMax
                detector->peakMax_recoverSpd = 0;
            }
            *peakMax = (S32)detector->peakMax_Out;
            
            // update external variables
            *peakMax = (S32)detector->peakMax_Out;
        }
        
        // also update period output variable value when measured period is greater than previous value
        if (detector->peakMax_period > detector->peakMax_period_Out)
        {
            detector->peakMax_period_Out = detector->peakMax_period;
            *peakMaxPeriod = detector->peakMax_period_Out;      // update external variable
        }
    }
    
    // update period when peak detected
    if (detector->peakMax_detected)
    {
        detector->peakMax_detected = false;
        detector->peakMax_period_Out = detector->peakMax_period;
        *peakMaxPeriod = detector->peakMax_period_Out;      // update external variable
        
        //detector->peakMax_recoverSpd = 0;
        detector->peak_recoverSpdStep = (detector->peakMax_Out - detector->peakMin_Out) * ( (F32)detector->peakRecoverAcc / 1000 ) / (1000 / detector->timeStep);
    }
    
    
    // MIN PEAK
    if (detector->peakMin < detector->peakMin_Out)  // falling
    {
        detector->peakMin_Out = detector->peakMin;
        detector->peakMin_period = 0;
        // update external variables
        *peakMin = (S32)detector->peakMin_Out;
    }
    else   // rising
    {
        detector->peakMin_period += detector->timeStep;
        
        // when max period reached
        if (detector->peakMin_period >= detector->maxPeriod)
        {
            // recovering ramp
            detector->peakMin_recoverSpd += detector->peak_recoverSpdStep;
            detector->peakMin_Out+= detector->peakMin_recoverSpd;
            if (detector->peakMin_Out > detector->peakMin)
            {
                detector->peakMin_Out = detector->peakMin;
                detector->peakMin_recoverSpd = 0;
            }
            *peakMin = (S32)detector->peakMin_Out;
            
            // update external variables
            *peakMin = (S32)detector->peakMin_Out;
        }
        
        // also update period output variable value when measured period is greater than previous value
        if (detector->peakMin_period > detector->peakMin_period_Out)
        {
            detector->peakMin_period_Out = detector->peakMin_period;
            *peakMinPeriod = detector->peakMin_period_Out;      // update external variable
        }
    }
    
    // update period when peak detected
    if (detector->peakMin_detected)
    {
        detector->peakMin_detected = false;
        detector->peakMin_period_Out = detector->peakMin_period;
        *peakMinPeriod = detector->peakMin_period_Out;      // update external variable
        
        //detector->peakMin_recoverSpd = 0;
        detector->peak_recoverSpdStep = (detector->peakMax_Out - detector->peakMin_Out) * ( (F32)detector->peakRecoverAcc / 1000 ) / (1000 / detector->timeStep);
    }
}


