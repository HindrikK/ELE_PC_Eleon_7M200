OS_QUEUE LocalGridSumQueue;

#define MESSAGE_SIZE 4
#define ENTRY_SIZE sizeof(GridSumIO)
#define ENTRIES (ENTRY_SIZE * 2)

const U16 GridSumEntries = ENTRIES;
const U8 GridSumMessageSize = MESSAGE_SIZE;

UNSIGNED GridSumQueue[MESSAGE_SIZE * ENTRIES];
SUBSCRIPTION LocalGridSumSubscription[ENTRY_SIZE];
UNSIGNED GridSumMessage[MESSAGE_SIZE];

STATUS InitGridSum(void)
{
   STATUS ReturnStatus;
   U16 InputCounter = 0;
   U16 OutputCounter = 0;

   AppDebugLog(5000, "Grid Summation task start initiating");

   // Get VarID for input variables
   for ( InputCounter = 0; Inputs[InputCounter].Name; InputCounter++ )
   {
      ReturnStatus = OS_VarDB_GetVariableID(Inputs[InputCounter].Name, &Inputs[InputCounter].Id);
      if(ReturnStatus)
      {
         AppSystemLog(10, "GridSumReadInVar: %d,%d", ReturnStatus, InputCounter);
      }
   }

   // Get VarID for output variables
   for ( OutputCounter = 0; Outputs[OutputCounter].Name; OutputCounter++ )
   {
      ReturnStatus = OS_VarDB_GetVariableID(Outputs[OutputCounter].Name, &Outputs[OutputCounter].Id);
      if(ReturnStatus)
      {
         AppSystemLog(10, "GridSumReadOutVar: %d,%d", ReturnStatus, OutputCounter);
      }
   }

   // Create a queue
   ReturnStatus = OS_Create_Queue(&LocalGridSumQueue,                // Pointer to queue control block
                                  "GridSumQ",                        // 8-charater queue name
                                  GridSumQueue,                      // Start address for queue
                                  GridSumMessageSize*GridSumEntries, // Queue size
                                  OS_FIXED_SIZE,                     // Message type
                                  GridSumMessageSize,                // Message size
                                  OS_FIFO);                          // Suspend type
   if(ReturnStatus)
   {
      AppSystemLog(5000, "GridSum Cr Queue: %d", ReturnStatus);
   }

   // Subscribe to all input variables
   subscriberLocal.queue.queue = &LocalGridSumQueue;
   subscriberLocal.queue.message_size = GridSumMessageSize;
   InputCounter = 0;
   for ( InputCounter = 0; Inputs[InputCounter].Name; InputCounter++ )
   {
      subscriberLocal.queue.reference = InputCounter;
      ReturnStatus = OS_SubHandl_Subscribe(&Inputs[InputCounter].LocalGridSumSubscription,
                                           Inputs[InputCounter].Id,
                                           QUEUE,
                                           subscriberLocal);
      if(ReturnStatus)
      {
         AppSystemLog(5000, "GridSum subscribe variables: %d", ReturnStatus);
      }
   }

   AppDebugLog(5000, "Grid Summation task initiated");
   return OS_SUCCESS;
}

// Task function
static void GridSumTask(UNSIGNED argc, VOID *argv)
{
   STATUS ReturnStatus;
   UNSIGNED ActualMessageSize;

   ReturnStatus = InitGridSum();
   if(ReturnStatus)
   {
      AppDebugLog(0, "Initialiazation of Grid summation failed: %d", ReturnStatus);
   }

   while(1)
   {
      ReturnStatus = OS_Receive_From_Queue(&LocalGridSumQueue,
                                           GridSumMessage,
                                           GridSumMessageSize,
                                           &ActualMessageSize,
                                           OS_SUSPEND);
      if(ReturnStatus)
      {
         AppDebugLog(5000, "GridSum RecFromQueue: %d", ReturnStatus);
      }
      else
      {
         ReturnStatus = GridSummationFunc(GridSumMessage[2]);
         if(ReturnStatus)
         {
            AppDebugLog(5000, "GridSumFunc: %d", ReturnStatus);
         }
      }
   }
}

static STATUS ModuleStatusCodeActive( U8 Failed, U8 Enable )
{
   if ( 0 == Enable )
   {
      return( 0 );
   }

   if ( 0 == Failed )
   {
      return( 0 );
   }

   return( 1 );
}

STATUS GridSummationFunc(U16 InputType)
{
   STATUS Status = 0;
   type_t VarTypeOutput[8] = {TYPE_NONE};
   type_t VarTypeInput[4]  = {TYPE_NONE};
   static U8 init = 1;
   static U8 firsttime[4] = {0};

   // Check that output varables exist
   Status = OS_VarDB_GetVariableType(Outputs[InputType].Id, &VarTypeOutput[InputType]);
   STATUS_LINE_VAR_RETURN_VALUE(Status);
   Status = OS_VarDB_GetVariableType(Outputs[InputType+4].Id, &VarTypeOutput[InputType+4]);
   STATUS_LINE_VAR_RETURN_VALUE(Status);

   // Check that input varables exist
   Status = OS_VarDB_GetVariableType(Inputs[InputType].Id, &VarTypeInput[InputType]);
   STATUS_LINE_VAR_RETURN_VALUE(Status);

   // Initialize output variables
   if(init)
   {
      for(init = 0; init < 4; ++init)
      {
         firsttime[init] = 1;
      }

      init = 0;
   }

   if(firsttime[InputType])
   {
      Status = OS_VarDB_ReadVariable(Outputs[InputType].Id,
                                     &Outputs[InputType].Value,
                                     VarTypeOutput[InputType] & TYPE_LENGTH_MASK,
                                     NULL);
      STATUS_LINE_VAR_RETURN_VALUE(Status);

      Status = OS_VarDB_ReadVariable(Outputs[InputType+4].Id,
                                     &Outputs[InputType+4].Value,
                                     VarTypeOutput[InputType+4] & TYPE_LENGTH_MASK,
                                     NULL);
      STATUS_LINE_VAR_RETURN_VALUE(Status);

      firsttime[InputType] = 0;
   }

   // Read variables from varDB
   Status = OS_VarDB_ReadVariable(Inputs[InputType].Id,
                                  &Inputs[InputType].LocalPowerDelta,
                                  VarTypeInput[InputType] & TYPE_LENGTH_MASK,
                                  NULL);
   STATUS_LINE_VAR_RETURN_VALUE(Status);


   // Actual summation
   //-----------------
   if(!ModuleStatusCodeActive(ModFailed, ModEnable))
   {

      Outputs[InputType+4].Value += Inputs[InputType].LocalPowerDelta; //Add value in [Ws] to local var

      if(Outputs[InputType+4].Value >= 360000)  // if local var is larger than 360000 - more than 0.1 kWh is produced
      {
         Outputs[InputType].Value = (U32)(Outputs[InputType+4].Value / 360000);  // add only the part of production larger than 100Wh

         // Use output in function instead, when event based
         Status = OS_VarDB_UpdateU32(Outputs[InputType].Id, Outputs[InputType].Value);
         STATUS_LINE_VAR_RETURN_VALUE(Status);

         Outputs[InputType+4].Value -= ((U32)(Outputs[InputType].Value * 360000)); // Store remainder (values below 100Wh)
                                                                                   // or in other words: subtract what has already
                                                                                   //  been added to summation
      }
   }

   // Write variable to varDB
   Status = OS_VarDB_UpdateVariableConditional(Outputs[InputType+4].Id,
                                               &Outputs[InputType+4].Value,
                                               VarTypeOutput[InputType+4] & TYPE_LENGTH_MASK);
   STATUS_LINE_VAR_RETURN_VALUE(Status);

   return OS_SUCCESS;
}
