static U8 StatusSetFunc103(void)
{
	   S32 LocalGridL1VectorSurge;
	   S32 LocalGridL2VectorSurge;
	   S32 LocalGridL3VectorSurge;

	   //Function to not set the status code while in Grid connection.

      if(V_GridConnection == 2 || !P_GridEnableVectorSurgeTest)
	   {
		   return 0;
	   }


	   /*Calculate the absolute value of the vector surge inputs*/
	   if ( AI_GridL1VectorSurge < 0 )
	   {
	      LocalGridL1VectorSurge = 0 - AI_GridL1VectorSurge;
	   }
	   else
	   {
	      LocalGridL1VectorSurge = AI_GridL1VectorSurge;
	   }
	   if ( AI_GridL2VectorSurge < 0 )
	   {
	      LocalGridL2VectorSurge = 0 - AI_GridL2VectorSurge;
	   }
	   else
	   {
	      LocalGridL2VectorSurge = AI_GridL2VectorSurge;
	   }
	   if ( AI_GridL3VectorSurge < 0 )
	   {
	      LocalGridL3VectorSurge = 0 - AI_GridL3VectorSurge;
	   }
	   else
	   {
	      LocalGridL3VectorSurge = AI_GridL3VectorSurge;
	   }
	   /*Check the number of phases for which to detect the vector surge.*/
	   switch(P_NumberOfVectorSurgePhases)
	   {
	      case 1 :      // 1 Phase selectet
	      {
	         if (LocalGridL1VectorSurge > P_MaxVectorSurgeDeg ||
	             LocalGridL2VectorSurge > P_MaxVectorSurgeDeg ||
	             LocalGridL3VectorSurge > P_MaxVectorSurgeDeg )
	         {
	            return 1;
	         }
	         break;
	      }
	      case 2 :      // 2 Phases selectet
	      {
	         if ( (LocalGridL1VectorSurge > P_MaxVectorSurgeDeg && LocalGridL2VectorSurge > P_MaxVectorSurgeDeg) ||
	              (LocalGridL1VectorSurge > P_MaxVectorSurgeDeg && LocalGridL3VectorSurge > P_MaxVectorSurgeDeg) ||
	              (LocalGridL2VectorSurge > P_MaxVectorSurgeDeg && LocalGridL3VectorSurge > P_MaxVectorSurgeDeg) )
	         {
	            return 1;
	         }
	         break;
	      }
	      case 3 :      // 3 Phases selectet
	      {
	         if ( LocalGridL1VectorSurge > P_MaxVectorSurgeDeg &&
	              LocalGridL2VectorSurge > P_MaxVectorSurgeDeg &&
	              LocalGridL3VectorSurge > P_MaxVectorSurgeDeg )
	         {
	            return 1;
	         }
	         break;
	      }
	   }
	   return 0;
}

