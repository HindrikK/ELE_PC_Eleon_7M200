//-----------------------------------------------------------------------
S32 pct_to_value(S32 pct, S32 base_value)
{
   return (S32)(((S64)pct * (S64)base_value) / (S64)1000000);
}

//-----------------------------------------------------------------------
S32 value_to_pct(S32 value, S32 base_value)
{
   if (base_value != 0) { return (S32)(((S64)1000000 * (S64)value) / (S64)base_value); }
   else { return 0; }
}


void StatusUpdate(U8 PM_State)
{
   switch (PM_State)
   {
      case 0: //
      {
         strncpy(V_PM_StatusString, V_PM_NoneString_Txt, sizeof(V_PM_NoneString_Txt));
         break;
      }
      case 1: //
      {
         if (V_PM_P_Sp_Local_kW < P_PM_P_Max_kW)
         {
            strncpy(V_PM_StatusString, V_PM_Localkw_Txt, sizeof(V_PM_Localkw_Txt));
         }
         else
         {
            strncpy(V_PM_StatusString, V_PM_NoneString_Txt, sizeof(V_PM_NoneString_Txt));
         }
         break;
      }
      case 2: //
      {
         if (V_PM_P_Sp_Local_kW < P_PM_P_Max_kW)
         {
            strncpy(V_PM_StatusString, V_PM_Localpct_Txt, sizeof(V_PM_Localpct_Txt));
         }
         else
         {
            strncpy(V_PM_StatusString, V_PM_NoneString_Txt, sizeof(V_PM_NoneString_Txt));
         }
         break;
      }
      case 3: //
      {
         if (V_PM_P_Sp_Local_kW < P_PM_P_Max_kW)
         {
            strncpy(V_PM_StatusString, V_PM_Remote_Txt, sizeof(V_PM_Remote_Txt));
         }
         else
         {
            strncpy(V_PM_StatusString, V_PM_NoneString_Txt, sizeof(V_PM_NoneString_Txt));
         }
         break;
      }
      case 4: // Power reduction set point
      {
         strncpy(V_PM_StatusString, V_PM_ReducedSetPointTxt, sizeof(V_PM_ReducedSetPointTxt));
         break;
      }
      case 5: // Frequency drop active
      {
         strncpy(V_PM_StatusString, "Freq. drop active", sizeof("Freq. drop active"));
         break;
      }
      case 6: //2806 High frequency reduction
      {
         strncpy(V_PM_StatusString, "Grid: High Freq.", sizeof("Grid: High Freq."));
         break;
      }
      case 7:
      {
         strncpy(V_PM_StatusString, "Low Limit Active", sizeof("Low Limit Active"));
         UserEvent(EventText_PowerManagementLowLimit);
         break;
      }
      case 8:
      {
         strncpy(V_PM_StatusString, "Ramp After Grid Loss", sizeof("Ramp After Grid Loss"));
         break;
      }
      default:
      {
         break;
      }
   }

   return;
}

