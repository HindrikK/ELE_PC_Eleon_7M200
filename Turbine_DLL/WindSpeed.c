{	
	STATUS LocalStatus = 0;
   
   #if defined DLL_SIMU
   LocalStatus = OS_VarDB_GetVariableID("DLL_IN_HubWindSpeed", &DLL_IN_HubWindSpeed_ID);
   if (LocalStatus != 0)
   {
      return LocalStatus;
   }
   #endif
   
    WindSpeedMax_RateLim = RateLim_New(400/*RateUp*/, 10/*RateDown*/, 1000/*TimeBase*/, WIS_TASK_SCANTIME/*TimeStep*/);
	WindSpeedMin_RateLim = RateLim_New(10/*RateUp*/, 400/*RateDown*/, 1000/*TimeBase*/, WIS_TASK_SCANTIME/*TimeStep*/);
	SpeedVariation_RateLim = RateLim_New(1/*RateUp*/, 1/*RateDown*/, 1000/*TimeBase*/, WIS_TASK_SCANTIME/*TimeStep*/);
	
	WindSpeedAverageInit();
	LoadParameters();
	
	return OS_SUCCESS;
}

