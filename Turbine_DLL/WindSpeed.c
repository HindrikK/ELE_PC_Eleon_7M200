struct  U32Buffer WindSpeedAverBuf;

static void WindSpeedAverageInit(void)
{
   ClearU32Buffer(&WindSpeedAverBuf);
}

static void LoadParameters()
{
	OS_VarDB_ReadVariableByName("P_WIS_SpeedOffset", &P_WIS_SpeedOffset, sizeof(P_WIS_SpeedOffset), NULL);
	OS_VarDB_ReadVariableByName("P_WIS_OffsetPeakSpeed", &P_WIS_OffsetPeakSpeed, sizeof(P_WIS_OffsetPeakSpeed), NULL);
	OS_VarDB_ReadVariableByName("P_WIS_OffsetMaxSpeed", &P_WIS_OffsetMaxSpeed, sizeof(P_WIS_OffsetMaxSpeed), NULL);
	OS_VarDB_ReadVariableByName("P_WIS_SpeedVariationRateUp", &P_WIS_SpeedVariationRateUp, sizeof(P_WIS_SpeedVariationRateUp), NULL);
	OS_VarDB_ReadVariableByName("P_WIS_SpeedVariationRateDown", &P_WIS_SpeedVariationRateDown, sizeof(P_WIS_SpeedVariationRateDown), NULL);
	
	
	// calculate offset gain values
	if(P_WIS_OffsetPeakSpeed != 0)	// prevent dividing by zero
	{
		OffsetGain1 = (F32)P_WIS_SpeedOffset / P_WIS_OffsetPeakSpeed;
	}
	else
	{
		OffsetGain1 = 0;
	}
	if( (P_WIS_OffsetMaxSpeed - P_WIS_OffsetPeakSpeed) != 0)	// prevent dividing by zero
	{
		OffsetGain2 = (F32)P_WIS_SpeedOffset / (P_WIS_OffsetMaxSpeed - P_WIS_OffsetPeakSpeed);
	}
	else
	{
		OffsetGain2 = 0;
	}
	
	RateLim_Set_RateUp(SpeedVariation_RateLim, P_WIS_SpeedVariationRateUp);
	RateLim_Set_RateDown(SpeedVariation_RateLim, P_WIS_SpeedVariationRateDown);
}

