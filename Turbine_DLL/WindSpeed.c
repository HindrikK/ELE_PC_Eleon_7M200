static S16 Get_Offset(U16 WindSpeed)
{
	U16 offset = 0;
	
	if(WindSpeed < P_WIS_OffsetPeakSpeed)
	{
		offset = (S16)(WindSpeed * OffsetGain1);
	}
	else if (WindSpeed < P_WIS_OffsetMaxSpeed)
	{
		offset = (S16)((P_WIS_OffsetMaxSpeed - WindSpeed) * OffsetGain2);
	}
	
	return offset;
}


static STATUS LocalCtrl(void)
{
	STATUS LocalStatus = 0;
	static S16 delayedStartTimer1 = 2000;
	static BOOL delayedStart1 = false;
   
   
   // reloading parameters from menu
	if (V_WIS_LoadParameters)
	{
		V_WIS_LoadParameters = false;
		OS_VarDB_UpdateVariableByName("V_WIS_LoadParameters", &V_WIS_LoadParameters, sizeof(V_WIS_LoadParameters));
		LoadParameters();
		InfoMessage("Parameters reloaded", 1);
	}
	

#if defined DLL_SIMU
   
   OS_VarDB_ReadVariable(DLL_IN_HubWindSpeed_ID, &DLL_IN_HubWindSpeed, sizeof(DLL_IN_HubWindSpeed), NULL);
   V_WindSpeed = DLL_IN_HubWindSpeed * 100;
   
#else
   
   // Wind Speed Sensor Selector
	switch(P_WindSpeedSensorSelect)
	{
		case 0: //AUTO
		{
			if(!V_WindSpeedSensor1Status && !V_WindSpeedSensor2Status)
			{
				strcpy(V_WindSpeedSensorsActive,"All" );
				//V_WindSpeed = (V_WindSpeedSensor1 + V_WindSpeedSensor2) / 2;

				if(V_WindSpeedSensor1 > V_WindSpeedSensor2)
				{
					V_WindSpeed = V_WindSpeedSensor1;
					V_WindSpeedRef = V_WindSpeedSensor1;
				}
				else
				{
					V_WindSpeed = V_WindSpeedSensor2;
					V_WindSpeedRef = V_WindSpeedSensor2;
				}
			}
			else if(!V_WindSpeedSensor1Status && V_WindSpeedSensor2Status)
			{
				strcpy(V_WindSpeedSensorsActive,"Sensor 1" );
				V_WindSpeed = V_WindSpeedSensor1;
				V_WindSpeedRef = V_WindSpeedSensor1;
			}
			else if(V_WindSpeedSensor1Status && !V_WindSpeedSensor2Status)
			{
				strcpy(V_WindSpeedSensorsActive,"Sensor 2" );
				V_WindSpeed = V_WindSpeedSensor2;
				V_WindSpeedRef = V_WindSpeedSensor2;
			}
			else
			{
				strcpy(V_WindSpeedSensorsActive,"None" );
				V_WindSpeed = 0;
				V_WindSpeedRef = 0;
			}
			break;
		}
		case 1: //Sensor 1 selected manually
		{
			if(!V_WindSpeedSensor1Status) //No Errors in sensor 1
			{
				strcpy(V_WindSpeedSensorsActive,"Sensor 1" );
				V_WindSpeed = V_WindSpeedSensor1;
				V_WindSpeedRef = V_WindSpeedSensor1;
			}
			else
			{
				strcpy(V_WindSpeedSensorsActive,"None" );
				V_WindSpeed = 0;
				V_WindSpeedRef = 0;
			}
			break;
		}
		case 2: //Sensor 2 selected manually
		{
			if(!V_WindSpeedSensor2Status)
			{
				strcpy(V_WindSpeedSensorsActive,"Sensor 2" );
				V_WindSpeed = V_WindSpeedSensor2;
				V_WindSpeedRef = V_WindSpeedSensor2;
			}
			else
			{
				strcpy(V_WindSpeedSensorsActive,"None" );
				V_WindSpeed = 0;
				V_WindSpeedRef = 0;
			}
			break;
		}
		default:
		{
			break;
		}
	}

	//Sensor status text update
	if(!V_WindSpeedSensor1Status)
		strcpy(V_WSSensor1StatusText,"Ok" );
	else
		strcpy(V_WSSensor1StatusText,"Error" );

	if(!V_WindSpeedSensor2Status)
		strcpy(V_WSSensor2StatusText,"Ok" );
	else
		strcpy(V_WSSensor2StatusText,"Error" );
      
   // Add wind speed offset
	V_WindSpeed += Get_Offset(V_WindSpeed);
   
#endif
	
	
	
#if defined(WIS_DEBUG) || defined(INCLUDE_DEBUG)
   V_WindSpeed = V_WIS_WindSpeed_Debug;   //DEBUG
#endif
	
	
	LocalStatus = UpdateU32Buffer(&WindSpeedAverBuf , V_WindSpeed);
	STATUS_LINE_VAR_RETURN_CONST(LocalStatus, ERR_3);
	V_WindSpeed1Sec = WindSpeed1s();
  	V_WindSpeed3Sec = WindSpeed3s();
	V_WindSpeed30Sec = WindSpeed30s();
	V_WindSpeed10Min = WindSpeed10m();
	
	
	//**********************************************************************************
	//**********************************************************************************
	// CONTINUE PROGRAM BEYOND THIS POINT ONLY WHEN START DELAY HAS PASSED
	//**********************************************************************************
	//**********************************************************************************
	if (!delayedStart1)
	{
	   	delayedStartTimer1 -= WIS_TASK_SCANTIME;
		if (delayedStartTimer1 > 0) return OS_SUCCESS;
		
		delayedStart1 = true;
		// reset rate limiter structs to have correct values after reboot and not to start from 0
		RateLim_Reset(WindSpeedMax_RateLim, V_WindSpeed/*Output*/);
		RateLim_Reset(WindSpeedMin_RateLim, V_WindSpeed/*Output*/);
	}
	//**********************************************************************************
	//**********************************************************************************
	
	// Get max wind speed value
	V_WIS_WindSpeedMax = RateLim_Control(WindSpeedMax_RateLim, V_WindSpeed3Sec);
	// Get min wind speed value
	V_WIS_WindSpeedMin = RateLim_Control(WindSpeedMin_RateLim, V_WindSpeed3Sec);
	// Get wind speed change value
	if (V_WIS_WindSpeedMax > V_WIS_WindSpeedMin && V_WIS_WindSpeedMin > 0)	// make sure it does not create negative value
	{
	   U16 speedChange = V_WIS_WindSpeedMax - V_WIS_WindSpeedMin;
	   V_WIS_SpeedVariationPct = (U16)(((U32)speedChange * 1000) / (V_WIS_WindSpeedMin + (speedChange / 2)));
	}
	else
	{
	   V_WIS_SpeedVariationPct = 0;
	}
	
	// rate limiter for wind speed variation value
	V_WIS_SpeedVariationPct = RateLim_Control(SpeedVariation_RateLim, V_WIS_SpeedVariationPct);

   return LocalStatus;
}

