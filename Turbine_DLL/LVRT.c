static struct StandardTimerVarStruct LVRTMaxDurationTimer =
{
   0,                               // Timer value [ms]
   &P_LVRT_MaxDurationTime,         // TimeMax value [ms]
   TIMER_20MS,                      // Internal variable, always set to 0
   TIMER_STOP,                      // TIMER_DOWN / TIMER_STOP / TIMER_UP
   0                                // Standard timer called flag.
};
//-----------------------------------------------------------------------
void LVRTControl(void)
{
   if (V_LVRT_Detected)
   {
      V_LVRT_Active = 1;
   }
   
   if (V_LVRT_Active)
   {
      if (FirstTime)
      {
         // Set parameters which currently are not used in control algorithm
         V_LVRT_GeneratorRpmLimit  = LVRT_SavedGeneratorRpmLimit;
         V_LVRT_PitchRPMsetpoint   = LVRT_SavedPitchRPMsetpoint;
         V_LVRT_PitchPowersetpoint = LVRT_SavedPitchPowersetpoint;
         
         // Set pitch parameters, to avoid uninitialized use (maybe we don't need to pitch manually)
         //V_LVRT_PitchMode          = CTRL_AUTO;
         V_LVRT_PitchSpeedLimit    = LVRT_SavedPitchSpeedLimit;
         V_LVRT_PitchPositionLimit = LVRT_SavedPitchPositionLimit;
         
         // Save LVRT start time
         V_LVRT_DropStartTimeStamp = OS_GetTimeInMs();
         OS_VarDB_UpdateVariableByName("V_LVRT_DropStartTimeStamp", &V_LVRT_DropStartTimeStamp, sizeof(V_LVRT_DropStartTimeStamp));
         
         // Clear timer
         V_LVRT_EventTimeOut = 0;
         ResetTimer(&LVRTMaxDurationTimer);
         StartTimer(&LVRTMaxDurationTimer);
         
         // Save trigger log
         if (P_LVRT_TriggerLogEnable)
         {
            OS_TOTLOG_SetTrig();
         }
      }
      
      // Pitch Angle control
      switch (PitchStep)
      {
         case PITCH_BACK:
         {
            // Update time for pitch back in manual mode
            V_LVRT_PitchBackTime = CalculatePichBackTime();
            
            // Pitch towards stop position, if needed
            if ((U32)(OS_GetTimeInMs() - V_LVRT_DropStartTimeStamp) < V_LVRT_PitchBackTime)
            {
               //V_LVRT_PitchMode          = CTRL_MANUAL;
               V_LVRT_PitchSpeedLimit    = P_LVRT_PitchSpeed;
               V_LVRT_PitchPositionLimit = P_PitchStopPosition;
            }
            else
            {
               // Change step if pitch back time ran out or is not needed
               if (V_ControllerRpm < P_LVRT_PitchRecoveryRpm)
               {
                  if (V_LVRT_Detected)
                  {
                     PitchStep = PITCH_HOLD;
                  }
                  else
                  {
                     PitchStep = PITCH_RECOVERY;
                  }
               }
               else
               {
                  PitchStep = PITCH_AUTO;
               }
            }
            
            break;
         }
         case PITCH_HOLD:
         {
            // Hold the blades until LVRT ends
            //V_LVRT_PitchMode          = CTRL_MANUAL;
            V_LVRT_PitchSpeedLimit    = P_LVRT_PitchSpeed;
            V_LVRT_PitchPositionLimit = V_PitchAngle;
            
            // Change step when LVRT ends or we exceed max rpm
            if (V_ControllerRpm < P_LVRT_PitchRecoveryRpm)
            {
               if (!V_LVRT_Detected)
               {
                  PitchStep = PITCH_RECOVERY;
               }
            }
            else
            {
               PitchStep = PITCH_AUTO;
            }
            break;
         }
         case PITCH_RECOVERY:
         {
            // Pitch back towards angle before LVRT
            if (V_PitchAngle - 50 > LVRT_SavedPichAngle && V_ControllerRpm < P_LVRT_PitchRecoveryRpm)
            {              //- 0.5deg
               //V_LVRT_PitchMode          = CTRL_MANUAL;
               V_LVRT_PitchSpeedLimit    = P_LVRT_PitchSpeed;
               V_LVRT_PitchPositionLimit = LVRT_SavedPichAngle;
            }
            else
            {
               // Change step if angle before LVRT is reached,
               // or max rpm is exceeded
               PitchStep = PITCH_AUTO;
            }
            break;
         }
         case PITCH_AUTO:
         {
            // Restore Pitch controller
            //V_LVRT_PitchMode          = CTRL_AUTO;
            V_LVRT_PitchSpeedLimit    = LVRT_SavedPitchSpeedLimit;
            V_LVRT_PitchPositionLimit = LVRT_SavedPitchPositionLimit;
            PitchRecovered            = 1;
            
            break;
         }
      }
      
      // Torque control
      switch (TorqueStep)
      {
         case TORQUE_DROP:
         {
            // Limit torque reference with actual torque
            V_LVRT_TorqueLimit = GetNm(V_Operate_TorqeuDuringLVRT);
            
            // Update display values
            strcpy(V_LVRT_OperateTimerTxt,"Phase Drop");
            V_LVRT_EventTimeOut += 20;//20ms
            
            // Change step if voltage drop is over
            if (!V_LVRT_Detected)
            {
               // Calculate LVRT duration time
               V_LVRT_DropDurationTime  = (U32)(OS_GetTimeInMs() - V_LVRT_DropStartTimeStamp);
               
               // Calculate time left to recover torque together with pitch angle
               if (V_LVRT_PitchBackTime > 0)
               {
                  if (V_LVRT_PitchBackTime + P_LVRT_PitchDelay > V_LVRT_DropDurationTime)
                  {
                     // Duration of drop was less than pitch back time, torque recovery will start earlier than pitch recovery
                     V_LVRT_TorqueRecoveryTimeClaculated = 2 * V_LVRT_PitchBackTime + P_LVRT_PitchDelay - V_LVRT_DropDurationTime;
                  }
                  else
                  {
                     // Duration of drop was bigger than pitch back time, torque recovery will start together with pitch recovery
                     V_LVRT_TorqueRecoveryTimeClaculated = V_LVRT_PitchBackTime + P_LVRT_PitchDelay;
                  }
               }
               else
               {
                  // There was no pitch back, recover torque as fast as possible
                  V_LVRT_TorqueRecoveryTimeClaculated = 1;//1ms
               }
               
               // Calculate recovery rate in % to recover torque together with pitch angle
               LVRT_TorqueRecoveryRatePct_Temp = (LVRT_SavedInverterTorqueAct - V_Operate_TorqeuDuringLVRT) * 1000 / V_LVRT_TorqueRecoveryTimeClaculated;//per 1s
               
               // Fit recovery rate in reasonable range
               if      (LVRT_TorqueRecoveryRatePct_Temp > P_LVRT_TorqueRecoveryRateMax) { V_LVRT_TorqueRecoveryRatePct = P_LVRT_TorqueRecoveryRateMax;    }
               else if (LVRT_TorqueRecoveryRatePct_Temp < P_LVRT_TorqueRecoveryRateMin) { V_LVRT_TorqueRecoveryRatePct = P_LVRT_TorqueRecoveryRateMin;    }
               else                                                                     { V_LVRT_TorqueRecoveryRatePct = LVRT_TorqueRecoveryRatePct_Temp; }
               
               // Calculate recovery rate in Nm
               LVRT_TorqueRecoveryRateNm = GetNm(V_LVRT_TorqueRecoveryRatePct);
               
               // Set timer
               V_LVRT_EventTimeOut = V_LVRT_TorqueRecoveryTimeClaculated;
               V_LVRT_TorqueRecoveryTimeReal = 0;
               
               TorqueStep = TORQUE_RECOVERY;
            }

            break;
         }
         case TORQUE_RECOVERY:
         {
            // Ramp torque limit towards torque table value
            V_LVRT_TorqueLimit += LVRT_TorqueRecoveryRateNm / 50;//per 20ms scan
            
            // Update display values
            strcpy(V_LVRT_OperateTimerTxt,"Power Recovery");
            V_LVRT_EventTimeOut = (V_LVRT_EventTimeOut >= 20) ? V_LVRT_EventTimeOut - 20 : 0;//20ms
            V_LVRT_TorqueRecoveryTimeReal += 20;//20ms
            
            //// Change step if torque table value is reached
            //if (V_LVRT_TorqueLimit > V_TorqueSPNm)
            //{
            //   TorqueStep = TORQUE_AUTO;
            //   
            //   // Remove one obsolete scan
            //   V_LVRT_TorqueRecoveryTimeReal -= 20;//20ms
            //}
         
            break;
         }
         case TORQUE_AUTO:
         {
            // Restore Torque Limit
            V_LVRT_TorqueLimit = LVRT_SavedTorqueLimit;
            TorqueRecovered    = 1;
         }
      }
      
      // LVRT control finishes here
      if (StandardTimer(&LVRTMaxDurationTimer) || (PitchRecovered && TorqueRecovered))
      {
         V_LVRT_Active = 0;
      }
      
      // Clear first time flag
      if (FirstTime)
      {
         FirstTime = 0;
      }
   }
   else
   {
      // Save Actual Torque 60ms before LVRT
      LVRT_SavedInverterTorqueAct  = LVRT_IntermediateTorque_3;  // -60ms
      LVRT_IntermediateTorque_3    = LVRT_IntermediateTorque_2;  // -40ms
      LVRT_IntermediateTorque_2    = LVRT_IntermediateTorque_1;  // -20ms
      LVRT_IntermediateTorque_1    = V_CNV_TorqueActNm;
      
      // Save current operational conditions
      LVRT_SavedPichAngle          = V_PitchAngle;
      LVRT_SavedPitchSpeedLimit    = V_PitchSpeedLimit;
      LVRT_SavedPitchPositionLimit = V_PitchPositionLimit;
      LVRT_SavedTorqueLimit        = V_TorqueLimit;
      LVRT_SavedGeneratorRpmLimit  = V_GeneratorRpmLimit;
      //LVRT_SavedPitchRPMsetpoint   = V_PitchRPMsetpoint;
      //LVRT_SavedPitchPowersetpoint = V_PitchPowersetpoint;
      
      // Clear steps & flags for next LVRT
      if (!FirstTime)
      {
         strcpy(V_LVRT_OperateTimerTxt,"None");
         V_LVRT_EventTimeOut = 0;
         PitchStep           = PITCH_BACK;
         TorqueStep          = TORQUE_DROP;
         PitchRecovered      = 0;
         TorqueRecovered     = 0;
         FirstTime           = 1;
      }
   }
}
//-----------------------------------------------------------------------
static STATUS LocalCtrl(void)
{
   STATUS LocalStatus = 0;
   
   if (P_LVRT_Enable)
   {
      LVRTDetection();
      LVRTSimulation();
      LVRTControl();
   }
   else if (V_LVRT_SimulateLVRT) 
   {
      V_LVRT_SimulateLVRT = 0;
      OS_VarDB_UpdateVariableByName("V_LVRT_SimulateLVRT", &V_LVRT_SimulateLVRT, sizeof(V_LVRT_SimulateLVRT));
   }
   
   return LocalStatus;
} 
