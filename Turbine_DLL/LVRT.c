//-----------------------------------------------------------------------
U32 GetPichBackTimeFromTable(S16 TorqueDrop)
{
   // Check upper & lower limit
   if (TorqueDrop <= PBTT_MIN_TORQUE_DROP)   { return *PitchBackTimesTable[0];                    }      
   if (TorqueDrop >= PBTT_MAX_TORQUE_DROP)   { return *PitchBackTimesTable[PBTT_TIMES_COUNT - 1]; }
   
   // Find in what segment torque drop fits in
   U8 LeftSeg = 0;
   U8 RightSeg = 1;
   for ( ; TorqueDrop > RightSeg * PBTT_TORQUE_DROP_STEP; LeftSeg++, RightSeg++);
   
   // Interpolate time value from the table
   //
   //                       (CurrentDrop - LowerDrop) * (UpperValue - LowerValue)
   //   Equation:   Value = ————————————————————————————————————————————————————— + LowerValue
   //                                       UpperDrop - LowerDrop
   //
   //   LaTeX:   $$ \frac{(CurrentDrop - LowerDrop)\cdot(UpperValue - LowerValue)}{UpperDrop - LowerDrop} + LowerValue $$
   //
   S32 TempTime = 0;
   TempTime = TorqueDrop - LeftSeg * PBTT_TORQUE_DROP_STEP;                                        // (CurrentDrop - LowerDrop)
   TempTime = TempTime * ((S32)*PitchBackTimesTable[RightSeg] - *PitchBackTimesTable[LeftSeg]);    // * (UpperValue - LowerValue)
   TempTime = TempTime / (RightSeg * PBTT_TORQUE_DROP_STEP - LeftSeg * PBTT_TORQUE_DROP_STEP);     // / (UpperDrop - LowerDrop)
   TempTime = TempTime + *PitchBackTimesTable[LeftSeg];                                            // + LowerValue
   
   return (U16)TempTime;
}
//-----------------------------------------------------------------------
U16 GetPitchAngleFactorFromTable(S32 Angle)
{
   // Check upper & lower limit
   if (Angle <= PAFT_MIN_ANGLE)   { return *PitchAngleFactorsTable[0];                      }      
   if (Angle >= PAFT_MAX_ANGLE)   { return *PitchAngleFactorsTable[PAFT_FACTORS_COUNT - 1]; }
   
   // Find in what segment angle fits in
   U8 LeftSeg  = 0;
   U8 RightSeg = 1;
   for ( ; Angle > RightSeg * PAFT_ANGLE_STEP; LeftSeg++, RightSeg++);
   
   // Interpolate from the table
   //
   //                      (CurrentAngle - LowerAngle) * (UpperValue - LowerValue)
   //   Equation:   Value = ——————————————————————————————————————————————————————— + LowerValue
   //                                       UpperAngle - LowerAngle
   //
   //   LaTeX:   $$ \frac{(CurrentAngle - LowerAngle)\cdot(UpperValue - LowerValue)}{UpperAngle - LowerAngle} + LowerValue $$
   //
   S32 TempFactor = 0;
   TempFactor = Angle - LeftSeg * PAFT_ANGLE_STEP;                                                          // (CurrentAngle - LowerAngle)
   TempFactor = TempFactor * ((S32)*PitchAngleFactorsTable[RightSeg] - *PitchAngleFactorsTable[LeftSeg]);   // * (UpperValue - LowerValue)
   TempFactor = TempFactor / (RightSeg * PAFT_ANGLE_STEP - LeftSeg * PAFT_ANGLE_STEP);                      // / (UpperAngle - LowerAngle)
   TempFactor = TempFactor + *PitchAngleFactorsTable[LeftSeg];                                              // + LowerValue
   
   return (U16)TempFactor;
}
//-----------------------------------------------------------------------
U32 CalculatePichBackTime(void)
{
   static U32 PitchBackTime = 0;
   static S32 TorqueDropCounter = 0;
   static S16 TorqueDropCurrentValuePct = 0;
   static S32 TorqueDropAccumulatedValuePct = 0;
   
   if (FirstTime)
   {
      // Get Pitch Angle Factor from table only once
      V_LVRT_PitchAngleFactorFromTable = GetPitchAngleFactorFromTable(LVRT_SavedPichAngle);
      
      // Reset counters
      TorqueDropAccumulatedValuePct = 0;
      TorqueDropCounter = 0;
   }
   
   // Calculate current torque drop value
   TorqueDropCurrentValuePct      = LVRT_SavedInverterTorqueAct - V_Operate_TorqeuDuringLVRT;
   
   // Calculate mean torque drop value
   if (V_LVRT_Detected)
   {
      TorqueDropAccumulatedValuePct += TorqueDropCurrentValuePct;
      TorqueDropCounter++;   
      V_LVRT_TorqueDropMeanValuePct  = TorqueDropAccumulatedValuePct / TorqueDropCounter;   
   }
   
   // Get Pitch Back Time from table based on MEAN torque drop value
   V_LVRT_PitchBackTimeFromTable = GetPichBackTimeFromTable(V_LVRT_TorqueDropMeanValuePct);
   
   // Calculate Pitch Back Time
   PitchBackTime = V_LVRT_PitchBackTimeFromTable * V_LVRT_PitchAngleFactorFromTable / 1000;//100.0%
   return PitchBackTime;
}
