static STATUS LocalCtrl(void)
{
   STATUS LocalStatus = 0;
   static S32 OutdoorTempSensor4Status_old          = S32_MIN;
   static S32 OutdoorAirHumidity_SensorStatus_old   = S32_MIN;
   S32 HumiditySensorErrorLow  = P_Outdoor_AirHumidityInputMin - P_Outdoor_AirHumidityInputErr;
   S32 HumiditySensorErrorHigh = P_Outdoor_AirHumidityInputMax + P_Outdoor_AirHumidityInputErr;

   // Air Humidity
   V_OutdoorAirHumidity_SensorStatus = SensorStatus( P_Outdoor_AirHumidity_Enable,
                                                    (S32)AI_OutdoorAirHumidity,
                                                     HumiditySensorErrorLow,
                                                     HumiditySensorErrorHigh
                                                   );
   if( SENSOR_STATUS_OK == V_OutdoorAirHumidity_SensorStatus )
   {
       V_OutdoorAirHumidity = (U32)LinearLimConvertion( (S32)AI_OutdoorAirHumidity,       //S32 Value
                                                         P_Outdoor_AirHumidityInputMin,   //S32 MinIn
                                                         P_Outdoor_AirHumidityInputMax,   //S32 MaxIn
                                                         P_Outdoor_AirHumidityOutputMin,  //S32 MinOut
                                                         P_Outdoor_AirHumidityOutputMax   //S32 MaxOut
                                                       );
   }
   SensorStatusTxt( V_OutdoorAirHumidity_SensorStatus,
                    &OutdoorAirHumidity_SensorStatus_old,
                    V_OutdoorAirHumidity_StatusText,
                    sizeof(V_OutdoorAirHumidity_StatusText)
                  );

   // Outdoor temp. Sensor 4
   V_OutdoorTempSensor4Status       = SensorStatus( P_OutdoorTempEnable_Sensor4,
                                                      (S32)AI_OutdoorTempSensor4_Curr,
                                                      P_OutdoorTempSensor4InputMin,
                                                      P_OutdoorTempSensor4InputMax );
   if( SENSOR_STATUS_OK == V_OutdoorTempSensor4Status  )
   {
       AI_OutdoorTempSensor4 = (S16)LinearLimConvertion( (S32)AI_OutdoorTempSensor4_Curr,
                                                       P_OutdoorTempSensor4InputMin,
                                                       P_OutdoorTempSensor4InputMax,
                                                       P_OutdoorTempSensorOutputMin,
                                                       P_OutdoorTempSensor4OutputMax
                                                  );
   }

   SensorStatusTxt( V_OutdoorTempSensor4Status,
                    &OutdoorTempSensor4Status_old,
                    V_OutdoorTempSensor4StatusTxt,
                    sizeof(V_OutdoorTempSensor4StatusTxt)
                  );

   V_OutdoorTempSensor3Status        = VerifyLimits((S32)PT_OutdoorTemperature,
                                                    P_Outdoor_PT100_ErrLow,
                                                    P_Outdoor_PT100_ErrHigh);

   // Sensor Selector
   	switch(P_OutdoorTempSensorSelect)
   	{
   		case 0: //AUTO
   		{
   			if(!V_OutdoorTempSensor1Status && !V_OutdoorTempSensor2Status)
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"All" );
   				V_OutDoorTemp = (V_OutdoorTempSensor1 + V_OutdoorTempSensor2) / 2;
   				if(V_OutdoorTempSensor1 > V_OutdoorTempSensor2)
   					V_OutdoorTemperatureRef = V_OutdoorTempSensor1;
   				else
   					V_OutdoorTemperatureRef = V_OutdoorTempSensor2;

   			}
   			else if(!V_OutdoorTempSensor1Status && V_OutdoorTempSensor2Status)
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"Sensor 1" );
   				V_OutDoorTemp = V_OutdoorTempSensor1;
   				V_OutdoorTemperatureRef = V_OutdoorTempSensor1;
   			}
   			else if(V_OutdoorTempSensor1Status && !V_OutdoorTempSensor2Status)
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"Sensor 2" );
   				V_OutDoorTemp = V_OutdoorTempSensor2;
   				V_OutdoorTemperatureRef = V_OutdoorTempSensor2;
   			}
   			else
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"None" );
   				V_OutDoorTemp = 0;
   				V_OutdoorTemperatureRef = 0;
   			}
   			break;
   		}
   		case 1: //Sensor 1 selected manually
   		{
   			if(!V_OutdoorTempSensor1Status) //No Errors in sensor 1
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"Sensor 1" );
   				V_OutDoorTemp = V_OutdoorTempSensor1;
   				V_OutdoorTemperatureRef = V_OutdoorTempSensor1;
   			}
   			else
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"None" );
   				V_OutDoorTemp = 0;
   				V_OutdoorTemperatureRef = 0;
   			}
   			break;
   		}
   		case 2: //Sensor 2 selected manually
   		{
   			if(!V_OutdoorTempSensor2Status) //No Errors in sensor 2
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"Sensor 2" );
   				V_OutDoorTemp = V_OutdoorTempSensor2;
   				V_OutdoorTemperatureRef = V_OutdoorTempSensor2;
   			}
   			else
   			{
   				strcpy(V_OutdoorTempSensorsActiveText,"None" );
   				V_OutDoorTemp = 0;
   				V_OutdoorTemperatureRef = 0;
   			}
   			break;
   		}
   		case 3: //Sensor 3 selected manually
        {
            if(!V_OutdoorTempSensor3Status) //No Errors in sensor 3
            {
                strcpy(V_OutdoorTempSensorsActiveText,"Sensor 3" );
                V_OutDoorTemp = PT_OutdoorTemperature;
                V_OutdoorTemperatureRef = PT_OutdoorTemperature;
            }
            else
            {
                strcpy(V_OutdoorTempSensorsActiveText,"None" );
                V_OutDoorTemp = 0;
                V_OutdoorTemperatureRef = 0;
            }
            break;
        }
   		case 4: //Sensor 4 selected manually
        {
            if(!V_OutdoorTempSensor4Status) //No Errors in sensor 4
            {
                strcpy(V_OutdoorTempSensorsActiveText,"Sensor 4" );
                V_OutDoorTemp = AI_OutdoorTempSensor4;
                V_OutdoorTemperatureRef = AI_OutdoorTempSensor4;
            }
            else
            {
                strcpy(V_OutdoorTempSensorsActiveText,"None" );
                V_OutDoorTemp = 0;
                V_OutdoorTemperatureRef = 0;
            }
            break;
        }
   		default:
   		{
   			break;
   		}
   	}

   	//Sensor status text update
   	if(!V_OutdoorTempSensor1Status)
   		strcpy(V_OutdoorTempSensor1StatusText,"Ok" );
   	else
   		strcpy(V_OutdoorTempSensor1StatusText,"Error" );
   	if(!V_OutdoorTempSensor2Status)
   		strcpy(V_OutdoorTempSensor2StatusText,"Ok" );
   	else
   		strcpy(V_OutdoorTempSensor2StatusText,"Error" );
   	if(!V_OutdoorTempSensor3Status)
        strcpy(V_OutdoorTempSensor3StatusText,"Ok" );
    else
        strcpy(V_OutdoorTempSensor3StatusText,"Error" );


	LocalStatus = UpdateS32Buffer(&OutdoorTempAverBuf , (S32)V_OutDoorTemp);
	STATUS_LINE_VAR_RETURN_CONST(LocalStatus, ERR_3);
	V_OutDoorTemp1Sec = (S16)(OutdoorTemp1s());
	V_OutDoorTemp3Sec = (S16)(OutdoorTemp3s());
	V_OutDoorTemp30Sec = (S16)(OutdoorTemp30s());
	V_OutDoorTemp10Min = (S16)(OutdoorTemp10m());

   return LocalStatus;
}
  
