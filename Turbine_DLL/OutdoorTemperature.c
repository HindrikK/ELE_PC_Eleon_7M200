static S32 LinearConvertion(S32 Value, S32 MinIn, S32 MaxIn, S32 MinOut, S32 MaxOut)
{
    S32 Res;

    if (MaxIn != MinIn) { Res = ((S64)Value - MinIn)*(MaxOut - MinOut)/(MaxIn - MinIn) + MinOut; }
    else                { Res = MinOut; }

    return Res;
}
//-----------------------------------------------------------------------
S32 LinearLimConvertion(S32 Value, S32 MinIn, S32 MaxIn, S32 MinOut, S32 MaxOut)
{
    S32 Res;

    Res = LinearConvertion( Value, MinIn, MaxIn, MinOut, MaxOut );

    if( MinOut <= MaxOut )
    {
        if      ( Res < MinOut )    { Res = MinOut; }
        else if ( Res > MaxOut )    { Res = MaxOut; }
    }
    else
    {
        if      ( Res < MaxOut )    { Res = MaxOut; }
        else if ( Res > MinOut )    { Res = MinOut; }
    }

    return Res;
}
//-----------------------------------------------------------------------
static U8 VerifyLimits(S32 Input, S32 Min, S32 Max)
{
    if( (Input > Max) || (Input < Min ) )
    {
        return SENSOR_STATUS_ERROR;
    }
    else
    {
        return SENSOR_STATUS_OK;
    }
}

static U8 SensorStatus( U8 Enable, S32 Input, S32 Min, S32 Max )
{
    if( !Enable )
    {
        return SENSOR_STATUS_DISABLED;
    }
    else
    {
        return VerifyLimits( Input, Min, Max );
    }
}

static void SensorStatusTxt( S32 Status, S32 *OldStatus, char *Text, S32 Size )
{
    if( Status != *OldStatus )
    {
        switch( Status )
        {
            case SENSOR_STATUS_OK:
            {
                strncpy( Text, "OK", Size );
            }
                break;
            case SENSOR_STATUS_DISABLED:
            {
                strncpy( Text, "Disabled", Size );
            }
                break;
            case SENSOR_STATUS_ERROR:
            {
                strncpy( Text, "Sensor Error", Size );
            }
                break;
            default:
            {
                strncpy( Text, "WARNING", Size );
            }
                break;
        }
    }

    *OldStatus = Status;
}

