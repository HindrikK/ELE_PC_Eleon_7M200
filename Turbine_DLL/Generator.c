// Read a string from file. The string must be between start and end characters.
// Function starts to search for the start character from the position that File Descriptor is currently pointing
// Function keeps looking till the end of the file for the start character.
//
//  file - File Descriptor
//  string - pointer for returned string(NULL terminated)
//  max - max string length(without NULL byte)
//  startChar - character that specifies start of the string
//  endChar - character that specifies end of the string
//
//  Returned value shows the number of characters in string(not counting NULL byte)
//  Negative return value indicates error
//    -1 - start character not found
//    -2 - string reached max length but end character not found
//
// Example:
//   File contains text - "This is <file> body"
//   Function parameters StartChar='<'; EndChar='>'
//   Returned string: "file"
static int File_ReadBetween(S32 file, char* string, S32 max, char startChar, char endChar)
{
    char character;

    while (true)// look for start character
    {
        if (OS_Read(file, &character, 1) == 1)
        {
            if (character == startChar)// found start character
            {
                break;
            }
        }
        else
        {
            // ERROR
            return -1;
        }
    }

    int i = 0;
    while (true)// read string and look for end character
    {
        if (i < max)
        {
            if (OS_Read(file, &character, 1) == 1)
            {
                if (character == endChar)// found end character
                {
                    string[i] = 0;// string end termination
                    return i;
                }
                else
                {
                    string[i] = character;
                }
            }
        }
        else
        {
            string[i] = 0;// string end termination
            // ERROR
            return -2;
        }

        i++;// keep reading string and look for end character
    }
}


#define NUM_OF_RPMFILTER_RPM_POINTS 15
#define NUM_OF_RPMFILTER_DEG_POINTS 36

static S32 FileDescriptor_Rpm;
static char Status_Txt_Rpm[30];
static S16 FilterTable_Rpm[NUM_OF_RPMFILTER_DEG_POINTS][NUM_OF_RPMFILTER_RPM_POINTS];

static void CloseFilterTable_Rpm()
{
    OS_Close(FileDescriptor_Rpm);
    OS_Release_File_User();
}


static void LoadFilterTable_Rpm()
{
    int result;
    char FileDirectory[] = "I:/App/Data/RpmFilter/";
    char FilePath[60];
    char FileName[30];

    // load file name
    time_t TimeStamp;
    result = OS_VarDB_ReadVariableByName("P_GEN_RpmFiltTbl_FileName", &FileName, sizeof(FileName), &TimeStamp);
    if (result < 0)
    {
        // ERROR
        sprintf(Status_Txt_Rpm, "No file name variable(%d)", result);
        InfoMessage(Status_Txt_Rpm, 3);
        CloseFilterTable_Rpm();
        return;
    }
    snprintf(FilePath, sizeof(FilePath) - 1, "%s%s", FileDirectory, FileName);

    // open file
    OS_Become_File_User();
    FileDescriptor_Rpm = OS_Open(&FilePath, PO_RDONLY, 0);
    if (FileDescriptor_Rpm <= 0)
    {
        // ERROR
        P_GEN_RpmFiltTbl_Enable = false;
        OS_VarDB_UpdateVariableByName("P_GEN_RpmFiltTbl_Enable", &P_GEN_RpmFiltTbl_Enable, sizeof(P_GEN_RpmFiltTbl_Enable));
        sprintf(Status_Txt_Rpm, "Error opening file(%d)", FileDescriptor_Rpm);
        InfoMessage(Status_Txt_Rpm, 3);
        CloseFilterTable_Rpm();
        return;
    }

    // read table version
    char string[15];
    result = File_ReadBetween(FileDescriptor_Rpm, &string, sizeof(string) - 1, '<', '>');
    if (result < 1)
    {
        // ERROR
        P_GEN_RpmFiltTbl_Enable = false;
        OS_VarDB_UpdateVariableByName("P_GEN_RpmFiltTbl_Enable", &P_GEN_RpmFiltTbl_Enable, sizeof(P_GEN_RpmFiltTbl_Enable));
        snprintf(Status_Txt_Rpm, sizeof(Status_Txt_Rpm) - 1, "Missing table version(%d)", result);
        InfoMessage(Status_Txt_Rpm, 3);
        CloseFilterTable_Rpm();
        return;
    }

    // check if correct version
    if (strcmp(string, "Ver.1.0"))
    {
        // ERROR
        P_GEN_RpmFiltTbl_Enable = false;
        OS_VarDB_UpdateVariableByName("P_GEN_RpmFiltTbl_Enable", &P_GEN_RpmFiltTbl_Enable, sizeof(P_GEN_RpmFiltTbl_Enable));
        snprintf(Status_Txt_Rpm, sizeof(Status_Txt_Rpm) - 1, "Wrong table: %s", string);
        InfoMessage(Status_Txt_Rpm, 3);
        CloseFilterTable_Rpm();
        return;
    }
    else
    {
        sprintf(Status_Txt_Rpm, "Table loaded");
        InfoMessage(Status_Txt_Rpm, 3);
    }

    // read data from table
    int i;
    int j;
    // reading lines - deg
    for (i = 0; i < NUM_OF_RPMFILTER_DEG_POINTS; i++)
    {
        #ifdef GEN_DEBUG
        OS_com_printf("\n");
        #endif
        // reading columns - rpm
        for (j = 0; j < NUM_OF_RPMFILTER_RPM_POINTS; j++)
        {
            result = File_ReadBetween(FileDescriptor_Rpm, &string, sizeof(string) - 1, '<', '>');// get correction value from file
            if (result < 1)
            {
                // ERROR
                P_GEN_RpmFiltTbl_Enable = false;
                OS_VarDB_UpdateVariableByName("P_GEN_RpmFiltTbl_Enable", &P_GEN_RpmFiltTbl_Enable, sizeof(P_GEN_RpmFiltTbl_Enable));
                snprintf(Status_Txt_Rpm, sizeof(Status_Txt_Rpm) - 1, "Invalid data format(%d, %d)", result, i);
                InfoMessage(Status_Txt_Rpm, 3);
                CloseFilterTable_Rpm();
                return;
            }

            S32 value;
            result = Convert_StringToInt(&string, &value, NULL);
            if(result < 0)
            {
            	// ERROR
                P_GEN_RpmFiltTbl_Enable = false;
                OS_VarDB_UpdateVariableByName("P_GEN_RpmFiltTbl_Enable", &P_GEN_RpmFiltTbl_Enable, sizeof(P_GEN_RpmFiltTbl_Enable));
                snprintf(Status_Txt_Rpm, sizeof(Status_Txt_Rpm) - 1, "Invalid value format(%d, %d)", result, i);
                InfoMessage(Status_Txt_Rpm, 3);
                CloseFilterTable_Rpm();
                return;
            }
            FilterTable_Rpm[i][j] = (S16)value;
            OS_com_printf("<%d> ", value);
        }
    }

    // read table end definition
    result = File_ReadBetween(FileDescriptor_Rpm, &string, sizeof(string) - 1, '<', '>');
    if (result < 1)
    {
        // ERROR
        P_GEN_RpmFiltTbl_Enable = false;
        OS_VarDB_UpdateVariableByName("P_GEN_RpmFiltTbl_Enable", &P_GEN_RpmFiltTbl_Enable, sizeof(P_GEN_RpmFiltTbl_Enable));
        snprintf(Status_Txt_Rpm, sizeof(Status_Txt_Rpm) - 1, "Missing table end(%d)", result);
        InfoMessage(Status_Txt_Rpm, 3);
        CloseFilterTable_Rpm();
        return;
    }

    // check if correct end definition
    if (strcmp(string, "END_OF_TABLE"))
    {
        // ERROR
        P_GEN_RpmFiltTbl_Enable = false;
        OS_VarDB_UpdateVariableByName("P_GEN_RpmFiltTbl_Enable", &P_GEN_RpmFiltTbl_Enable, sizeof(P_GEN_RpmFiltTbl_Enable));
        snprintf(Status_Txt_Rpm, sizeof(Status_Txt_Rpm) - 1, "Invalid end(%s)", string);
        InfoMessage(Status_Txt_Rpm, 3);
        CloseFilterTable_Rpm();
        return;
    }

    CloseFilterTable_Rpm();
}

//read correction value
static F32 GetCorrectionValue_Rpm(F32 rpm)
{
    static U16 indexDeg;
    static U16 indexRpm;
    
    rpm = (U32)(rpm * 100);   // convert to integer value with scaling x100

    // calculate deg index
    if (V_GEN_RotorAzimuth < 0) { indexDeg = 0; }
    else if (V_GEN_RotorAzimuth > 3599)
    {
        indexDeg = NUM_OF_RPMFILTER_DEG_POINTS - 1;
        V_GEN_RotorAzimuth = 3599;
    }
    else { indexDeg = V_GEN_RotorAzimuth / 100; }

    // calculate rpm index
    if (rpm < 0) { indexRpm = 0; }
    else if (rpm > 1400) { indexRpm = NUM_OF_RPMFILTER_RPM_POINTS - 1; }
    else { indexRpm = rpm / 100; }

    return (F32)(FilterTable_Rpm[indexDeg][indexRpm]) / 100;
}


   

static void UpdateEncoder1Rpm(void)
{
    F32 frequency = (F32)(HSI_GEN_EncoderFrequency1 + HSI_GEN_EncoderFrequency2) / 2;
    F32 rpm = frequency / 1000 * 60 / P_GEN_EncoderPulsPerRev;
    V_GEN_EncoderRpm = (U32)((rpm + 0.005) * 100);  // convert and round
}

static void UpdateEncoder2Rpm(void)
{
    F32 frequency = (F32)(HSI_GEN_Encoder2Frequency1 + HSI_GEN_Encoder2Frequency2) / 2;
    F32 rpm = frequency / 1000 * 60 / (ROTOR_ENCODER2_PPR / 4);
    V_GEN_Encoder2Rpm = (U32)((rpm + 0.005) * 100);  // convert and round
}

static void UpdatePulserRpm(void)
{
    F32 rpm = (F32)AI_GEN_PulserFrequency / 1000 * 60 / P_GEN_PulserPulsPerRev;
    V_GEN_PulserRpm = (U32)((rpm + 0.005) * 100);  // convert and round
}

static void UpdateRotorRpm(void)
{
    static F32 rpm_prev = 0;
    F32 rpm;
    
    UpdateEncoder1Rpm();
    UpdateEncoder2Rpm();
    UpdatePulserRpm();
    
    switch (P_SelectControllerRpm)
    {
        case 1:
        {
         rpm = (F32)V_GEN_EncoderRpm / 100;
         break;
        }
        case 2:
        {
         rpm = (F32)V_GEN_PulserRpm / 100;
         break;
        }
        case 3:
        {
         rpm = (F32)V_GEN_Encoder2Rpm / 100;
         break;
        }
        default:
        {
         //rpm = (F32)V_GEN_EncoderRpm / 100;
         break;
        }
    }

    // rpm filter based on rotor position lookup table
    V_ControllerRpm_FiltTbl = rpm + GetCorrectionValue_Rpm(rpm);
    if (P_GEN_RpmFiltTbl_Enable)
    {
        rpm = V_ControllerRpm_FiltTbl;
    }


    // advanced rpm filter
    //****************************************************************************************
    // average of prev and new value
    V_ControllerRpm_Filt = (rpm + rpm_prev) / 2;
    rpm_prev = V_ControllerRpm_Filt;

    // dV/dt limiter
    V_ControllerRpm_Filt = AccLim_Control(RpmAccLimFilter, rpm);
    if(V_ControllerRpm_Filt < 0)
    {
      V_ControllerRpm_Filt = 0;     // prevent negative value because V_ControllerRpm variable is unsigned type
    }

    // prevent the filtered value from lagging the measured value too much
    if(V_ControllerRpm_Filt < (rpm - P_GEN_RpmFilt_DiffLim))
    {
      V_ControllerRpm_Filt = rpm - P_GEN_RpmFilt_DiffLim;
    }
    else if (V_ControllerRpm_Filt > (rpm + P_GEN_RpmFilt_DiffLim))
    {
      V_ControllerRpm_Filt = rpm + P_GEN_RpmFilt_DiffLim;
    }
    //****************************************************************************************

    if(P_GEN_RpmFilt_Enable)
    {
      rpm = V_ControllerRpm_Filt;
    }

    // rpm hysteresis
    if(P_GEN_RpmFilterHysteresis > 0)
    {
        rpm = Hyst_v2_Control(RpmFilter_Hyst, rpm);
    }

    V_ControllerRpm = (U32)((rpm + 0.005) * 100);
}


