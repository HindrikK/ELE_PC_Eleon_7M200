// Receive and handle the lidar data packets
// RETURN:
//    1-SUCCESS; -1-NO DATA; -2-INVALID DATA
static int Telegram_Handler(char* serialBuffer, int buffLen)
{
    S32 numValue = 0;
    int datablockLen;


    // check message min and max length limits
    if (buffLen < 6 || buffLen > 128)
    {
        if (V_LID_RxDebugOut_Enable != 0) { OS_com_printf("BuffLen fail (%d)\n", buffLen); }
        return -2;
    }
    // check start character '$'
    if (serialBuffer[0] != 0x24)
    {
        if (V_LID_RxDebugOut_Enable != 0) { OS_com_printf("Invalid start character (%c)\n", serialBuffer[0]); }
        return -1;  // no start character
    }
    // get message ID number
    if (Convert_StringToInt(&serialBuffer[2], &numValue, 2) < 0)
    {
        if (V_LID_RxDebugOut_Enable != 0) { OS_com_printf("Failed to get message ID (%c%c)\n", serialBuffer[2], serialBuffer[3]); }
        return -2;
    }

    V_LID_TelegramCount++;
    datablockLen = buffLen - 7;
    switch (numValue)
    {
        case 33:
            return Data_Decode_33(&serialBuffer[5], datablockLen);

        case 35:
            return Data_Decode_35(&serialBuffer[5], datablockLen);

        case 36:
            return Data_Decode_36(&serialBuffer[5], datablockLen);
        
        case 37:
            return Data_Decode_37(&serialBuffer[5], datablockLen);

        default:
            if (V_LID_RxDebugOut_Enable != 0) { OS_com_printf("Unknown telegram ID %d", numValue); }
            return -2;
    }
}


