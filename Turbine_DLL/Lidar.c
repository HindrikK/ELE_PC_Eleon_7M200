static STATUS Load_Parameters()
{
    STATUS LocalStatus = 0;

    S16 lidarAngle_tilt;
    S16 beamAngle_vertical;
    S16 beamAngle_horizontal;

    S16 value_S16;
    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_LidarAngle_Tilt", &value_S16, sizeof(value_S16), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    lidarAngle_tilt = value_S16;

    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_BeamAngle_Vertical", &value_S16, sizeof(value_S16), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    beamAngle_vertical = value_S16;

    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_BeamAngle_Horizontal", &value_S16, sizeof(value_S16), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    beamAngle_horizontal = value_S16;

    F32 a = cos(PI / 180 * (beamAngle_vertical - lidarAngle_tilt));
    F32 b = cos(PI / 180 * beamAngle_horizontal);
    cosPhi_ConeHigher = (U16)(a * b * 100);     // x100 for precision

    a = cos(PI / 180 * (beamAngle_vertical + lidarAngle_tilt));
    //b = cos(PI / 180 * beamAngle_horizontal);
    cosPhi_ConeLower = (U16)(a * b * 100);      // x100 for precision

    U8 ChangeAverage_Count;
    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_ChangeAverage_Count", &ChangeAverage_Count, sizeof(ChangeAverage_Count), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    U16 ChangeAverage_Period;
    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_ChangeAverage_Period", &ChangeAverage_Period, sizeof(ChangeAverage_Period), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    LocalStatus = FilterAvg_LoadParameters(Average_speedHigher, ChangeAverage_Count, ChangeAverage_Period);
    if (LocalStatus != 0) { return LocalStatus; }
    LocalStatus = FilterAvg_LoadParameters(Average_speedLower, ChangeAverage_Count, ChangeAverage_Period);
    if (LocalStatus != 0) { return LocalStatus; }

    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_ChangeAverage_PeakUpLimit", &ChangeAverage_PeakUpLimit, sizeof(ChangeAverage_PeakUpLimit), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_ChangeAverage_PeakDownLimit", &ChangeAverage_PeakDownLimit, sizeof(ChangeAverage_PeakDownLimit), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_IdleStateWind", &IdleStateWind, sizeof(IdleStateWind), NULL);
    if (LocalStatus != 0) { return LocalStatus; }
    LocalStatus = OS_VarDB_ReadVariableByName("P_LID_OperateStateWind", &OperateStateWind, sizeof(OperateStateWind), NULL);
    if (LocalStatus != 0) { return LocalStatus; }

    return LocalStatus;
}

