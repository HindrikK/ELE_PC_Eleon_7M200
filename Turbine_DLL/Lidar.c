//***********************************************************************
// STATE : Lidar is switched ON and starting
//***********************************************************************
static void Starting_State(BOOL firstentry)
{
    static U8 step;

    //-----------------------------------------------------------------------
    //ENTRY ACTION
    //-----------------------------------------------------------------------
    if (firstentry)
    {
        step = 0;
        strcpy(V_LID_StatusTxt, "Starting");
        ProcessTimer = 0;
    }


    //-----------------------------------------------------------------------
    //CONTROL
    //-----------------------------------------------------------------------
    Communication_Process();


    //-----------------------------------------------------------------------
    // EXIT CONDITIONS
    //-----------------------------------------------------------------------

    // timeout, if starting takes too long
    ProcessTimer += LID_TASK_SCANTIME;
    if (ProcessTimer > LID_START_TIMEOUT)   // TIMEOUT
    {
        StateFunctionPointer = CommError_State;
        return;
    }

    // wait till lidar has booted and communication is up
    if (CommStatus)
    {
        switch (B_LID_StateControl)
        {
            case LID_STATECONTROL_AUTO:     // decide based on wind speed if we should go to operate or idle state
                if (V_WindSpeed10Min > OperateStateWind)
                {
                    StateFunctionPointer = Operating_State;
                }
                else
                {
                    StateFunctionPointer = Idle_State;
                }
                break;

            case LID_STATECONTROL_OPERATE:
                StateFunctionPointer = Operating_State;
                break;

            case LID_STATECONTROL_IDLE:
                StateFunctionPointer = Idle_State;
                break;
        }
    }
    else if (B_LID_StateControl == LID_STATECONTROL_OFF) // switching OFF
    {
        StateFunctionPointer = PowerOFF_State;
    }
}


