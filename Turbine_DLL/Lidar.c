// All serial communication is handled in this function
static void Communication_Process()
{
    STATUS Status = 0;
    char receivedByte = 0;
    static U32 badTelegramCnt_period = 0;
    static U16 commTimeout_Timer = 0;
    static U16 BytesReceived = 0;
    static BOOL receivingFrame = false;

    //while (GetNumberOfBytesInRxComBuffer_simulate())  //DEBUG
    while (GetNumberOfBytesInRxComBuffer(Com_LidarRx_ID))
    {
        Status = GetDataFromRxComBuffer(Com_LidarRx_ID, &receivedByte);        // get one byte from Rx buffer
        //Status = GetDataFromRxComBuffer_simulator(&receivedByte);  //DEBUG
        if (V_LID_RxDebugOut_Enable != 0)
        {
            if (V_LID_RxDebugOut_Enable == 1) { OS_com_printf("%c", receivedByte); }           // print out received byte as ASCII character to debug port
            else if (V_LID_RxDebugOut_Enable == 2) { OS_com_printf("%2X,", receivedByte); }    // print out received byte in hex format to debug port
        }
        if (Status != 1)
        {
            STATUS_LINE_VAR(Status);
        }
#ifdef LID_DEBUG
        OS_com_printf("%d, %d, %d\n", BytesReceived, receivedByte, receivingFrame);
#endif


        // wait for start character
        if (!receivingFrame && receivedByte == 0x24)
        {
            receivingFrame = true;
            LidarRx_Buffer[0] = receivedByte;
            BytesReceived = 1;
        }
        // keep receiving frame
        else if (receivingFrame)
        {
            LidarRx_Buffer[BytesReceived] = receivedByte;
            BytesReceived++;
            if (BytesReceived >= LID_COM_IN_DATA_SIZE)
            {
                receivingFrame = false;
                if (V_LID_RxDebugOut_Enable != 0) { OS_com_printf("\nInput buffer full"); }

                receivingFrame = false;
                return; // input buffer full
            }

            //look for end character '\n'(10)
            if (receivedByte == 10)
            {
                receivingFrame = false;
                if (V_LID_RxDebugOut_Enable == 2) { OS_com_printf("\n"); }  // put new line after each packet when in HEX debug

                int result = Telegram_Handler(LidarRx_Buffer, BytesReceived);
                if (result == 1)
                {
                    badTelegramCnt_period = 0;
                    commTimeout_Timer = 0;
                    CommStatus = true;
                }
                else if (result < 0)
                {
                    if (V_LID_RxDebugOut_Enable != 0) { OS_com_printf("Windar_Telegram_Handler error (%d)\n", result); }
                    V_LID_BadTelegramCnt++;
                    badTelegramCnt_period++;
                }
            }

            if (BytesToSend > 0 && !receivingFrame)   // sending data to lidar at the moment when FINISHED receiving data
            {
                if (V_LID_RxDebugOut_Enable != 0)
                {
                    OS_com_printf("\nSending(%d): ", BytesToSend);
                    int i;
                    for (i = 0; i < BytesToSend; i++)
                    {
                        if (V_LID_RxDebugOut_Enable == 1) { OS_com_printf("%c", LidarTx_Buffer[i]); }           // print out received byte as ASCII character to debug port
                        else if (V_LID_RxDebugOut_Enable == 2) { OS_com_printf("%2X,", LidarTx_Buffer[i]); }    // print out received byte in hex format to debug port
                    }
                    if (V_LID_RxDebugOut_Enable == 2) { OS_com_printf("\n"); }  // put new line after each packet when in HEX debug
                }
                SendDataToTxComBuffer(Com_LidarTx_ID, LidarTx_Buffer, BytesToSend);     // this function will also automatically write bytes to send count to zero
                BytesToSend = 0;
            }
        }
    }

    if (BytesToSend > 0 && !receivingFrame)   // sending data to lidar at the moment when NOT receiving data (in case no data is received)
    {
        if (V_LID_RxDebugOut_Enable != 0)
        {
            OS_com_printf("\nSending(%d): ", BytesToSend);
            int i;
            for (i = 0; i < BytesToSend; i++)
            {
                if (V_LID_RxDebugOut_Enable == 1) { OS_com_printf("%c", LidarTx_Buffer[i]); }           // print out received byte as ASCII character to debug port
                else if (V_LID_RxDebugOut_Enable == 2) { OS_com_printf("%2X,", LidarTx_Buffer[i]); }    // print out received byte in hex format to debug port
            }
            if (V_LID_RxDebugOut_Enable == 2) { OS_com_printf("\n"); }  // put new line after each packet when in HEX debug
        }
        SendDataToTxComBuffer(Com_LidarTx_ID, LidarTx_Buffer, BytesToSend);     // this function will also automatically write bytes to send count to zero
        BytesToSend = 0;
    }

    commTimeout_Timer += LID_TASK_SCANTIME;

    if (badTelegramCnt_period >= 10 || commTimeout_Timer >= LID_COMM_TIMEOUT)
    {
        CommStatus = false;
    }
}


static void WindSpeedChange_Processing()
{
#define LID_BUFFER_SIZE (17)
#define LID_BUFFER_STEPDISTANCE (6)     // how many meters represents on buffer step

    // WindGustBuffer holds ("LID_BUFFER_SIZE" - 1) number of values, each one represents wind gust location "LID_BUFFER_STEPDISTANCE" meters of distance from turbine,
    // it makes total distance of 96 meters. This is the beam measuring point distance where wind speed is actually measured.
    // When wind gust is detected, it is inserted into buffer from the end.
    // Buffer is periodically shifted. Shifting speed depends on wind speed.
    // From buffer it can be identified when wind gust reaches turbine(buffer index 0)

    static S32 WindGustBuffer[LID_BUFFER_SIZE];
    static U32 BufferShiftTimer = 0;
    static U32 shiftPeriod;

    S32 speedChangeHigher;
    S32 speedChangeLower;

    V_LID_WindSpeedHigher_Avg = FilterAvg_Control(Average_speedHigher, V_LID_WindSpeedHigher);
    V_LID_WindSpeedLower_Avg = FilterAvg_Control(Average_speedLower, V_LID_WindSpeedLower);
    V_LID_WindSpeed_Avg = (V_LID_WindSpeedHigher_Avg + V_LID_WindSpeedLower_Avg) / 2;

    // calculate wind speed change values for high and low beams
    speedChangeHigher = V_LID_WindSpeedHigher - V_LID_WindSpeedHigher_Avg;
    speedChangeLower = V_LID_WindSpeedLower - V_LID_WindSpeedLower_Avg;

    // get one wind speed change value which is biggest of the two
    if (speedChangeHigher > 0)
    {
        if (speedChangeHigher > speedChangeLower) { V_LID_WindSpeedChange = speedChangeHigher; }
        else { V_LID_WindSpeedChange = speedChangeLower; }
    }
    else
    {
        if (speedChangeHigher < speedChangeLower) { V_LID_WindSpeedChange = speedChangeHigher; }
        else { V_LID_WindSpeedChange = speedChangeLower; }
    }

    // handle wind gust buffer shifting; shifting speed(period) depends on average wind speed
    if (V_LID_WindSpeed_Avg > 0)    // nos hifting when wind average wind speed 0
    {
        BufferShiftTimer += LID_TASK_SCANTIME;
        shiftPeriod = (LID_BUFFER_STEPDISTANCE * 10000) / V_LID_WindSpeed_Avg;
        if (BufferShiftTimer >= shiftPeriod)
        {
            BufferShiftTimer = 0;

            int i;
            for (i = 0; i < LID_BUFFER_SIZE - 1; i++)
            {
                WindGustBuffer[i] = WindGustBuffer[i + 1];
                V_LID_WindGustVisu_Txt[i] = V_LID_WindGustVisu_Txt[i + 1];
            }
            WindGustBuffer[LID_BUFFER_SIZE - 1] = 0;            // new inserted value remains 0 when no wind gust exists, otherwise it will be replaced by actual detected Gust value
            V_LID_WindGustVisu_Txt[LID_BUFFER_SIZE - 1] = '_';

            // check for limits
            if (V_LID_WindSpeedChange > ChangeAverage_PeakUpLimit || V_LID_WindSpeedChange < -ChangeAverage_PeakDownLimit)
            {
                // add new wind gust into buffer
                WindGustBuffer[LID_BUFFER_SIZE - 1] = V_LID_WindSpeedChange;
                if (V_LID_WindSpeedChange > 0)
                {
                    V_LID_WindGustVisu_Txt[LID_BUFFER_SIZE - 1] = '+';
                }
                else
                {
                    V_LID_WindGustVisu_Txt[LID_BUFFER_SIZE - 1] = '-';
                }
            }
        }
    }
}


// Analyze the received wind speed info and as a result get two values - one for higher and one lower beams.
// Fill in missing data when other beam in same height has value.
static void WindSpeed_Processing()
{
    // get resulting value for HIGHER BEAMS
    if (V_LID_WindSpeed_HL_Raw == 0)
    {
        if (V_LID_WindSpeed_HR_Raw == 0) { return; }                    // other beam is also 0, so don't update value
        else { V_LID_WindSpeedHigher = V_LID_WindSpeed_HR_Raw / 10; }   // other beam is not 0, so use this value
    }
    else if (V_LID_WindSpeed_HR_Raw == 0)
    {
        V_LID_WindSpeedHigher = V_LID_WindSpeed_HL_Raw / 10;        // other beam is not 0, so use this value
    }
    else
    {
        // get highest of both beams
        if (V_LID_WindSpeed_HL_Raw > V_LID_WindSpeed_HR_Raw) { V_LID_WindSpeedHigher = V_LID_WindSpeed_HL_Raw / 10; }
        else { V_LID_WindSpeedHigher = V_LID_WindSpeed_HR_Raw / 10; }
        // get average from: left and right beam and 2 x higher speed beam value
        V_LID_WindSpeedHigher = (V_LID_WindSpeed_HR_Raw / 10 + V_LID_WindSpeed_HL_Raw / 10 + V_LID_WindSpeedHigher + V_LID_WindSpeedHigher) / 4;
    }

    // get resulting value for LOWER BEAMS
    if (V_LID_WindSpeed_LR_Raw == 0)
    {
        if (V_LID_WindSpeed_LL_Raw == 0) { return; }                    // other beam is also 0, so don't update value
        else { V_LID_WindSpeedLower = V_LID_WindSpeed_LL_Raw / 10; }    // other beam is not 0, so use this value
    }
    else if (V_LID_WindSpeed_LL_Raw == 0)
    {
        V_LID_WindSpeedLower = V_LID_WindSpeed_LR_Raw / 10;         // other beam is not 0, so use this value
    }
    else
    {
        // get higher of both beams
        if (V_LID_WindSpeed_LR_Raw > V_LID_WindSpeed_LL_Raw) { V_LID_WindSpeedLower = V_LID_WindSpeed_LR_Raw / 10; }
        else { V_LID_WindSpeedLower = V_LID_WindSpeed_LL_Raw / 10; }
        // get average from: left and right beam and 2 x higher speed beam value
        V_LID_WindSpeedLower = (V_LID_WindSpeed_LR_Raw / 10 + V_LID_WindSpeed_LL_Raw / 10 + V_LID_WindSpeedLower + V_LID_WindSpeedLower) / 4;
    }

    // calculate real wind speed based on beam angles
    V_LID_WindSpeedHigher = (V_LID_WindSpeedHigher * 100) / cosPhi_ConeHigher;
    V_LID_WindSpeedLower = (V_LID_WindSpeedLower * 100) / cosPhi_ConeLower;
}


