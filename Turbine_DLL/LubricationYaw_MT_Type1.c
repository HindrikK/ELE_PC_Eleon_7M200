void YawLubricationPeriodicSimple(U8 NewModeFlag)
{
static U8 FirstTime = 1;
static U8 State = YAW_LUB_AUTO_OFF;

    if( FirstTime )
    {
        FirstTime = 0;
        State = YAW_LUB_AUTO_OFF;
        if( (B_YawLubricationTimer > 0) && (B_YawLubricationTimer < P_LubricationYaw_PumpOffTime) )
        {
            TimerSet(&YawLubrication_OffTimer, B_YawLubricationTimer);
            TimerStart(&YawLubrication_OffTimer);
        }
        else
        {
            TimerReset(&YawLubrication_OffTimer);
        }
    }

    if( NewModeFlag )
    {
        TimerReset(&YawLubrication_OffTimer);
        State = YAW_LUB_AUTO_OFF;
    }

    if( BlockYawLubrication() )
    {
        DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
        strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
        if( YAW_LUB_AUTO_ON == State ) { StopCycle(&LubricationY_StrokeCtrl); }
        V_Lub_Equip2_State = STATE_BLOCKED;
        State = YAW_LUB_AUTO_OFF;
    }
    else
    {
        switch( State )
        {
        case YAW_LUB_AUTO_OFF:
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            V_Lub_Equip2_State = STATE_AUTO_OFF;
            if( TimerCheck(&YawLubrication_OffTimer) )
            {
                B_YawLubricationTimer = 0;
                if( MANAGER_MODE_ENABLE == V_Lub_Equip2_Mode )
                {
                    ResetCycle(&LubricationY_StrokeCtrl, DI_LubricationYaw_PumpFault);
                    StartCycle(&LubricationY_StrokeCtrl);

                    State = YAW_LUB_AUTO_ON;
                }
                else
                {
                    StopCycle(&LubricationY_StrokeCtrl);
                    State = YAW_LUB_SUSPENDED;
                }
            }
            else
            {
                TimeToText(TimerLeft(&YawLubrication_OffTimer), V_LubricationYaw_TimeIndicationTxt, sizeof(V_LubricationYaw_TimeIndicationTxt));
                B_YawLubricationTimer = TimerLeft(&YawLubrication_OffTimer);  //  Backup timer
            }
            break;

        case YAW_LUB_SUSPENDED:
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            V_Lub_Equip2_State = STATE_SUSPENDED;
            strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
            if( (MANAGER_MODE_ENABLE == V_Lub_Equip2_Mode) && (0 == V_Inverter_StatusLVRT) )
            {
                StartCycle(&LubricationY_StrokeCtrl);
                State = YAW_LUB_AUTO_ON;
            }
            if ( V_Inverter_StatusLVRT )                                                { V_Lub_Equip2_Status = YAW_LUB_STATUS_LVRT; }
            else if (MANAGER_MODE_DISABLE == V_Lub_Equip2_Mode)                               { V_Lub_Equip2_Status = YAW_LUB_STATUS_LM_BLOCK; }
            else                                                                        { V_Lub_Equip2_Status = YAW_LUB_STATUS_OK; }

            break;

        case YAW_LUB_AUTO_ON:
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_ON;
            V_Lub_Equip2_State = STATE_AUTO_ON;

            if( WaitCycle(DI_LubricationYaw_PumpFault, P_LubricationYaw_Stroke_ON, &LubricationY_StrokeCtrl) )
            {
                TimerReset(&YawLubrication_OffTimer);
                State = YAW_LUB_AUTO_OFF;
            }
            else if(( MANAGER_MODE_DISABLE == V_Lub_Equip2_Mode) || V_Inverter_StatusLVRT)
            {
                DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
                StopCycle(&LubricationY_StrokeCtrl);
                State = YAW_LUB_SUSPENDED;
            }

            break;

        default:
            YawLubrication_Exception(Ex_YAW_LUB_LOST_MODE);
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            break;
        }
    }
}
