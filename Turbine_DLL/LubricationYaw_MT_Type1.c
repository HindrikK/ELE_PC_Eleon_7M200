void YawLubricationAutoCtrl(U8 ServiceModeChanged)
{
static U8 InternalModeOldValue = 0;
static U8 FirstTime = 1;
U8 NewModeFlag = 0;

    if(FirstTime)
    {
        FirstTime = 0;
        InternalModeOldValue = P_LubricationYaw_InternalMode;
    }
    if(InternalModeOldValue != P_LubricationYaw_InternalMode)
    {
        InternalModeOldValue = P_LubricationYaw_InternalMode;
        NewModeFlag = 1;
    }
    if( ServiceModeChanged ) { NewModeFlag = 1; }

    switch( P_LubricationYaw_InternalMode )
    {
    case LUB_MODE_PERIODIC_EXTRA_CTRL:
        YawLubricationPeriodicExtraCtrl(NewModeFlag);
        break;
    case LUB_MODE_PERIODIC_SIMPLE:
        YawLubricationPeriodicSimple(NewModeFlag);
        break;
    case LUB_MODE_ALWAYS_ON:
        YawLubricationAlwaysOn(NewModeFlag);
        break;
    default:
        DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
        strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
        YawLubrication_Exception(Ex_YAW_LUB_NUMBER_TYPE);
        V_Lub_Equip2_State = STATE_AUTO_OFF;
        break;
    }
}

