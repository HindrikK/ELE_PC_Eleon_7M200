void YawLubricationPeriodicExtraCtrl(U8 NewModeFlag)
{
static U8 FirstTime = 1;
static U8 State = YAW_LUB_AUTO_OFF;
static U8 V_YawCommand_Start;

    if( FirstTime )
    {
        FirstTime = 0;
        State = YAW_LUB_AUTO_OFF;
        if( (B_YawLubricationTimer > 0) && (B_YawLubricationTimer < P_LubricationYaw_PumpOffTime) )
        {
            TimerSet(&YawLubrication_OffTimer, B_YawLubricationTimer);
            TimerStart(&YawLubrication_OffTimer);
        }
        else
        {
            TimerReset(&YawLubrication_OffTimer);
        }
    }

    if( NewModeFlag )
    {
        TimerReset(&YawLubrication_OffTimer);
        State = YAW_LUB_AUTO_OFF;
    }

    if( BlockYawLubrication() )
    {
        DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
        strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
        V_Lub_Equip2_State = STATE_BLOCKED;
        State = YAW_LUB_AUTO_OFF;
    }
    else
    {
        switch( State )
        {
        case YAW_LUB_AUTO_OFF:
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            V_Lub_Equip2_State = STATE_AUTO_OFF;
            if( (( YAW_LUB_YAW_UNWIND_CW == V_YAW_State ) || ( YAW_LUB_YAW_UNWIND_CCW == V_YAW_State )) &&
                ( MANAGER_MODE_ENABLE == V_Lub_Equip2_Mode ) )
            {
                ResetCycle(&LubricationY_StrokeCtrl, DI_LubricationYaw_PumpFault);
				StartCycle(&LubricationY_StrokeCtrl);

                TimerReset(&YawLubrication_UntwistOffTimer);
                B_YawLubricationTimer = 0;

                V_YawCommand_Start = V_YAW_State;          //  to remember should it be lubrication during unwinding or just one-time on.
                DO_LubricationYaw_Pump = YAW_LUB_PUMP_ON;
                State = YAW_LUB_AUTO_ON;
            }
            if( TimerCheck(&YawLubrication_OffTimer) )
            {
                B_YawLubricationTimer = 0;
                if( (( YAW_LUB_YAW_RUN_CW == V_YAW_State ) || ( YAW_LUB_YAW_RUN_CCW == V_YAW_State )) &&
                    ( MANAGER_MODE_ENABLE == V_Lub_Equip2_Mode ) )
                {
                    ResetCycle(&LubricationY_StrokeCtrl, DI_LubricationYaw_PumpFault);
                    StartCycle(&LubricationY_StrokeCtrl);

                    V_YawCommand_Start = V_YAW_State;      //  to remember should it be lubrication during unwinding or just one-time on.
                    State = YAW_LUB_AUTO_ON;
                }
                else
                {
                    StopCycle(&LubricationY_StrokeCtrl);

                    DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
                    State = YAW_LUB_SUSPENDED;
                }
            }
            else
            {
                TimeToText(TimerLeft(&YawLubrication_OffTimer), V_LubricationYaw_TimeIndicationTxt, sizeof(V_LubricationYaw_TimeIndicationTxt));
                B_YawLubricationTimer = TimerLeft(&YawLubrication_OffTimer);  //  Backup timer
            }
            break;

        case YAW_LUB_SUSPENDED:
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            V_Lub_Equip2_State = STATE_SUSPENDED;

            strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));

            if( ( MANAGER_MODE_ENABLE == V_Lub_Equip2_Mode ) && ( 0 == V_Inverter_StatusLVRT ) &&
                ( ( YAW_LUB_YAW_UNWIND_CW == V_YAW_State ) || ( YAW_LUB_YAW_UNWIND_CCW == V_YAW_State ) ||
                  ( YAW_LUB_YAW_RUN_CW == V_YAW_State ) || ( YAW_LUB_YAW_RUN_CCW == V_YAW_State ) ) )
            {
                StartCycle(&LubricationY_StrokeCtrl);

                TimerStart(&YawLubrication_UntwistOffTimer);

                V_YawCommand_Start = V_YAW_State;      //  to remember should it be lubrication during unwinding or just one-time on.

                DO_LubricationYaw_Pump = YAW_LUB_PUMP_ON;
                State = YAW_LUB_AUTO_ON;
            }
            if ( V_Inverter_StatusLVRT )                                                { V_Lub_Equip2_Status = YAW_LUB_STATUS_LVRT; }
            else if( MANAGER_MODE_DISABLE == V_Lub_Equip2_Mode )                              { V_Lub_Equip2_Status = YAW_LUB_STATUS_LM_BLOCK; }
            else if( ( YAW_LUB_YAW_UNWIND_CW != V_YAW_State ) &&
                     ( YAW_LUB_YAW_UNWIND_CCW != V_YAW_State ) &&
                     ( YAW_LUB_YAW_RUN_CW != V_YAW_State ) &&
                     ( YAW_LUB_YAW_RUN_CCW != V_YAW_State ) )                          { V_Lub_Equip2_Status = YAW_LUB_STATUS_YAW_ACTIVITY; }
            else                                                                        { V_Lub_Equip2_Status = YAW_LUB_STATUS_OK; }

            break;

        case YAW_LUB_AUTO_ON:
            if( ( YAW_LUB_YAW_UNWIND_CW == V_YawCommand_Start ) || ( YAW_LUB_YAW_UNWIND_CCW == V_YawCommand_Start ) )   //  Yaw Unwind procedure is active
            {
                if( ( YAW_LUB_YAW_UNWIND_CW != V_YAW_State ) && ( YAW_LUB_YAW_UNWIND_CCW != V_YAW_State ) )   //  return if Unwind procedure is not active any more
                {
                    TimerReset(&YawLubrication_OffTimer);

                    DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
                    State = YAW_LUB_AUTO_OFF;
                }
                else        //  Unwind is still running
                {
                    if(( MANAGER_MODE_DISABLE == V_Lub_Equip2_Mode) || V_Inverter_StatusLVRT)     //  Pump must be turned off immediately
                    {
                        if( YAW_LUB_PUMP_OFF == DO_LubricationYaw_Pump ) { TimerStop(&YawLubrication_UntwistOffTimer); }

                        StopCycle(&LubricationY_StrokeCtrl);

                        DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
                        State = YAW_LUB_SUSPENDED;
                    }
                    else        //  turn pump On/Off periodically
                    {
                        if( YAW_LUB_PUMP_ON == DO_LubricationYaw_Pump )
                        {
                            V_Lub_Equip2_State = STATE_AUTO_ON;
                            if( WaitCycle(DI_LubricationYaw_PumpFault, P_LubricationYawUnwinding_Stroke_ON, &LubricationY_StrokeCtrl) )
                            {
                                TimerReset(&YawLubrication_UntwistOffTimer);
                                TimerStart(&YawLubrication_UntwistOffTimer);

                                DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
                            }
                        }
                        else
                        {
                            V_Lub_Equip2_State = STATE_AUTO_OFF;
                            TimeToText(TimerLeft(&YawLubrication_UntwistOffTimer), V_LubricationYaw_TimeIndicationTxt, sizeof(V_LubricationYaw_TimeIndicationTxt));
                            if(TimerCheck(&YawLubrication_UntwistOffTimer))
                            {
                                ResetCycle(&LubricationY_StrokeCtrl, DI_LubricationYaw_PumpFault);
                                StartCycle(&LubricationY_StrokeCtrl);

                                DO_LubricationYaw_Pump = YAW_LUB_PUMP_ON;
                            }
                        }
                    }
                }
            }
            else            //  Normal Yaw run is active (or was active when entered this state)
            {
                V_Lub_Equip2_State = STATE_AUTO_ON;
                //  switch to unwinding mode if it started during normal yawing or just after return from suspended state
                if( (( YAW_LUB_YAW_UNWIND_CW == V_YAW_State ) || ( YAW_LUB_YAW_UNWIND_CCW == V_YAW_State )) &&
                    ( MANAGER_MODE_ENABLE == V_Lub_Equip2_Mode ) )
                {
                    ResetCycle(&LubricationY_StrokeCtrl, DI_LubricationYaw_PumpFault);
                    StartCycle(&LubricationY_StrokeCtrl);

                    TimerReset(&YawLubrication_UntwistOffTimer);
                    B_YawLubricationTimer = 0;
                    V_YawCommand_Start = V_YAW_State;          //  to remember should it be lubrication during unwinding or just one-time on.
                    DO_LubricationYaw_Pump = YAW_LUB_PUMP_ON;
                    State = YAW_LUB_AUTO_ON;
                }
                //  lubrication during normal yawing
                if( WaitCycle(DI_LubricationYaw_PumpFault, P_LubricationYaw_Stroke_ON, &LubricationY_StrokeCtrl) )
                {
                    TimerReset(&YawLubrication_OffTimer);
                    State = YAW_LUB_AUTO_OFF;
                }
                else
                {
                    if(( MANAGER_MODE_DISABLE == V_Lub_Equip2_Mode) || V_Inverter_StatusLVRT)
                    {
                        DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
                        StopCycle(&LubricationY_StrokeCtrl);
                        State = YAW_LUB_SUSPENDED;
                    }
                    else
                    {
                        DO_LubricationYaw_Pump = YAW_LUB_PUMP_ON;
                    }
                }
            }
            break;

        default:
            YawLubrication_Exception(Ex_YAW_LUB_LOST_MODE);
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            break;
        }
    }
}
