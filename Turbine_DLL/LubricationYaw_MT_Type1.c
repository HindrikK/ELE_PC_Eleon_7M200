static STATUS LocalCtrl(void)
{
    STATUS LocalStatus = 0;
    static U8 FirstTime = 1;
    U8 ServiceModeChanged = 0;
    static U8 P_LubricationYaw_ServiceCtrl_old;


    if(FirstTime)
    {
        AssignParameters();
        FirstTime = 0;
        strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
        P_LubricationYaw_ServiceCtrl_old = P_LubricationYaw_ServiceCtrl;
    }

    if(P_LubricationYaw_ServiceCtrl_old != P_LubricationYaw_ServiceCtrl)
    {
        P_LubricationYaw_ServiceCtrl_old = P_LubricationYaw_ServiceCtrl;
        ServiceModeChanged = 1;
    }

    if( BlockYawLubrication() ) { V_Lub_Equip2_State = STATE_BLOCKED; }        //Retrieve Status

    switch( P_LubricationYaw_ServiceCtrl )
    {
        case MODE_AUTO :
            YawLubricationAutoCtrl(ServiceModeChanged);
            break;

        case MODE_SERVICE_OFF :
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            V_Lub_Equip2_State = STATE_SERVICE_OFF;
            V_Lub_Equip2_Status = YAW_LUB_STATUS_OK;
            strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
            break;

        case MODE_SERVICE_ON :
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_ON;
            V_Lub_Equip2_State = STATE_SERVICE_ON;
            V_Lub_Equip2_Status = YAW_LUB_STATUS_OK;
            strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
            break;

        default:
            DO_LubricationYaw_Pump = YAW_LUB_PUMP_OFF;
            YawLubrication_Exception(Ex_YAW_LUB_NUMBER_MODE);
            V_Lub_Equip2_State = STATE_SERVICE_OFF;
            V_Lub_Equip2_Status = YAW_LUB_STATUS_OK;
            strncpy(V_LubricationYaw_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationYaw_TimeIndicationTxt));
            break;
    }

    AssignParameters();

    // update object State and Status and write to VDB if updated
    WriteState();
    WriteStatus();

   return LocalStatus;
}

