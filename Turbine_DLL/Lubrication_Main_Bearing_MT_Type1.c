static void TimeToText(U64 Time, char *Text, U8 size)
{
    Time = Time / 1000; //  result in seconds
    if(Time > 3600)
    {
        sprintf( Text, "%u h, %d min",
                 (U32)(Time / 3600), (U32)((Time % 3600) / 60));
    }
    else if((Time < 3600) && (Time > 60))
    {
        snprintf( Text, size, "%u min, %u sec",
                  (U32)(Time / 60), (U32)(Time % 60));
    }
    else if(Time < 60)
    {
        snprintf(Text, size, "%u sec", (U32)Time);
    }
}
