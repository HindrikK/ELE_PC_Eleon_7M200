//________________________________________________________________________
static void TimerReset(STimer_t *pTimer)        //  Reset all values to default and start timer automatically
{
    pTimer->TimeStart = OS_GetTimeInMs();
    pTimer->Backup    = 0;
    pTimer->OStatus   = 0;
    pTimer->Suspend   = 0;
    pTimer->FirstRun  = 1;
    pTimer->Start     = 1;
}
//________________________________________________________________________
static time_t TimerLeft(STimer_t *pTimer)
{
time_t  TimeDiff = 0;

    if( !TimerCheck(pTimer) )
    {
        TimeDiff = OS_GetTimeInMs();    //  time can change within two following commands, so it is needed to save time at some moment of time to temporary variable
        return (pTimer->P_Time > ( TimeDiff - pTimer->TimeStart )) ? (pTimer->P_Time - ( TimeDiff - pTimer->TimeStart )) : 0;
    }
    else
    {
        return 0;
    }
}
//________________________________________________________________________
static void TimerSet(STimer_t *pTimer, time_t Delay)        //  Timer can be restored from backup by this function
{
    if(Delay < pTimer->P_Time) { pTimer->TimeStart = OS_GetTimeInMs() -  pTimer->P_Time + Delay; }
    else                       { pTimer->TimeStart = OS_GetTimeInMs(); }
}
//________________________________________________________________________
static BOOL TimerCheck(STimer_t *pTimer)
{
    if( pTimer->Start && !pTimer->OStatus )
    {
        pTimer->TimeLast = OS_GetTimeInMs() - pTimer->TimeStart;
        if ( pTimer->TimeLast >= pTimer->P_Time )
        {
            pTimer->TimeStart = 0;
            pTimer->OStatus   = 1;
        }
    }
    return pTimer->OStatus;
}
//________________________________________________________________________
static void TimerStop(STimer_t *pTimer)         //  Backup current time and stop timer
{
    if( 0 == pTimer->Suspend ) { pTimer->Backup = TimerLeft(pTimer); }
    pTimer->Start = 0;
    pTimer->Suspend = 1;
}
//________________________________________________________________________
static void TimerStart(STimer_t *pTimer)        //  Start timer if it was stopped before or was not started/resetted
{
    if( pTimer->Suspend )                       //  timer was stopped previously, so reinitialize timer from backup and start it
    {
        TimerSet(pTimer, pTimer->Backup);
        pTimer->Suspend = 0;
        pTimer->Backup  = 0;
        pTimer->OStatus = 0;
        pTimer->Start   = 1;
    }
    else
    {
        pTimer->OStatus   = 0;
        pTimer->Suspend   = 0;
        pTimer->Backup    = 0;
        pTimer->FirstRun  = 1;
        pTimer->Start     = 1;
    }
}
