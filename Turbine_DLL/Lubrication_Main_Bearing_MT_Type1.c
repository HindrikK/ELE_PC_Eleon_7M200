void MBLubricationPeriodicExtraCtrl(U8 NewModeFlag)
{
static U8 FirstTime = 1;
static U8 State = MB_LUB_AUTO_OFF;

    if( FirstTime )
    {
        FirstTime = 0;
        State = MB_LUB_AUTO_OFF;
        if( (B_MBLubricationTimer > 0) && (B_MBLubricationTimer < P_LubricationMB_PumpOffTime) )
        {
            TimerSet(&MBLubrication_OffTimer, B_MBLubricationTimer);
            TimerStart(&MBLubrication_OffTimer);
        }
        else
        {
            TimerReset(&MBLubrication_OffTimer);
        }
    }

    if( NewModeFlag )
    {
        TimerReset(&MBLubrication_OffTimer);
        State = MB_LUB_AUTO_OFF;
    }

    if( BlockMBLubrication() )
    {
        DO_LubricationMB_Pump = MB_LUB_PUMP_OFF;
        strncpy(V_LubricationMB_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationMB_TimeIndicationTxt));
        V_Lub_Equip1_State = STATE_BLOCKED;
    }
    else
    {
        switch( State )
        {
            case MB_LUB_AUTO_OFF:
            {
                DO_LubricationMB_Pump = MB_LUB_PUMP_OFF;
                V_Lub_Equip1_State = STATE_AUTO_OFF;
                if( TimerCheck(&MBLubrication_OffTimer) )
                {
                    B_MBLubricationTimer = 0;
                    if( ( V_ControllerRpm >= P_LubricationMB_MinRotorRPM) &&
                        ( MANAGER_MODE_ENABLE == V_Lub_Equip1_Mode ) )
                    {
                        ResetCycle(&LubricationMB_StrokeCtrl, DI_LubricationMB_PumpFault);
						      StartCycle(&LubricationMB_StrokeCtrl);
                        State = MB_LUB_AUTO_ON;
                    }
                    else
                    {
                        StopCycle(&LubricationMB_StrokeCtrl);
                        State = MB_LUB_SUSPENDED;
                    }
                }
                else
                {
                    TimeToText(TimerLeft(&MBLubrication_OffTimer), V_LubricationMB_TimeIndicationTxt, sizeof(V_LubricationMB_TimeIndicationTxt));
                    B_MBLubricationTimer = TimerLeft(&MBLubrication_OffTimer);  //  Backup timer
                }
            }
            break;

            case MB_LUB_SUSPENDED:
            {
                DO_LubricationMB_Pump = MB_LUB_PUMP_OFF;
                V_Lub_Equip1_State = STATE_SUSPENDED;
                strncpy(V_LubricationMB_TimeIndicationTxt, NA_TEXT, sizeof(V_LubricationMB_TimeIndicationTxt));
                if( ( V_ControllerRpm >= P_LubricationMB_MinRotorRPM) &&
                    ( MANAGER_MODE_ENABLE == V_Lub_Equip1_Mode ) &&
                    ( 0 == V_Inverter_StatusLVRT ) )
                {
                    StartCycle(&LubricationMB_StrokeCtrl);
                    State = MB_LUB_AUTO_ON;
                }
                if ( V_Inverter_StatusLVRT )                               { V_Lub_Equip1_Status = MB_LUB_STATUS_LVRT; }
                else if (MANAGER_MODE_DISABLE == V_Lub_Equip1_Mode)        { V_Lub_Equip1_Status = MB_LUB_STATUS_LM_BLOCK; }
                else if( V_ControllerRpm < P_LubricationMB_MinRotorRPM ) { V_Lub_Equip1_Status = MB_LUB_STATUS_ROTOR_SPEED; }
                else                                                       { V_Lub_Equip1_Status = MB_LUB_STATUS_OK; }
            }
            break;

            case MB_LUB_AUTO_ON:
            {
                V_Lub_Equip1_State = STATE_AUTO_ON;
                if( WaitCycle(DI_LubricationMB_PumpFault, P_LubricationMB_Stroke_ON, &LubricationMB_StrokeCtrl) )
                {
                    TimerReset(&MBLubrication_OffTimer);
                    State = MB_LUB_AUTO_OFF;
                }
                else
                {
                    if(( MANAGER_MODE_DISABLE == V_Lub_Equip1_Mode) || V_Inverter_StatusLVRT)
                    {
                        DO_LubricationMB_Pump = MB_LUB_PUMP_OFF;
                        StopCycle(&LubricationMB_StrokeCtrl);
                        State = MB_LUB_SUSPENDED;
                    }
                    else
                    {
                        DO_LubricationMB_Pump = MB_LUB_PUMP_ON;
                    }
                }
            }
            break;

            default: { } break;
        }
    }
}
