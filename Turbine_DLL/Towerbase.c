static void VentilationCtrl( void )
{
    // Ventilation 1
     switch( P_TB_Ventilation1Mode )
     {
         case MODE_AUTO :
         {
             if( P_TB_Ventilation1TempON < PT_TowerBaseTemp )
             {
                 DO_TB_Ventilation1_ON = ON;
             }
             if( (P_TB_Ventilation1TempOFF > PT_TowerBaseTemp) || (ON == DO_TB_Ventilation2_ON) )
             {
                 DO_TB_Ventilation1_ON = OFF;
             }
         }
         break;
         case MODE_SERVICE_ON :
         {
             DO_TB_Ventilation1_ON = ON;
         }
         break;
         case MODE_SERVICE_OFF :
         default:
         {
             DO_TB_Ventilation1_ON = OFF;
         }
         break;
     }
     SetVentilation1State();

     // Ventilation 2
    switch( P_TB_Ventilation2Mode )
    {
        case MODE_AUTO :
        {
          if( P_TB_Ventilation2TempON < PT_TowerBaseTemp )
          {
              DO_TB_Ventilation2_ON = ON;
          }
          else if( P_TB_Ventilation2TempOFF > PT_TowerBaseTemp )
          {
              DO_TB_Ventilation2_ON = OFF;
          }
        }
        break;
        case MODE_SERVICE_ON :
        {
          DO_TB_Ventilation2_ON = ON;
        }
        break;
        case MODE_SERVICE_OFF :
        default:
        {
          DO_TB_Ventilation2_ON = OFF;
        }
        break;
    }
    SetVentilation2State();

    // Ventilation 3
    switch( P_TB_Ventilation3Mode )
    {
        case MODE_AUTO :
        {
           if( P_TB_Ventilation3TempON < PT_TowerBaseTemp )
           {
               DO_TB_Ventilation3_ON = ON;
           }
           else if( P_TB_Ventilation3TempOFF > PT_TowerBaseTemp )
           {
               DO_TB_Ventilation3_ON = OFF;
           }
        }
        break;
        case MODE_SERVICE_ON :
        {
           DO_TB_Ventilation3_ON = ON;
        }
        break;
        case MODE_SERVICE_OFF :
        default:
        {
           DO_TB_Ventilation3_ON = OFF;
        }
        break;
    }
    SetVentilation3State();
}
