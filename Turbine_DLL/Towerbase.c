static STATUS LocalCtrl( void )
{
    static S32 TowerBaseTempSensorState_Old = 100500;
    static S32 TowerBaseInsulation_Old = 100500;

    // Update value to 5min log and total log
    V_TowerbaseTempLog = PT_TowerBaseTemp;

    SensorStatus();
    SensorStatusTxt(V_TowerBaseTempSensorState, &TowerBaseTempSensorState_Old, V_TowerBaseTempSensorStateTxt, sizeof(V_TowerBaseTempSensorStateTxt) );
    ResetSmokeDetector();

    GetInsulationDeviceValues();
    SetInsulationDeviceSettings();
    InsulationStatusTxt(V_IM_Alarm, &TowerBaseInsulation_Old, V_TowerBaseInsulationTxt, sizeof(V_TowerBaseInsulationTxt));

    VentilationCtrl();

    return OS_SUCCESS;
}

