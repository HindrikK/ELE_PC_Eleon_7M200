static void ResetSmokeDetector()
{
    if (V_TowerBaseSmokeDetectorReset)
    {
        DO_TowerBaseSmokeDetectorSupply = 0;
        StartTimer(&SmokeResetTimer);
        if (StandardTimer(&SmokeResetTimer))
        {
            V_TowerBaseSmokeDetectorReset = 0;
            OS_VarDB_UpdateVariable(V_TowerBaseSmokeDetectorResetID, &V_TowerBaseSmokeDetectorReset, sizeof V_TowerBaseSmokeDetectorReset);
            DO_TowerBaseSmokeDetectorSupply = 1;
        }
    }
    else
    {
        ResetTimer(&SmokeResetTimer);
        DO_TowerBaseSmokeDetectorSupply = 1;
    }
}
