            break;

        case PR_CNVINDUCTTEMP:
            sprintf(StatusTxt, "Cnv inductor temp reduction");
            break;

        case PR_CNVNACELLETEMP:
            sprintf(StatusTxt, "Nacelle temp reduction");
            break;

        case PR_WINDLOAD:
