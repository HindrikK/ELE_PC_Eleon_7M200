//***********************************************************************
// Wind load reduction
//***********************************************************************
static S32 WindLoadReduction()
{
    static U8 step = 0;
    static S32 reducedSP = 0;
    static S32 PowerSetpoint_beforePR;


    switch (step)
    {
        case 0:  // NO REDUCTION
        {
            U16 windLimitShift = Linear_Conversion(V_WIN_WindTurbulence,
                										 P_PR_WindTurbulenceLimit1,
                										 P_PR_WindTurbulenceLimit2,
                										 P_PR_WindSpeedLimitRed1,
                										 P_PR_WindSpeedLimitRed2);
            
            U16 speedLimit1 = P_PR_WindSpeedLimit1 - windLimitShift;
            U16 speedLimit2 = P_PR_WindSpeedLimit2 - windLimitShift;
            
            if (V_WindSpeed30Sec > speedLimit1)
            {
                // check what will be the reduction
                reducedSP = Linear_Conversion(V_WindSpeed30Sec,
                                              speedLimit1,
                                              speedLimit2,
                                              P_PR_WindSpeedPwrLimit1,
                                              P_PR_WindSpeedPwrLimit2);

                // activate only if reduction is lower than current setpoint and current active power
                if (reducedSP < V_PM_ActivePowerSetpointW && reducedSP < V_GridRealPower3Sec)
                {
                    step = 1;
                }
            }
            return P_PM_P_Max_kW;
        }
        case 1:  // ACTIVATING REDUCTION
        {
            LoadParameters();

            PowerSetpoint_beforePR = V_PM_ActivePowerSetpointW;

            RateLim_Reset(WindReduction_Limiter, PowerSetpoint_beforePR/*Output*/);

            step = 2;
            return P_PM_P_Max_kW;
        }
        case 2:  // REDUCTION ACTIVE
        {
            if (MinuteTick)
            {
                B_PR_WindTime++;
                OS_VarDB_UpdateVariable(B_PR_WindTime_ID, &B_PR_WindTime, sizeof(B_PR_WindTime));
            }

            U16 windLimitShift = Linear_Conversion(V_WIN_WindTurbulence,
                										 P_PR_WindTurbulenceLimit1,
                										 P_PR_WindTurbulenceLimit2,
                										 P_PR_WindSpeedLimitRed1,
                										 P_PR_WindSpeedLimitRed2);
                
            U16 speedLimit1 = P_PR_WindSpeedLimit1 - windLimitShift;
            U16 speedLimit2 = P_PR_WindSpeedLimit2 - windLimitShift;
            
            // get reduced setpoint based on windspeed and then add reduction based on wind speed change value(turbulence)
            if (V_WindSpeed30Sec > speedLimit1)
            {
                reducedSP = Linear_Conversion(V_WindSpeed30Sec,
                                              speedLimit1,
                                              speedLimit2,
                                              P_PR_WindSpeedPwrLimit1,
                                              P_PR_WindSpeedPwrLimit2);
            }
            else
            {
                reducedSP = P_PM_P_Max_kW;
            }

            reducedSP = RateLim_Control(WindReduction_Limiter, reducedSP);

            // CHECKING DEACTIVATION CONDITIONS
            if (reducedSP >= PowerSetpoint_beforePR)
            {
                step = 0;
            }
            else if (V_ControllerRpm < 2000)	// if turbine not running(speed less than 2,000 rpm)
            {
               step = 0;
            }

            break;
        }
    }

    return reducedSP;
}



