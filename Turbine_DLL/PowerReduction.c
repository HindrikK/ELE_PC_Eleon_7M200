// if no reduction, returns max power setpoint(P_PM_P_Max_kW)


//***********************************************************************
// Generator temperature reduction
//***********************************************************************
static S32 GenTempReduction()
{
    static U8 step = 0;
    static S32 reducedSP = 0;
    static S32 ActivePower_beforePR;
    static S32 PowerSetpoint_beforePR;


    switch (step)
    {
        case 0:  // NO REDUCTION
        {
            if (V_GENC_MaxGeneratorTemp > P_PR_GeneratorTempLimit)
            {
                if (V_GridRealPower3Sec > P_PR_MinPower)    // do not activate reduction function if actual power is lower than this limit
                {
                    step = 1;
                }
            }
            return P_PM_P_Max_kW;
        }
        case 1:  // ACTIVATING REDUCTION
        {
            if (V_GridRealPower3Sec > V_PM_ActivePowerSetpointW)    // if actual power is higher than power setpoint, then use power setpoint as limit
            {
                ActivePower_beforePR = V_GridRealPower3Sec;
            }
            else { ActivePower_beforePR = V_PM_ActivePowerSetpointW; }
            PowerSetpoint_beforePR = V_PM_ActivePowerSetpointW;

            F32 PImin = (-ActivePower_beforePR + P_PR_MinPower) / 1000;
            F32 PImax;

            LoadParameters();

            PImax = (P_PM_P_Max_kW - ActivePower_beforePR) / 1000;

            PI_LoadParameters(GenTemp_PI, P_PR_GeneratorTempKp/*Kp*/, P_PR_GeneratorTempKi/*Ki*/, PImin/*Min*/, PImax/*Max*/);
            PI_Reset(GenTemp_PI);

            step = 2;
            break;
        }
        case 2:  // REDUCTION ACTIVE
        {
            if (MinuteTick)
            {
                B_PR_GeneratorTempTime++;
                OS_VarDB_UpdateVariable(B_PR_GeneratorTempTime_ID, &B_PR_GeneratorTempTime, sizeof(B_PR_GeneratorTempTime));
            }

            // reduction is not adjusted if actual power is more than 100 kW lower than "V_PM_ReducedSetPoint_kW". It means there is not enough wind for this power.
            // When temperature is over the limit, then it will adjust reduced power setpoint even if there is not enough wind for this power.
            if ((V_GridRealPower3Sec + 100000) > V_PM_ReducedSetPoint_kW || V_GENC_MaxGeneratorTemp > P_PR_GeneratorTempLimit)
            {
                reducedSP = ActivePower_beforePR + PI_Control(GenTemp_PI, (F32)P_PR_GeneratorTempLimit / 10, (F32)V_GENC_MaxGeneratorTemp / 10) * 1000; // * 1000 used for [W]->[kW]
            }

            // CHECKING DEACTIVATION CONDITIONS
            if (V_GENC_MaxGeneratorTemp < P_PR_GeneratorTempOff || reducedSP > PowerSetpoint_beforePR)
            {
                step = 0;
            }
            else if (V_ControllerRpm < 2000)	// if turbine not running(speed less than 2,000 rpm)
            {
               step = 0;
            }

            break;
        }
    }

    return reducedSP;
}



