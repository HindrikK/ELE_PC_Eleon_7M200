static STATUS StandardAverage(U8 ModuleState, U8* DoneState, U32 Interval)
{
	switch (ModuleState)
	{
	case APP_RUN:
	{
		Mid2StateUpdate();

		*DoneState = APP_RUN;
		break;
	}
	//__________________________________________________________________________
	case APP_INIT1:
	{
		*DoneState = APP_INIT1;
		break;
	}
	//__________________________________________________________________________
	case APP_INIT2:
	{
		*DoneState = APP_INIT2;
		break;
	}
	//__________________________________________________________________________
	case APP_INIT3:
	{
		*DoneState = APP_INIT3;
		break;
	}
	//__________________________________________________________________________
	case APP_INIT4:
	{
		*DoneState = APP_INIT4;
		break;
	}
	//__________________________________________________________________________
	case APP_INIT5:
	{
		*DoneState = APP_INIT5_DONE;
		break;
	}
	//__________________________________________________________________________
	case AS_SHUTDOWN:
	{
		*DoneState = AS_SHUTDOWN;
		break;
	}
	//__________________________________________________________________________
	case AS_REBOOT:
	{
		*DoneState = AS_REBOOT;
		break;
	}
	//__________________________________________________________________________
	case AS_SHUTDOWN_FAST:
	{
		*DoneState = AS_SHUTDOWN_FAST;
		break;
	}
	//__________________________________________________________________________
	default:
	{
		*DoneState = ModuleState;
		break;
	}
	}

	return OS_SUCCESS;
}



static STATUS log_app_state_requests(U8 request)
{
    if ( AS_RUN > request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": INIT ROUND %u", request );
        //OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": INIT ROUND %u", request );
    }
    else if ( AS_RUN == request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": RUN" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": RUN" );
    }
    else if ( AS_SHUTDOWN == request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": SHUTDOWN" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": SHUTDOWN" );
    }
    else if ( AS_REBOOT == request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": REBOOT" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": REBOOT" );
    }
    else if ( AS_SHUTDOWN_FAST == request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": SHUTDOWN FAST" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": SHUTDOWN FAST" );
    }
    else if ( AS_SHUTDOWN_DONE == request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": SHUTDOWNDONE" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": SHUTDOWNDONE" );
    }
    else if ( AS_FAILED == request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": FAILED" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": FAILED" );
    }
    else if ( AS_TERMINATED == request )
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": TERMINATED" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": TERMINATED" );
    }
    else
    {
        AppSystemLog           (APPSYSLOGCODE, APP_ID": UNKNOWN STATE" );
        OS_EVENTLOG_make_entry (APPSYSLOGCODE, APP_ID": UNKNOWN STATE" );
    }

    return OS_SUCCESS;
}

static STATUS log_app_state_done(U8 done_state)
{
    if ( AS_RUN > done_state )
    {
        AppSystemLog           ( APPSYSLOGCODE, APP_ID": INIT ROUND %u DONE", done_state );
        //OS_EVENTLOG_make_entry ( APPSYSLOGCODE, APP_ID": INIT ROUND %u DONE", done_state );
    }
    else if ( AS_RUN == done_state )
    {
        AppSystemLog           ( APPSYSLOGCODE, APP_ID": RUN DONE" );
        OS_EVENTLOG_make_entry ( APPSYSLOGCODE, APP_ID": RUN DONE" );
    }
    else if ( AS_SHUTDOWN == done_state )
    {
        AppSystemLog           ( APPSYSLOGCODE, APP_ID": SHUTDOWN DONE" );
        OS_EVENTLOG_make_entry ( APPSYSLOGCODE, APP_ID": SHUTDOWN DONE" );
    }
    else if ( AS_REBOOT == done_state )
    {
        AppSystemLog           ( APPSYSLOGCODE, APP_ID": REBOOT DONE" );
        OS_EVENTLOG_make_entry ( APPSYSLOGCODE, APP_ID": REBOOT DONE" );
    }

    else if ( AS_SHUTDOWN_FAST == done_state )
    {
        AppSystemLog           ( APPSYSLOGCODE, APP_ID": SHUTDOWN FAST DONE" );
        OS_EVENTLOG_make_entry ( APPSYSLOGCODE, APP_ID": SHUTDOWN FAST DONE" );
    }
    else if ( AS_SHUTDOWN_DONE == done_state )
    {
        AppSystemLog           ( APPSYSLOGCODE, APP_ID": SHUTDOWN_DONE DONE" );
        OS_EVENTLOG_make_entry ( APPSYSLOGCODE, APP_ID": SHUTDOWN_DONE DONE" );
    }
    else
    {
        AppSystemLog           ( APPSYSLOGCODE, APP_ID": UNKNOWN STATE DONE" );
        OS_EVENTLOG_make_entry ( APPSYSLOGCODE, APP_ID": UNKNOWN STATE DONE" );
    }

    return OS_SUCCESS;
}


