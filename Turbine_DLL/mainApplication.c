void  app_mainApplication(UNSIGNED Argc, struct application_arguments *Argv)
{

	static STATUS Status = OS_SUCCESS;

	static OS_EVENT_GROUP EvGr_ReqStateChahge;
	static SUBSCRIPTION   Sbcr_ReqStateChahge;
	static UNSIGNED       ReqEv_ReqStateChahge,
		RetEv_ReqStateChahge;

	static U8             first_time_init = 1;
	static U8             emulate_req_state = 0;
	static U8             ReqState = 0;

	static U8             CurState = 0;
	static U8             AppTasksCreated = 0;

	AppSystemLog(APPSYSLOGCODE, APP_ID": Initializing");

	// Get the id of system variables 
	Set_App_RequestId_StateId(Argv->RequestId, Argv->StateId);
	SetMemPool(&Argv->Pool);

	MemPool = &Argv->Pool;      //Backward Compatibility

	Status = SetupParameterBackupFileNames(Argv->StateId);
	STATUS_LINE_VAR_RETURN(Status);

	//______________________________________________________________________________
	//Create Application Menu
	Status = AppMenuInit();              STATUS_LINE_VAR_RETURN(Status);


#ifdef MAIN_APP
   Status = Set_Sts_ControlPrgNames();  STATUS_LINE_VAR_RETURN(Status);
#endif
    //--------------------------------------------------------------------------------
    //Configurator Object

	//--------------------------------------------------------------------------------
	Status = AddTasks();                             STATUS_LINE_VAR_RETURN(Status);
	//--------------------------------------------------------------------------------
	Status = AssignFuctionCalls();                   STATUS_LINE_VAR_RETURN(Status);
	//--------------------------------------------------------------------------------
	Status = Set_Obj_Init_Order();                   STATUS_LINE_VAR_RETURN(Status);
	//--------------------------------------------------------------------------------
	Status = Set_Obj_Menu_Order();                   STATUS_LINE_VAR_RETURN(Status);
	//--------------------------------------------------------------------------------

	if (AppTasksCreated)
	{
		return 0;
	}
	else if (!first_time_init) {
		return -2;
	}


	//___________________________________________________________________________________________________
	while (TRUE)
	{
		if (first_time_init)
		{
			first_time_init = 0;

			if (APP_RUN == ReqState)
			{
				emulate_req_state = 1;
				ReqState = APP_INIT1;
				AppSystemLog(APPSYSLOGCODE, APP_ID": Asynchronous Starting");
			}
		}
		else  //emulate Init rounds for Async Application
		{
			if (APP_RUN > CurState)
			{
				ReqState = CurState + 1;
			}

			if (APP_RUN == ReqState)
			{
				emulate_req_state = 0;
			}
		}

		SetAppState(ReqState);

		log_app_state_requests(ReqState);

		Request = ReqState;   //Backward Compatibility


		if (APP_INIT1 == ReqState) //IO Channels Auto Detection
		{
			InitIo();
		}

		if (!StartAppTaskBeforeInitRounds)  //Init rounds from 0 89 executed in one task
		{
			if (APP_RUN > ReqState)
			{
				Status = AppTasks_InitCall(ReqState, &CurState);
                if (Status < 0)
                {
                    printf("\nINITIALIZATION FAILED\nAppTasks_InitCall returned error: %d\n\n", Status);
                }
				STATUS_LINE_VAR_RETURN(Status);

				if (APP_INIT5_DONE == CurState && !AppTasksCreated)
				{

					Status = AppTasks_Create(&Argv->Pool);
                    if (Status < 0)
                    {
                        printf("Error status: %d", Status);
                    }
					STATUS_LINE_VAR_RETURN(Status);
					AppSystemLog(APPSYSLOGCODE, APP_ID": Tasks Created");
					STATUS_LINE_VAR_RETURN(Status);
					AppTasksCreated = 1;

					return OS_SUCCESS;
				}

				//confirmation to OS that state done
				Status = OS_VarDB_UpdateU8(Get_StateId(), CurState);
                if (Status < 0)
                {
                    printf("Error status: %d", Status);
                }
				STATUS_LINE_VAR_RETURN(Status);
			}
		}
	}
	
    return -1;
}

