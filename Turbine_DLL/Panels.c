static void SensorStatusTxt( S32 Status, S32 *OldStatus, char *Text, S32 Size )
{
    if( Status != *OldStatus )
    {
        switch( Status )
        {
            case SENSOR_STATUS_OK:
            {
                strncpy( Text, "OK", Size );
            }
                break;
            case SENSOR_STATUS_DISABLED:
            {
                strncpy( Text, "Disabled", Size );
            }
                break;
            case SENSOR_STATUS_ERROR:
            {
                strncpy( Text, "Sensor Error", Size );
            }
                break;
            default:
            {
                strncpy( Text, "WARNING", Size );
            }
                break;
        }
    }

    *OldStatus = Status;
}

