static void SensorStatus( void )
{
    if( !P_TowerControlPanelTempEnable )
    {
        V_TowerPanelTempSensorState = SENSOR_STATUS_DISABLED;
    }
    else if( (PT_TowerControlPanelTemp > P_PanelsTempErrHigh) || (PT_TowerControlPanelTemp < P_PanelsTempErrLow) )
    {
        V_TowerPanelTempSensorState = SENSOR_STATUS_ERROR;
    }
    else
    {
        V_TowerPanelTempSensorState = SENSOR_STATUS_OK;
    }

    if( !P_NacelleControlPanelTempEnable )
    {
        V_NacellePanelTempSensorState = SENSOR_STATUS_DISABLED;
    }
    else if( (PT_NacelleControlPanelTemp > P_PanelsTempErrHigh) || (PT_NacelleControlPanelTemp < P_PanelsTempErrLow) )
    {
        V_NacellePanelTempSensorState = SENSOR_STATUS_ERROR;
    }
    else
    {
        V_NacellePanelTempSensorState = SENSOR_STATUS_OK;
    }

    if( !P_PowerPanelLeftSideTempEnable )
    {
        V_PowerPanelLSTempSensorState = SENSOR_STATUS_DISABLED;
    }
    else if( (PT_PowerPanelLeftSideTemp > P_PanelsTempErrHigh) || (PT_PowerPanelLeftSideTemp < P_PanelsTempErrLow) )
    {
        V_PowerPanelLSTempSensorState = SENSOR_STATUS_ERROR;
    }
    else
    {
        V_PowerPanelLSTempSensorState = SENSOR_STATUS_OK;
    }

    if( !P_PowerPanelRightSideTempEnable )
    {
        V_PowerPanelRSTempSensorState = SENSOR_STATUS_DISABLED;
    }
    else if( (PT_PowerPanelRightSideTemp > P_PanelsTempErrHigh) || (PT_PowerPanelRightSideTemp < P_PanelsTempErrLow) )
    {
        V_PowerPanelRSTempSensorState = SENSOR_STATUS_ERROR;
    }
    else
    {
        V_PowerPanelRSTempSensorState = SENSOR_STATUS_OK;
    }

    if( !P_HubControlPanelTempEnable )
    {
        V_HubPanelTempSensorState = SENSOR_STATUS_DISABLED;
    }
    else if( (PT_HubControlPanelTemp > P_PanelsTempErrHigh) || (PT_HubControlPanelTemp < P_PanelsTempErrLow) )
    {
        V_HubPanelTempSensorState = SENSOR_STATUS_ERROR;
    }
    else
    {
        V_HubPanelTempSensorState = SENSOR_STATUS_OK;
    }
}
